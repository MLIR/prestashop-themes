<h3>Update 1</h3>
<p>In order to install the Styler v1.1 update pack over pre-configured site, please perform following steps:</p>

<ol class="index-list">
    <li><p>Copy the archive content styler_v1.1.zip into the root directory of your site via FTP or hosting account File Manager;</p></li>
    <li><p>Go to the Prestashop dashboard -> "Modules":
        <ul class="marked-list">
            <li>
                <p>find and install the "TM Google Map" module;</p>
                <figure class="img-polaroid">
                    <img alt="" src="img/08-update-1.png">
                </figure>
            </li>
            <li>
                <p>add following module to the topColumn hook and define position 1 for it</p>
                <figure class="img-polaroid">
                    <img alt="" src="img/08-update-2.png">
                </figure>
            </li>
        </ul>
    </li>
    <li><p>To add new pages, you will need to go to the Preferences -> CMS:</p>
        <ul class="marked-list">
            <li><p>Click Add new</p></li>
            <li><p>Specify the name, url and other required fields</p></li>
            <li><p>Click Source code</p></li>
            <li><p>Insert code from txt file included within the updated package in to the apropriate popup window.</p></li>
        </ul>
    </li>
</ol>

