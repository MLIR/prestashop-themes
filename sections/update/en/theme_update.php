<h3>How to update Eveprest</h3>
<p class="alert alert-warning">The changes you make may affect your existing store, so you are strongly suggested to back up your site before updating it.</p>
<p>To get the updated version of Eveprest, you'll need to restore the download link. Next, download the Eveprest archive again.</p>
<p>To use new theme, you need to add it using the instructions in Template Installation section.</p>
<p>To add and use new modules, open the update folder in flagman archive, copy the corresponding folders to your /modules folder and install them from the admin panel.</p>

