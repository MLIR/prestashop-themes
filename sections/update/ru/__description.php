<h2 class="item7">Обновление</h2>

<p>Для того чтоб установить пакет обновлений на уже установленный шаблон, Вам необходимо:</p>

<ol class="index-list">
    <li><p>Загрузить содержимое архива update_1.zip в корень вашего сайта;</p></li>
    <li><p>Зайти в админ панель сайта, в раздел "Модуля":
        <ul class="marked-list">
            <li>
                <p>найти и установить модуль "Tm Google Map"</p>
                <figure class="img-polaroid">
                    <img alt="" src="img/08-update-1.png">
                </figure>
            </li>
            <li>
                <p>добавить этот модуль в хук topColumn на 1 позицию</p>
                <figure class="img-polaroid">
                    <img alt="" src="img/08-update-2.png">
                </figure>
            </li>
        </ul>
    </li>
    <li><p>Для того чтоб добавить новые страницы, нужно зайти в раздел Preferences -> CMS</p>
        <ul class="marked-list">
            <li><p>Нажать кнопку Add new</p></li>
            <li><p>Указать название, url и отметить остальные опции</p></li>
            <li><p>Нажать кнопку Source code</p></li>
            <li><p>Скопировать в появившееся окно содержимое файла соответсвующей страницы из папки cms_pages</p></li>
        </ul>
    </li>
</ol>

