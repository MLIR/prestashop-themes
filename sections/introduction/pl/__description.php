<h2 class="item1">Wstęp</h2>

<h3>Czym jest PrestaShop e-commerce?</h3>

<p>PrestaShop jest rozwiązaniem e-commerce, które jest bezpłatne dla podstawowego jądra i open source. Obsługuje sposoby
    płatności, takie jak: Google Checkout, PayPal lub płatności pro poprzez API.</p>

<p>PrestaShop jest dostępny w ramach Licencji Wolnego Oprogramowania i oficjalnie został otwarty w sierpniu 2007 roku
    dla małych i średnich przedsiębiorstw.Oprogramowanie, które oparte jest na silniku szablonów Smarty, jest używane
    przez kilka tysięcy sklepów na całym świecie.</p>
<h3>Czym jest szablon PrestaShop?</h3>

<p>Szablon PrestaShop jest motywem dla PrestaShop e-commerce. Oferuje ogromne możliwości dostosowywania i pozwala na
    stworzenie sklepu internetowego, który w pełni spełnia Twoje oczekiwania.</p>

