<h2 class="item1">Introducci&oacute;n</h2>

<h3>&iquest;Qu&eacute; es el comercio electr&oacute;nico PrestaShop?</h3>

<p>PrestaShop es una soluci&oacute;n de comercio electr&oacute;nico gratuita para el kernel b&aacute;sico y de c&oacute;digo
    abierto. Es compatible con servicios de pago como Google Checkout, PayPal o pagos mediante el uso de APIs.</p>

<p>PrestaShop est&aacute; disponible bajo licencia de software abierto y fue lanzado oficialmente en agosto de 2007 para
    PYMES (peque&ntilde;as y medianes empresas peque&ntilde;as. El software que est&aacute; basado en el motor de
    plantillas Smarty es utilizado por miles de tiendas en todo el mundo.</p>
<h3>&iquest;Qu&eacute; es la plantilla de PrestaShop?</h3>

<p>La plantilla de PrestaShop es un tema para la soluci&oacute;n de comercio electr&oacute;nico PrestaShop. Le ofrece
    una gran variedad de posibilidades de personalizaci&oacute;n y le permite crear una tienda en l&iacute;nea que
    cumpla plenamente sus necesidades.</p>

