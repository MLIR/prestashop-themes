<h2 class="item1">Introduction</h2>

<h3>Qu'est-ce que PrestaShop e-commerce?</h3>

<p>PrestaShop est une solution d'e-commerce gratuite pour le noyau de base et open source. Elle prend en charge les
    plate-formes de paiement telles que Google checkout, paypal ou des paiements professionnels via des API.</p>

<p>PrestaShop est disponible sous Licence Open Software et a été lancée en Août 2007 pour les petites et moyennes
    entreprises. Le logiciel, qui est basé sur le moteur de template Smarty, est utilisé par des milliers de boutiques
    dans le monde.</p>
<h3>Qu'est-ce que PrestaShop template?</h3>

<p>PrestaShop template est un thème pour la solution d'e-commerce PrestaShop. Il propose une large variété d'options de
    personnalisation et permet de créer une boutique en ligne entièrement adaptée à vos besoins.</p>

