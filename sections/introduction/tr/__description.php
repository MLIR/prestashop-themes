<h2 class="item1">Giriş</h2>

<h3>PrestaShop e-ticaret nedir?</h3>

<p>PrestaShop, temel kernel için kullanabileceğiniz açık kaynaklı bir e-ticaret çözümüdür. Google checkout, Paypal veya
    program arabirimi aracılığıyla yapılan ödemeleri desteklemektedir.</p>

<p>PrestaShop, küçük ve orta ölçekli kurumlar için Ağustos 2007 yılında Açık Kaynak Kodlu Yazılım Sözleşmesi ile
    piyasaya sürülmüştür. Smarty şablonunu temel alan yazılım, dünyada binlerce çeşitli mağaza tarafından
    kullanılmıştır.</p>
<h3>PrestaShop şablonu nedir?</h3>

<p>PrestaShop şablonu, PrestaShop e-ticaret çözümü için kullanılan bir temadır. Geniş düzenleme seçenekleriyle size,
    online mağazanız için tüm ihtiyaçlarınızı karşılayabilecek imkan sunar.</p>

