<h2 class="item1">Introduzione</h2>

<h3>Cos'è PrestaShop e-commerce?</h3>

<p>PrestaShop è una soluzione e-commerce open source e gratuita (con il kernel di base). Supporta vari canali di
    pagamento come Google Checkout, PayPal o pagamenti via API.</p>

<p>PrestaShop è disponibile su Licenza Open Software ed è stato ufficialmente introdotto nel 2007, pensato per aziende
    di medie o piccole dimensioni. Il software, basato sul motore Smarty template, è ormai utilizzato da migliaia di
    negozi in tutto il mondo.</p>
<h3>Cos'è PrestaShop template?</h3>

<p>PrestaShop template è un tema per la soluzione e-commerce PrestaShop. Offre all'utente ampie possibilità di
    personalizzazione e quindi di creare uno store online che risponda davvero alle sue esigenze.</p>

