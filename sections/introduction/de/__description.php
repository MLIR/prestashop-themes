<h2 class="item1">Einführung</h2>

<h3>Was ist eigentlich die PrestaShop Plattform?</h3>

<p>PrestaShop ist eine kostenlose Plattform für den Internethandel mit dem Open Source. Sie unterstützt Zahlungssysteme
    wie Google checkout, paypal oder payments pro per APIs.</p>

<p>PrestaShop ist unter den Bedingungen der Open Source Software (Open Software Licence) verfügbar und wurde im August
    2007 offiziell für kleine und mittlere Unternehmen gestartet. Diese Plattform wurde auf der Basis von Smarty Engine
    entwickelt, die Tausende Online-Shops weltweit verwenden.</p>
<h3>Was ist eigentlich eine PrestaShop Vorlage?</h3>

<p>PrestaShop Vorlage ist das Thema für die Internethandel-Plattform PrestaShop. Sie bietet breite Möglichkeiten zur
    Bearbeitung und ermöglicht eine Online-Shop zu erstellen, der völlig Ihren Anforderungen entsprechen wird.</p>

