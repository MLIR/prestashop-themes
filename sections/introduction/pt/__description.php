<h2 class="item1">Introdução</h2>

<h3>O que é a PrestaShop e-commerce?</h3>

<p>PrestaShop é uma solução de comércio eletrônico que tem um núcleo básico grátis e de código aberto. Ele suporta
    portais de pagamento como o Google checkout, paypal ou pagamentos pro via APIs.</p>

<p>PrestaShop está sob licença Open Software License e foi lançado oficialmente em Agosto de 2007 para empresas de
    pequeno e médio-porte. O programa, que é baseado no motor de template Smarty, é usado em milhares de lojas no mundo
    todo.</p>

<h3>O que é um template do PrestaShop?</h3>

<p>Template do PrestaShop é um tema para sua solução de comércio PrestaShop. Isso te possibilita uma grande variedade de
    oportunidades de personalizar seu site e permite que você crie uma loja online que atende as suas necessidades.</p>

