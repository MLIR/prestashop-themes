<h2 class="item1">Введение</h2>

<h3>Что представляет собой платформа PrestaShop?</h3>

<p>PrestaShop - это бесплатная платформа электронной коммерции с открытым исходным кодом. Она поддерживает платежные
    системы, такие как Google checkout, paypal или payments pro через APIs.</p>

<p>PrestaShop доступен под лицензией открытого программного обеспечения (Open Software Licence) и официально запущен в
    августе 2007 года для малого и среднего бизнеса. Программа, основанная на движке Smarty, используется тысячами
    магазинов по всему миру.</p>
<h3>Что представляет собой шаблон PrestaShop?</h3>

<p>Шаблон PrestaShop - это тема для платформы электронной коммерции PrestaShop. Он предоставляет широкие возможности
    редактирования и позволяет создать магазин, который бы полностью соответствовал всем Вашим потребностям.</p>

