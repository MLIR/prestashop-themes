<h4>Smart Blog</h4>

<p>Bu, blog işlevselliği için gerekli olan tüm ayarlamaları kapsayan ana modüldür. Yüklendikte sonra, yönetici
    panelinize yerleşerek size yazıları/kategorileri ekleyip/kaldırmak/düzenlemek imkanı sunar. Ayrıca yazılarınızdaki
    görselleri ve boyutlarını da düzenleyebilirsiniz. Modülün tüm var olan ayarlarını düzenlemek için
    <strong>Smart Blog (Modules->Front Office Features->Smart Blog)</strong>
    bölümüne girmelisiniz.
</p>

