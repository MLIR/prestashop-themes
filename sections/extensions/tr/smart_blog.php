<h4>RSS Smart Blog Feed</h4>

<p>Blogunuz için yeni bir feed oluşturan modül.</p>

<p>
    <strong>Yönetici panelinde pek çok ayar seçeneği bulunmaktadır:</strong>
</p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Update Period</dt>
            <dd>yazıları belirli zaman perdiyoduna (saat/gün/hafta/ay/yıl) göre göstermeye yarar.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Update Frequency</dt>
            <dd>güncelleme sıklığını ayarlar (1 hayfa, 2 saat vs.).</dd>
        </dl>
    </li>
</ol>
<p>Üst kısımda, haberlerin nerede yer alacağına dair dosya yolu bulunmaktadır. Burada website URL +
    /modules/smartblogfeed/rss.php yer alır.</p>

