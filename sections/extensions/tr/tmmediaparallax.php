<h5>
    Media parallax modülü eklemek
</h5>
<ol class="index-list">
    <li>HTML sayfanızın içeriğine bir id veya class eklemeniz gerekir. İsterseniz tekrarlanmayacak özgün bir etiket de
        ekleyebilirsiniz. </li>

    <li>Uygun olan boyuttaki görseli (efektin ekleneceği bloktan daha büyük bir görsel hazırlanamanız önerilir)
        oluşturun. Video eklemek isterseniz, 3 format kullanabilirsiniz : .mp4,.webm ve .ogv, ayrıca aynı boyutta bir ön
        gösterim görseli (.png, .jpg ve .gif) de hazırlamalısınız. </li>

    <li>
        <p>Şimdi
            <strong>Add new block</strong>
            kısmında yer alan
            <strong>Add container</strong>
            bölümüne tıklayın.
        </p>

        <figure class="img-polaroid">
            <img src="img/paralax-1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Açılan ekranda tüm gerekli ayarları ekleyin.</p>


        <h6>
            <strong>Ayarlar:</strong>
        </h6>

        <ol class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Selector</dt>
                    <dd>efektin ekleneceği element (id veya özgün etiket).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Parallax type</dt>
                    <dd>kullanılacak olan efekt türü (görsel veya video).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Media files</dt>
                    <dd>istenen tüm dosyaları yükleyin.</dd>
                </dl>
            </li>
            <li>Ve
                <strong>Save</strong>
                e tıklayın.
            </li>
