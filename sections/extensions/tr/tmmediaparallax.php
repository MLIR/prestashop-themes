<h3>Media Parallax Module</h3>

<h4>Parallax efekti nedir?</h4>

<p>Parallax scrolling, bilgisayar grafiklerinde arkaplan görsellerinin öndeki görsellerden daha yavaş olarak
    kaydırıldığı özel bir kaydırma tekniğidir. Bu şekilde video oyunlarındaki 2D derinlik ve gölgeleme sağlanır. Web
    tasarımcıları 2011 yılında parallax scrolling ile çalışmaya başladılar ve HTML5 ile CSS3'ü de tasarımlarına
    eklediler. O günden beri popülerliği artmaya başladı. Çünkü kullanıcıların daha çok dikkatini çekiyordu. Parallax
    scrolling, görselliği artıran dinamik ve interaktif bir kullanıcı deneyimi sağlamaktadır.</p>

<p>Bu modül, sitenize kolaylıkla parallax effects eklemenizi sağlar. Hem görsel hem de arkaplan videosu
    ekleyebilirsiniz.</p>


