<h3>TM Related Products</h3>

<p>Bu modül; mağazanızdaki herhangi bir ürüne, o ürüne benzer başka ürünler de eklemenizi sağlar. Tüm benzer ürünler, ürün sayfasındaki atanmış karusel içinde gösterilir.</p>

<h5>Kurulum ve Kaldırma</h5>

<p>Modül, diğer tüm PrestaShop modülleri gibi kurulup kaldırılır.</p>

<h5>Modül ayarları</h5>

<p>Modül kurulduktan sonra, modül düzenleme sayfasından tüm ayarları yapabilirsiniz.</p>

<p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-1.png" alt="" /></p>

<ol>
    <li><strong>Display price on products</strong> - ürün fiyatlarını gösterir.</li>
    <li><strong>Number of displayed products</strong> - sayfada gösterilecek olan ürün sayısı.</li>
</ol>

<p>Benzer ürünleri, <b>Katalog->Ürünler</b> kısmındaki ürün ayarları bölümünden ekleyip silebilirsiniz.</p>

<ol>
    <li>Öncelikle <b>TM Related Products</b> sekmesini açın: <p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-2.png" alt="" /></p></li>
    <li>Ürün arama alanına, ürünün adını yazın ve onu listeden seçin.</li>
    <li>Listeden ürün silmek için, ürünün yanındaki “Çarpı” işaretine basın.</li>
    <li>İşlem bittikten sonra <b>Kaydet ve Çıkma</b> veya <b>Kaydet</b> düğmelerinden birine basınız.</li>
</ol>