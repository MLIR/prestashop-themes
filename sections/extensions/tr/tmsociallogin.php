<h3>TM Social Login</h3>

<p>Bu modül, Facebook ve Google gibi sosyal aðlardaki kullanýcý verileriyle kayýt ve
    giriþ yapýlmasýný saðlar.</p>
<h5>Modüle kurulum/kaldýrma</h5>

<p>Bu modül de, diðer tüm PrestaShop modülleri gibi kurulup kaldýrýlabilir.</p>
<h5>Modül ayarlarý</h5>

<p>Modül kurulduktan sonra tüm düzenlemeleri modülün ayar sayfasýndan
    yapabilirsiniz.</p>

<p>Modül ayarlarýnýn yapýlabilmesi için gerekli veriyi almak üzere, ilgili kaynaktan
    uygulama oluþturmanýz gerekir.</p>

<p>Ayný anda hem Google hem de Facebook kullanýmý istemiyorsanýz, bir tanesini de
    seçebilirsiniz.</p>
