<h3>TM Related Products</h3>

<p>Bu modül ürün sayfanýzda, o ürünle ilgili olan diðer ürünleri gösterir.</p>
<h5>Modüle kurulum/kaldýrma</h5>

<p>Bu modül de, diðer tüm PrestaShop modülleri gibi kurulup kaldýrýlabilir.</p>
<h5>Modüle ayarlarý</h5>

<p>Modül kurulduktan sonra tüm düzenlemeleri modülün ayar sayfasýndan
    yapabilirsiniz.</p>

<figure class="img-polaroid">
    <img src="img/tmrelatedproducts-1.png" alt="">
</figure>
<ol class="index-list">
    <li>Ürün fiyatýný göster.
    </li>
    <li>Gösterilecek ürün sayýsý.</li>
</ol>
<p>Ana ürünle ilgili olan ürünleri, ürün ayarlarý kýsmýndaki
    <strong>Catalog->Products</strong> bölümünden ekleyip çýkartabilirsiniz.</p>
<ol class="index-list">
    <li><strong>TM Related Products</strong> bölümüne girin.
        <figure class="img-polaroid">
            <img src="img/tmrelatedproducts-2.png" alt="">
        </figure>
    </li>
    <li>Ürün arama alanýna ürün ismini yazýn ve istediðiniz ürünü seçin;</li>
    <li>Ürünü listeden kaldýrmak için, ürünün yanýndaki X e basýn;</li>
    <li>Tüm düzenlemeleri yaptýktan sonra <strong>Save and Stay</strong> e veya
        sadece <strong>Save</strong> e basýn.
    </li>
</ol>
