<h3>TM Media Parallax 2.0</h3>
<p>Модуль предназначен для добавления эффекта параллакс на Dom-элементы страницы. В модуле предусмотрен многослойный параллакс. Также реализована возможность добавлять пользовательское фоновое видео (которое можно загрузить с компьютера на сервер) и видео с сервиса Youtube.</p>
<h4 id="tmmediaparallax-mainpanel">TM Media Parallax Main Panel</h4>
<p>Главная панель модуля, где вы можете добавлять, редактировать и удалять элементы параллакс. </p>
<figure class="img-polaroid"><img class="presta_scr2" src="img/main-panel.jpg" alt="" /></figure>
<h4 id="tmmediaparallax-add">Добавление нового элемента параллакс.</h4>
<p>Для того чтобы добавить новый элемент параллакс нужно нажать на 'плюс'(1), в правом верхнем углу главной панели.</p>
<figure class="img-polaroid"><img class="presta_scr2" src="img/main-panel-add-item.jpg" alt="" /></figure>
<p>Затем появится следующая форма.</p>
<figure class="img-polaroid"><img class="presta_scr2" src="img/add-item.jpg" alt="" /></figure>
Описания полей формы добавления элемента параллакс
<ul class="index-list">
    <li>Selector - css селектор элемента, к которому будет применяться эффект параллакс. К примеру, '#header .nav'</li>
    <li>Item status - статус элемента (если выключен, то параллакс не будет отображаться)</li>
    <li>Speed - число от 0 до 2, которое является скоростью параллакса для основного блока</li>
    <li>Offset - отступ основного слоя по оси Y.</li>
    <li>Inverse - направление параллакса</li>
    <li>Fade - опция, позволяющая настроить растворение главного блока при прокрутке страницы</li>
    <li>Forced full width - принудительное растягивание параллакса на всю ширину экрана</li>
</ul>
После заполнения всех нужных полей нажимаем `Сохранить`.
