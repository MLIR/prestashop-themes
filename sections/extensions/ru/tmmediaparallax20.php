<h4 id="file-manager">Загрузка видео и картинок на сервер</h4>
<p>Для того чтобы загрузить картинку или видео на сервер с помощью файлового менеджера, для начала нужно нажать на иконку `файлик`(1) напротив интересующего нас поля.</p>
<figure class="img-polaroid"><img class="presta_scr2" src="img/upload-file.jpg" alt="" /></figure>
<p>В появившемся окне, в строке `actions`, нажать upload(2)</p>
<figure class="img-polaroid"><img class="presta_scr2" src="img/upload-file-2.jpg" alt="" /></figure>
<p>Появится еще одно всплывающее окно, здесь и загружаем фото или видео.</p>
<h4 id="layout-info">Предостережения и рекомендации </h4>
<ul class="index-list">
    <li>На мобильных устройствах (смартфон, планшет) по умолчанию эффект параллакс отключен, вместо него в качестве фона блока используется  самый верхний слой, у которого есть картинка</li>
    <li>Принудительное растягивание блока параллакс на всю ширину экрана более критично влияет на скорость загрузки страницы, чем загрузка обычного блока во всю ширину экрана</li>
    <li>Чем меньше будет размер загружаемого видео, тем лучше.</li>
</ul>