<h5>Добавление кастомного стиля</h5>

<p>Можно добавить любой стиль <a target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>
    в ваш модуль. Для этого выберите интересующий вас стиль на сайте. Скопируйте
    JavaScript Style Array в файл .js и сохраните этот файл в папке модуля
    /js/styles/(для названия файла рекомендуется использовать название стиля с заменой
    пробела на нижнее подчеркивание и удалением всех спецсимволов. Например: название
    стиля Subtle Grayscale - название файла subtle_grayscale.js, название стиля Apple
    Maps-esque - название файла apple_maps-esque.js). После этого зайдите в модуль и
    выберите ваш стиль в выпадающем списке "Стиль карты"</p>
