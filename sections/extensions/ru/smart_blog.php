        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Description (Мета-описание)</dt>
            <dd>описание блога, используемое поисковыми движками для лучшего SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Main Blog Url (Основной Url блога)</dt>
