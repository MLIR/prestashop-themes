        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Use .html with Friendly Url (Добавлять .html к человекопонятной ссылке)</dt>
            <dd>включить/отключить добавление .html к человекопонятной ссылке.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Number of posts per page (Количество записей на странице)</dt>
            <dd>количество записей, отображаемых на странице (страницы категорий/архивов, и т.п.).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Auto accepted comment (Автоматическое добавление комментариев)</dt>
