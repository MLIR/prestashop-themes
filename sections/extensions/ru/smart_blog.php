        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>AVATAR Image: (Фото:)</dt>
            <dd>добавление стандартного фото для всех пользователей.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show No Image (Показывать изображение "Нет фото")</dt>
