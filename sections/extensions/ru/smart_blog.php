        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Enable Comment (Включить комментарии)</dt>
            <dd>включить/отключить оставление комментариев пользователями.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Author Name (Показывать имя автора)</dt>
            <dd>включить/отключить отображение автора записи на страницах блога и отдельных записей</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Post Viewed (Показывать посещенные записи)</dt>
            <dd>включить/отключить отображение количества просмотров записи пользователями. Располагается под
