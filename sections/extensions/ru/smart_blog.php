<h4>Лента RSS Smart Blog</h4>

<p>Модуль генерирует новостную ленту для блога.</p>

<p><strong>Включает несколько настроек в панели управления:</strong></p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Update Period (Период обновления)</dt>
            <dd>позволяет отображать записи за определенный период времени (час/день/неделя/месяц/год).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Update Frequency (Частота обновления)</dt>
            <dd>позволяет задать частоту обновления (1 неделя, 2 часа, и т.п.).</dd>
        </dl>
    </li>
</ol>
<p>Сверху находится путь к файлу, в котором будут генерироваться новости. Он состоит
    из URL сайта + /modules/smartblogfeed/rss.php.</p>

<figure class="img-polaroid">
    <img src="img/feed.png" alt="">
</figure>

<h4>Архив Smart Blog</h4>

<p>Этот блок отображается в левой/правой колонке и сортирует записи по периоду
    времени (месяц).</p>

<p><strong>Не имеет дополнительных настроек.</strong></p>

