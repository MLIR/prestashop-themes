<h4>Smart Blog</h4>

<p>Это основной модуль с множеством настроек функциональности блога. После его
    установки в панели управления в главном меню появится новая вкладка Блог, где вы
    сможете добавлять/удалять/редактировать записи/категории или выбирать тип и
    разрешения изображений, используемых в записях (типы и разрешения картинок
    записей отличаются от настроек изображений в магазине). Чтобы добавить настройки
    отображения модуля на сайте, перейдите в <strong>Smart Blog (Modules->Front
        Office Features->Smart Blog) ((Модули->Функции front-office->Smart
        Blog))</strong> и добавьте все параметры.</p>

<h6><strong>Описание параметров:</strong></h6>

