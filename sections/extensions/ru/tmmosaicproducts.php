<h3>TM Mosaic Products</h3>

<p>Модуль позволяет отображать категории с продуктами и баннерами на главной странице.</p>

<h5>Установка и удаление</h5>

<p>Модуль устанавливается и удаляется так же, как и любой другой модуль Prestashop.</p>

<h5>Настройки модуля</h5>

<p>После установки модуля вы можете добавить нужные блоки с категориями продуктов и ранее добавленными баннерами.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-1.png" alt=""/>
</figure>

<h5>Добавление/удаление/редактирование блоков</h5>

<p>Для добавления новой категории нажмите кнопку "Добавить новый"</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-2.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Select category</strong> - выберите категорию, для которой будете добавлять продукты, баннеры.</li>
    <li><strong>Use custom name</strong> - статус пользовательской категории.</li>
    <li><strong>Custom name</strong> - название категории.</li>
     <li><strong>Status</strong> - статус элемента.</li>
</ol>

<p>Для добавления нового ряда нажмите кнопку "Добавить ряд". Появится окно выбора, в котором представлены восемь макетов.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-3.png" alt=""/>
</figure>

<p>Для добавления макета нажмите на один из восьми вариантов, этот макет будет добавлен в форму.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-4.png" alt=""/>
</figure>

<p>Для добавления элементов нажмите на одну из ячеек макета, появятся две кнопки с вариантами выбора типа добавляемого элемента.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-5.png" alt=""/>
</figure>

<p>После нажатия одной из кнопок вы увидите форму с выбором элемента этого типа.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-6.png" alt=""/>
</figure>


<p>После нажатия на один из элементов, он добавится в ячейку макета, так можно заполнить макет разными блоками.</p>.

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-7.png" alt=""/>
</figure>

<p>После сохранения категории с выбранными макетами, она появится в форме добавленных категорий.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-8.png" alt=""/>
</figure>

<p>Для удаления элемента нажмите кнопку "Delete".</p>

<h5>Добавление/удаление/редактирование баннера</h5>

<p>Для добавления нового баннера нажмите кнопку "Добавить новый баннер". В появившейся форме заполните необходимые поля.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-9.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Select a file</strong> - выберите файл, обязательно для заполнения (как минимум, на основном языке).</li>
    <li><strong>Title</strong> - название баннера, обязательно для заполнения (как минимум, на основном языке).</li>
    <li><strong>Url</strong> - путь ссылки, обязательно для заполнения (как минимум, на основном языке).</li>
    <li><strong>Description</strong> - описание баннера (как минимум, на основном языке).</li>
    <li><strong>Specific class</strong> - CSS класс для блока.</li>
</ol>