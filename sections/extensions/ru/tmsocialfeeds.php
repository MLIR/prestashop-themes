<h4>Instagram</h4>

<p>Client ID - уникальный ID, который присваевается при регистрации виджета на Инстаграм.</p>

<p>User Name - имя пользователя, которое будет использоваться для ссылки на страницу владельца.</p>

<p>Выбор, по какому принцыпу будут выбираться картинки - картинки определенного пользователя или по тегу.</p>

<p>Тег, по которому будут выбираться картинки.</p>

<p>Id пользователя, с чьей страницы будут выбираться картинки.</p>

<p>Instagram access token - разрешение пользователя, которому принадлежит страница.</p>

<p>Во второй части модуля вы можете добавить нужный вам модуль в нужную позицию. Позиций всего 4. При этом в каждой из
    позиций вы можете добавлять свои настройки для блоков. Общей настройкой будут только данные для получения ленты того
    или иного сервиса.</p>

<p>Для каждого сервиса доступны свои варианты настроек.</p>

