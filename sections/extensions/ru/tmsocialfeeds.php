<h3>TM Social Feeds</h3>

<p>Данный модуль отображает ленту постов из социальных сетей. Он позволяет посетителям сайта быть в курсе всех последних
    обновлений и сообщений о вашем сайте в социальных сетях.</p>

<h5>Установка и удаление модуля</h5>

<p>Модуль устанавливается и удаляется, как любой другой модуль Prestashop.</p>

<h5>Настройки модуля</h5>

<p>После установки модуля вы можете настраивать его на странице конфигураций модуля.</p>

<p>В верхней форме устанвливаются глобальные настройки для социального сервиса. Для каждого из сервисов нужны свои
    настройки.</p>

