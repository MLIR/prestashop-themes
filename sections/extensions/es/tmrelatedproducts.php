<h3>TM Related Products</h3>

<p>Este módulo le permite añadir productos relacionados con cualquier producto de su tienda. Todos los productos relacionados se muestran
    en un carrusel en las páginas de productos, a los que se asignan.</p>

<h5>Instalación y Desinstalación</h5>

<p>El módulo se instala y desinstalado como cualquier otro módulo de PrestaShop.</p>

<h5>Ajustes de módulo</h5>

<p>Después de la instalación del módulo, puede configurarlo en la página de configuración del módulo.</p>

<p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-1.png" alt="" /></p>

<ol>
    <li><strong>Mostrar los precios de productos</strong> - mostrando el precio del producto.</li>
    <li><strong>Número de producto mostrados</strong> - el número de productos, que se muestra en una página.</li>
</ol>

<p>Puede agregar y eliminar productos relacionados con la configuración del producto: <b>Catalogo-> Productos (Catalog->Products)</b>.</p>

<ol>
    <li>Vaya a la pestaña <b>TM Related Products</b>: <p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-2.png" alt="" /></p></li>
    <li>En el campo de la búsqueda de productos, por favor escriba el nombre del producto necesario y seleccionelo de la lista.</li>
    <li>Con el fin de eliminar un producto de la lista, pulse el icono de "Cruz" ("Cross") frente al producto necesario.</li>
    <li>Cuando haya terminado, pulse uno de los botones para guardar: <b>Guardar y Quedarse (Save and Stay)</b> o <b> Guardar (Save)</b>.</li>
</ol>