<h3>TM Media Parallax 2.0</h3>
<p>Este módulo está diseñado para permitir añadir efecto de paralaje a los elementos DOM de la página. Se le permite crear paralaje de varias capas. También ofrece la posibilidad de añadir un vídeo de fondo personalizado (puede subirlo desde su ordenador al servidor) o añadir un vídeo de YouTube.</p>
<h4 id="tmmediaparallax-mainpanel">TM Media Parallax Main Panel</h4>
<p>Panel del módulo principal que le permite añadir, editar o eliminar elementos de paralaje. </p>
<figure class="img-polaroid"><img src="img/main-panel.jpg" alt="" /></figure>
<h4 id="tmmediaparallax-add">Creating a new parallax item</h4>
<p>Con el fin de crear un nuevo elemento de paralaje, pulse el icono 'más' (1), en la esquina superior derecha del panel principal.</p>
<figure class="img-polaroid"><img src="img/main-panel-add-item.jpg" alt="" /></figure>
<p>A continuación, podrá ver el siguiente formulario.</p>
<figure class="img-polaroid"><img src="img/add-item.jpg" alt="" /></figure>
<h4>Opciones de formulario para agregar elementos de paralaje</h4>
<ul class="index-list">
    <li>Selector (Selector) - selector CSS del elemento con efecto de paralaje aplicado. Por ejemplo, '#header .nav'</li>
    <li>Estatus del elemento (Item status) - estado del elemento (paralaje no se mostrará si es desactivado)</li>
    <li>Velocidad (Speed) - valor del dígito de 0 a 2, que define la velocidad del bloque principal de paralaje</li>
    <li>Offset (Offset) - espacio de capa principal en el eje Y</li>
    <li>Inverso (Inverse) - dirección de paralaje</li>
    <li>Ocultar (Fade) - esta opción permite configurar bloque principal que desaparece al hacer scroll a la página</li>
    <li>Forzado a todo lo ancho (Forced full width) - forzado ancho total del bloque de paralaje</li>
</ul>
Pulse "Guardar" (Save) después de rellenar todos los campos requeridos.
