<h4 id="file-manager">La carga de vídeo y las imágenes al servidor</h4>
<p>Con el fin de subir un video o una imagen al servidor a través File Manager, primero hay que pulsar 'archivo' (file) (1) en el icono junto al campo necesario.</p>
<figure class="img-polaroid"><img src="img/upload-file.jpg" alt="" /></figure>
<p>En la siguiente ventana, en la línea de 'acciones' ('actions'), seleccione 'cargar' ('upload')(2)</p>
<figure class="img-polaroid"><img src="img/upload-file-2.jpg" alt="" /></figure>
<p>A continuación, aparecerá una ventana más de pop-up, para cargar una imagen o un archivo de vídeo aquí.</p>
<h4 id="layout-info">Advertencias y recomendaciones</h4>
<ul class="index-list">
    <li>Los dispositivos móviles (smartphones, tablets) tienen el efecto de paralaje desactivado por defecto, en cambio se utiliza como fondo la capa de imagen de bloque superior</li>
    <li>El ancho total del bloque de paralaje afecta a la velocidad de carga de las páginas de manera más crítica que un ancho regular del bloque de carga</li>
    <li>Cuanto más pequeño es el tamaño del vídeo la carga, es mejor.</li>
</ul>