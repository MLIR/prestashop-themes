<h3>M&oacute;dulo Media parallax</h3>

<h4>&iquest;Qu&eacute; es el efecto de parallax?</h4>

<p>El scroll parallax es una t&eacute;cnica de desplazamiento especial en los gr&aacute;ficos por ordenador, en el que
    las im&aacute;genes del fondo se mueven m&aacute;s lentas que las im&aacute;genes en primer plano, creando una ilusi&oacute;n
    de profundidad en un videojuego en 2D y a&ntilde;adiendo la inmersi&oacute;n. Los dise&ntilde;adores web comenzaron
    a incorporar scroll parallax en 2011, usando HTML5 y CSS3. Las p&aacute;ginas web con fondos parallax se est&aacute;n
    convirtiendo en una estrategia cada vez m&aacute;s popular, los defensores de esta t&eacute;cnica dicen que es una
    forma sencilla de mostar fluidez en la web. Adem&aacute;s, dicen utilizar los fondos parallax como herramienta para
    mejorar la experiencia del usuario. Scroll parallax permite la creaci&oacute;n de experiencias de usuario din&aacute;micas
    e interactivas y tienen el potencial de mejorar el atractivo visual.</p>

<p>Este m&oacute;dulo permite a&ntilde;adir efectos parallax a su sitio web sin conocimientos previos. Es posible a&ntilde;adir
    im&aacute;genes y v&iacute;deo de fondo.</p>


