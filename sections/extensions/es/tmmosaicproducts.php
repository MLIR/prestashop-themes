<h3>TM Mosaic Products</h3>

<p>Este módulo le permite mostrar categorías con productos, banners en cualquier fila en la página principal del sitio.</p>

<h5>Instalación y Eliminación</h5>

<p>El módulo está instalado y eliminado como cualquier otro módulo de PrestaShop.</p>

<h5>Ajustes del módulo</h5>

<p>Después de que el módulo ha sido instalado, puede agregar los bloques necesarios con la categoría de productos, banners añadido previamente.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-1.png" alt=""/>
</figure>

<h5>Bloques añadir/eliminar/editar</h5>

<p>In order to add a new category, press the "Add new" button.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-2.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Seleccione una categoría (Select category)</strong> - Seleccione la categoría a la que desea agregar productos, banners.</li>
    <li><strong>Use el nombre perdonalizado (Use custom name)</strong> - estado de categoría personalizada.</li>
    <li><strong>Nombre perdonalizado (Custom name)</strong> - título de la categoría.</li>
     <li><strong>Estado (Status)</strong> - estado del artículo.</li>
</ol>

<p>Con el fin de agregar la nueva fila, pulse el botón "Añadir row" ("Add row"). A continuación, elija uno de los ocho diseños presentados en la siguiente ventana.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-3.png" alt=""/>
</figure>

<p>Con el fin de agregar el diseño, haga clic en una de las ocho opciones para recogerlo, y se agrega al formulario a continuación.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-4.png" alt=""/>
</figure>

<p>Para añadir elementos, haga clic en una de las celdas de diseño, dos botones que enumeran las opciones disponibles para agregar el elemento de cierto tipo.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-5.png" alt=""/>
</figure>

<p>Después de pulsar uno de los botones verá la forma de elegir el elemento del tipo recogido.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-6.png" alt=""/>
</figure>


<p>Después de hacer clic en uno de los artículos, que se añadirá a la celda de disposición. Puede rellenar el diseño con diferentes bloques de esta manera.</p>.

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-7.png" alt=""/>
</figure>

<p>Después de guardar la categoría con diseños elegidos, se mostrará en la forma de las categorías añadidas.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-8.png" alt=""/>
</figure>

<p>Con el fin de eliminar el elemento, pulse el botón "Borrar" ("Delete").</p>

<h5>Banner añadir/eliminar/editar</h5>

<p>Con el fin de añadir un nuevo banner, haga clic en el botón "Añadir nuevo banner" ("Add new banner"). A continuación, rellene los campos requeridos en el formulario que aparece.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-9.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Seleccione el archivo (Select a file)</strong> - elige un archivo, este campo se requiere (al menos para el idioma por defecto).</li>
    <li><strong>Título (Title)</strong> - título del banner, este campo se requiere (al menos para el idioma por defecto).</li>
    <li><strong>Url (Url)</strong> - trayectoria del enlace, este campo es obligatorio (al menos para el idioma por defecto).</li>
    <li><strong>Descripсión (Description)</strong> - descripción del banner (al menos para el idioma por defecto).</li>
    <li><strong>Clase específica (Specific class)</strong> - bloque de clase CSS.</li>
</ol>