            <dt>Activar Captcha</dt>
            <dd>activar/desactivar Captcha debajo del formulario Dejar comentario en las p&aacute;ginas de
                productos.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Activar comentarios</dt>
            <dd>activar/desactivar dejar comentarios por parte de los usuarios.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostrar nombre de autor</dt>
            <dd>activar/desactivar mostrar autor de mensajes en las p&aacute;ginas del blog y p&aacute;ginas
