<h4>Fuentes RSS del Blog Inteligente</h4>

<p>Es el m&oacute;dulo que genera el nuevo feed de tu blog.</p>

<p><strong>Tiene varias opciones de configuraci&oacute;n en el panel de administraci&oacute;n:</strong>
</p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Per&iacute;odo de actualizaci&oacute;n</dt>
            <dd>permite visualizar mensajes en un per&iacute;odo de tiempo espec&iacute;fico (horas/d&iacute;as/semanas/meses/a&ntilde;os).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Frecuencia de actualizaci&oacute;n</dt>
            <dd>permite configurar la frecuencia de actualizaci&oacute;n (1 semana, 2 horas, etc.).</dd>
        </dl>
    </li>
</ol>
<p>En la parte superior hay una direcci&oacute;n donde se generan las noticias.
    Consiste en la URL del sitio web + /modules/smartblogfeed/rss.php.</p>

