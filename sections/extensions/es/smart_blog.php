            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>CSS personalizado</dt>
            <dd>puede a&ntilde;adir estilos adicionales en este campo. Tenga cuidado con las clases que ya se
                utilizan
                en el tema, ya que pueden producirse algunos conflictos.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Elimnar el antiguo Thumblr</dt>
