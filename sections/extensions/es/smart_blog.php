        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostrar p&aacute;ginas vistas</dt>
            <dd>activar/desactivar el n&uacute;mero de veces que los usuarios vieron el mensaje. Se mostrar&aacute;
