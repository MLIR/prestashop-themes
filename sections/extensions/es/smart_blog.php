<h4>Blog Inteligente</h4>

<p>Este es el m&oacute;dulo principal, contiene muchos ajustes para la funcionalidad
    del blog. Despu&eacute;s de su instalaci&oacute;n aparecer&aacute; la pesta&ntilde;a
    Blog en el men&uacute; principal del panel de administraci&oacute;n donde podr&aacute;
    a&ntilde;adir/eliminar/editar los mensajes/categor&iacute;as o seleccionar el
    tipo de im&aacute;genes y sus dimensiones utilizado en los puestos (los tipos y
    dimensiones de las im&aacute;genes de los mensajes son diferentes a las im&aacute;genes
    de los productos de la tienda). Para agregar la configuraci&oacute;n frontend
    del m&oacute;dulo, por favor vaya a <strong>Blog Inteligente (M&oacute;dulos->Caracter&iacute;sticas
        de Front Office->Blog Inteligente)</strong> y a&ntilde;ada todos los par&aacute;metros.
</p>

