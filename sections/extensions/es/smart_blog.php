        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Description</dt>
            <dd>descripci&oacute;n de tu blog utilizada por los motores de b&uacute;squeda para una mejor optimizaci&oacute;n
                SEO.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>URL principal del Blog</dt>
            <dd>la URL de tu Blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Usar .html con Urls amigables</dt>
            <dd>activar/desactivar a&ntilde;adir .html a las p&aacute;ginas del blog utilizando URLs amigables.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>N&uacute;mero de mensajes por p&aacute;gina</dt>
            <dd>n&uacute;mero de mensajes que se muestran por p&aacute;gina (p&aacute;ginas de categor&iacute;as/archivos,
