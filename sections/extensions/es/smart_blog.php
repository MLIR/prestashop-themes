        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Estilo del nombre de autor</dt>
            <dd>cambiar el formato del nombre del autor.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Imagen del AVATAR:</dt>
            <dd>a&ntilde;adir la imagen del avatar est&aacute;ndar para todos los usuarios.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostrar sin imagen</dt>
            <dd>activar/desactivar la imagen. Sin imagen se activa si no hay ninguna imagen para el mensaje/categor&iacute;a,
                etc.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostrar Categor&iacute;a</dt>
            <dd>activar/desactivar la imagen de la categor&iacute;a y la descripci&oacute;n en las p&aacute;ginas de
