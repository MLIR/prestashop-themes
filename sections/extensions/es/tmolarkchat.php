<h3>TM Chat de Olark</h3>

<p>M&oacute;dulo para mostrar el Chat de Olark en su tienda.</p>
<h5>M&oacute;dulo de instalaci&oacute;n/desinstalaci&oacute;n</h5>

<p>Este m&oacute;dulo se puede instalar/desinstalar como cualquier otro m&oacute;dulo de
    PrestaShop.</p>
<h5>Configuraci&oacute;n del m&oacute;dulo</h5>

<p>Despu&eacute;s de que se haya instalado el m&oacute;dulo puede configurarlo en la p&aacute;gina
    de configuraci&oacute;n del m&oacute;dulo.</p>

<p>Para configurar el Chat de Olark como el de la plantilla online, debe registrarse en
    la <a href="http://www.olark.com/" target="_blank" title="Olark Registration">p&aacute;gina
        web oficial</a> y obtener el ID para su sitio web. Introduzca el ID en el campo
    "Olark site ID" y ajuste el m&oacute;dulo en "Live Demo". Ahora guarde la
    configuraci&oacute;n haciendo clic en el bot&oacute;n <strong>Guardar</strong>.</p>


