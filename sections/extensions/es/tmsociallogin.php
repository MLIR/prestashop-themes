<h3>TM Inicio de Sesi&oacute;n de Redes Sociales</h3>

<p>Este m&oacute;dulo le permite registrarse e iniciar sesi&oacute;n con su cuenta de
    redes sociales como Facebook o Google.</p>
<h5>Module install/uninstall</h5>

<p>Este m&oacute;dulo se puede instalar/desinstalar como cualquier otro m&oacute;dulo de
    PrestaShop.</p>
<h5>Configuraci&oacute;n del m&oacute;dulo</h5>

<p>Despu&eacute;s de que se haya instalado el m&oacute;dulo puede configurarlo en la p&aacute;gina
    de configuraci&oacute;n del m&oacute;dulo.</p>

<p>Para obtener los datos necesarios de configuraci&oacute;n del m&oacute;dulo necesita
    crear la aplicaci&oacute;n correspondiente en el recurso necesario.</p>

<p class="alert alert-info">Puede conseguir informaci&oacute;n m&aacute;s detallada siguiendo los enlaces: <a
        href="https://support.google.com/cloud/answer/6158849?hl=en&ref_topic=6262490" target="_blank">Google
        App</a> y <a href="https://developers.facebook.com/docs/apps/register" target="_blank">Facebook
        App</a>.</p>

<p>No es necesario el uso de ambas variantes de inicio de sesi&oacute;n (Google,
    Facebook) al mismo tiempo, puede utilizar s&oacute;lo uno de ellos.</p>
<h4>Campos de Facebook</h4>
<ol class="index-list">
    <li>ID de su aplicaci&oacute;n.</li>
    <li>Clave secreta de su aplicaci&oacute;n.</li>
</ol>
<h4>Campos de Google</h4>
<ol class="index-list">
    <li>ID de su aplicaci&oacute;n.</li>
    <li>Clave secreta de su aplicaci&oacute;n.</li>
    <li>Redirigir a la ruta de acceso despu&eacute;s de registrarse en <strong>(URL
            de su
            tienda+index.php?fc=module&module=tmsociallogin&controller=googlelogin)</strong>.

    </li>
</ol>
<p class="alert alert-warning">
    La ruta de redirecci&oacute;n debe coincidir con la que ha indicado durante la creaci&oacute;n de la aplicaci&oacute;n
    de Google.</p>

