<p>Puede a&ntilde;adir o eliminar productos relacionados al producto principal en la
    configuraci&oacute;n del producto <strong>Catalog->Products</strong>.</p>
<ol class="index-list">
    <li>Vaya a <strong>TM Productos Relacionados</strong>
    </li>
    <li>En el campo de b&uacute;squeda de productos empice introduciendo el t&iacute;tulo
        del producto, y lo elija entre las variantes que se ofrecen;
    </li>
    <li>Para eliminar el producto de la lista clic en la X antes de que el
        producto;
    </li>
    <li>Una vez que haya completado la configuraci&oacute;n haga clic en el bot&oacute;n
        <strong>Guardar y Permancer</strong> o <strong>Guardar</strong>.
    </li>
</ol>

