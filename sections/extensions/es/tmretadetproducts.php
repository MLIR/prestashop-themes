<h3>TM Productos Relacionados</h3>

<p>El m&oacute;dulo fue dise&ntilde;ado para a&ntilde;adir y mostrar los productos
    relacionados en la p&aacute;gina del producto.</p>
<h5>M&oacute;dulo de instalaci&oacute;n/desinstalaci&oacute;n</h5>

<p>Este m&oacute;dulo se puede instalar/desinstalar como cualquier otro m&oacute;dulo de
    PrestaShop.</p>
<h5>Configuraci&oacute;n del m&oacute;dulo</h5>

<p>Despu&eacute;s de que se haya instalado el m&oacute;dulo puede configurarlo en la p&aacute;gina
    de configuraci&oacute;n del m&oacute;dulo.</p>
<ol class="index-list">
    <li>Mostrar el precio del producto.</li>
    <li>Cantidad de productos a mostrar.</li>
</ol>

