<h4>Instagram</h4>

<p>Client ID - a unique ID assigned during widget registration on Instagram.</p>

<p>User Name - username used to link the owner's page.</p>

<p>Selection type: you can set whether the images are loaded from specific user or by tag.</p>

<p>Tag by which images are loaded.</p>

<p>Id of the user whose images will be loaded.</p>

<p>Instagram access token - access token of the user whom the page belongs.</p>

<p>In the second part of the module configuration page you can add the required module to the position you want. There
    are 4 positions. You can add the block settings for each position. The only global setting is the data of social
    networks which feeds will be loaded.</p>

<p>Each social network has its own settings.</p>

