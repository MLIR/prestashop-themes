<h3>TM Related Products</h3>

<p>This module allows you to add related products to any product of your store. All related products are displayed in a carousel on product pages, to which they are assigned.</p>

<h5>Installation and Uninstall</h5>

<p>The module is installed and uninstalled as any other PrestaShop module.</p>

<h5>Module settings</h5>

<p>After module installation, you can set it up on the module configuration page.</p>

<p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-1.png" alt="" /></p>

<ol>
    <li><strong>Display price on products</strong> - displaying product price.</li>
    <li><strong>Number of displayed products</strong> - the number of products, displayed on a page.</li>
</ol>

<p>You can add and delete related products in product settings: <b>Catalog->Products</b>.</p>

<ol>
    <li>Navigate to <b>TM Related Products</b> tab: <p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-2.png" alt="" /></p></li>
    <li>In products search field, please enter the name of the needed product and select it from the list.</li>
    <li>In order to delete a product from the list, press the “Cross” icon in front of the needed product.</li>
    <li>When you are done, press one of the buttons for saving: <b>Save and Stay</b> or <b>Save</b>.</li>
</ol>