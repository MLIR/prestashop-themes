<h3>TM Product List Gallery</h3>

<p>Das jeweilige Modul zeigt alle verfügbaren Produktbilder als Karussell auf den
    Produktlistenseiten.</p>
<h5>Installation und Entfernung des Moduls</h5>

<p>Dieses Modul wird wie ein anderes PrestaShop Modul installiert und entfernt.</p>
<h5>Moduleinstellungen</h5>

<p>Dieses Modul hat keine zusätzlichen Einstellungen</p>
<h5>Zusätzliche Einstellungen</h5>

<p>Damit dieses Modul funktionieren kann, sollen Sie die folgende Zeile:</p>

<p><code>{hook h='displayProductListGallery' product=$product}</code></p>

<p>zur Datei <strong>product-list.tpl</strong> im Stamm Ihres Themas hinzufügen. Nach
    dem schließenden Tag <code>&lt;/a&gt;</code> mit der Klasse
    <code>.product_img_link</code></p>
