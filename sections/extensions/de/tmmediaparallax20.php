<h3>TM Media Parallax 2.0</h3>
<p>Dieses Modul ist entworfen damit Sie Parallaxe auf die DOM-Elemente Ihrer Seite hinzufügen können. Es ermöglicht Ihnen, mehrschichtige Parallaxe zu erstellen. Es bietet auch eine Möglichkeit, ein benutzerdefiniertes Hintergrundvideo hinzuzufügen (Sie können es von Ihrem Computer zum Server hochladen) oder ein YouTube-Video hinzuzufügen.</p>
<h4 id="tmmediaparallax-mainpanel">TM Media Parallax Main Panel</h4>
<p>Das Hauptmodul-Panel erlaubt Ihnen Parallaxelemente zu bearbeiten, löschen und hinzufügen. </p>
<figure class="img-polaroid"><img src="img/main-panel.jpg" alt="" /></figure>
<h4 id="tmmediaparallax-add">Neues Parallaxelement erstellen</h4>
<p>Um ein neues Parallaxelement zu erstellen, drücken Sie 'plus' (1) Symbol in der oberen rechten Ecke des Hauptfeld.</p>
<figure class="img-polaroid"><img src="img/main-panel-add-item.jpg" alt="" /></figure>
<p>Dann werden Sie das folgende Formular sehen.</p>
<figure class="img-polaroid"><img src="img/add-item.jpg" alt="" /></figure>
<h4>Formular Optionen zum Hinzufügen von Parallaxelementen</h4>
<ul class="marked-list">
    <li>Selektor (Selector) - CSS-Selektor vom Element mit Parallaxelement-Effekt. Beispielsweise, '#header .nav'</li>
    <li>Elementstatus (Item status) - Elementstatus (parallax wird nicht angezeigt, wenn es deaktiviert ist)</li>
    <li>Geschwindigkeit (Speed) - Wert von 0 bis 2,welcher die Geschwindigkeit des Hauptparallaxblock definiert</li>
    <li>Offset - Hauptschicht Einrückung auf der Y-Achse</li>
    <li>Inverse - Parallax Richtung</li>
    <li>Verblassen (Fade) - diese Option erlaubt Ihnen das  Hauptblock Verblassen beim Scrollen zu regulieren</li>
    <li>Volle Breite forcieren (Forced full width) - Volle Breite forcieren vom Parallaxblock</li>
</ul>
Drücken Sie auf "Speichern", nachdem Sie alle erforderlichen Felder ausgefüllt haben.
