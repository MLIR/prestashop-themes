<p>Um Video oder ein Bild auf Server über Datei-Manager zu laden, müssen Sie zuerst 'Datei' (1) neben dem benötigten Feld zu drücken.</p>
<figure class="img-polaroid"><img src="img/upload-file.jpg" alt="" /></figure>
<p>Im folgenden Fenster in der Zeile 'actions', wählen Sie "Upload" (2)</p>
<figure class="img-polaroid"><img src="img/upload-file-2.jpg" alt="" /></figure>
<p>Dann werden Sie ein weiteres Pop-up-Fenster sehen, um Bild- oder Videodateien hier hochzuladen</p>
<h4>Warnungen und Empfehlungen</h4>
<ul class="index-list">
    <li>Mobile Geräte (Smartphones, Tablets) haben Parallaxeffekt standardmäßig deaktiviert,anstatt wird der oberste Bildschichtblock  als Hintergrund verwendet </li>
    <li>Erzwungene volle Breite des ParallaxBlocks beeinflusst die Seiten Ladegeschwindigkeit kritischer als ein normaler Vollbreiten Block.</li>
    <li>Je kleiner die Größe der hochgeladenen Videos ist, umso besser.</li>
</ul>