<h3>TM Related Products</h3>

<p>Dieses Modul ermöglicht es Ihnen, ähnliche Produkte zu jedem Produkt von Ihrem Geschäft hinzuzufügen. Alle zusammenhängende
    Produkte werden in einem Karussell auf den Produktseiten angezeigt, denen sie zugeordnet sind.
</p>

<h5>Installation and Deinstallation</h5>

<p>Das Modul wird wie jedes andere PrestaShop Modul installiert und deinstalliert.</p>

<h5>Moduleinstellungen</h5>

<p>Nach der Installation des Moduls, können Sie es auf der Modulkonfigurationsseite einstellen.</p>

<p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-1.png" alt="" /></p>

<ol>
    <li><strong> Produktpreis anzeigen (Display price on products)</strong> - Produktpreis anzeigen.</li>
    <li><strong>Anzahl der angezeigten Produkte(Number of displayed products)</strong> - Anzahl der Produkte, auf einer Seite.</li>
</ol>

<p>Sie können ähnliche Produkte in den Produkteinstellungen hinzufügen und löschen: <b>Catalog->Products</b>.</p>

<ol>
    <li>Gehen Sie zum <b>TM Related Products</b> Tab: <p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-2.png" alt="" /></p></li>
    <li>Im Produkt-Suchfeld , geben Sie bitte den Namen des benötigten Produkts ein und wählen Sie aus der Liste.</li>
    <li>Um ein Produkt aus der Liste zu löschen, drücken Sie die "Kreuz" Symbol vor dem benötigten Produkt.</li>
    <li>Wenn Sie fertig sind, drücken Sie eine der den Knopf zum Speichern: <b>Save and Stay</b> oder <b>Save</b>.</li>
</ol>