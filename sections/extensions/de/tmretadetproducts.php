<h3>TM Related Products</h3>

<p>Das jeweilige Modul ermöglicht verwandte Produkte auf der Produktinformationsseite
    hinzuzufügen und anzuzeigen.</p>

<h5>Installierung und Entfernung des Moduls</h5>

<p>Dieses Modul wird wie ein anderes PrestaShop Modul installiert und entfernt.</p>

<h5>Moduleinstellungen</h5>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der
    Modulkonfigurationsseite einstellen.</p>

<figure class="img-polaroid">
    <img src="img/tmrelatedproducts-1.png" alt="">
</figure>
<ol class="index-list">
    <li>Den Produktpreis anzeigen.</li>
    <li>Die Anzahl von angezeigten Produkten.</li>
</ol>
<p>Die verwandten Produkte können zum Hauptprodukt in Produkteinstellungen <strong>Catalog->Products</strong>
    hinzufügen oder entfernen.</p>

<ol class="index-list">
    <li>Öffnen Sie den Tab <strong>TM Related Products</strong>

        <figure class="img-polaroid">
            <img src="img/tmrelatedproducts-2.png" alt="">
        </figure>
    </li>
    <li>Im Suchfeld fangen Sie an, den Namen des notwendigen Produktes einzugeben und
        wählen Sie es aus den vorgeschlagenen Varianten aus;
    </li>
    <li>Um das Produkt von der Liste zu entfernen, drücken Sie auf das Kreuz neben
        dem notwendigen Produkt;
    </li>
    <li>Nachdem Sie die Änderungen vorgenommen haben, drücken Sie entweder <strong>Save
            and Stay</strong> oder <strong>Save</strong> um sie zu speichern
    </li>
</ol>
