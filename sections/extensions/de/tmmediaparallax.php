<h3>Modul Media Parallax</h3>

<h4>Was ist eigenlich ein Parallax-Effekt (parallax)?</h4>

<p>Parallax-Scrolling ist eine spezielle Scrolling-Technik in der Computergraphik. Dank diesem Effekt bewegen sich die
    Bilder im Hintergrund langsamer als im Vordergrund. Dadurch wird die Illusion der Tiefe und des zweidimensionalen
    Videospiel erstellt. Ab 2011 begannen Webdesigner das Parallax-Scrolling umzusetzen. Dafür haben sie HTML5 und CSS3
    verwendet. Webseiten mit dem Parallax-Effekt sind gefragt geworden, weil er sie beweglich macht. Außerdem zieht der
    Parallax-Effekt scharfe Aufmerksamkeit der Kunden und macht jede Webseite einzigartig. Das Parallax-Scrolling
    verbessert das Aussehen der Webseite und ermöglicht dynamisches, interaktives Interface für Benutzer zu
    erstellen.</p>

<p>Dieses Modul fügt den Parallax-Effekt zur Webseite ohne zusätzliche Fähigkeiten und Mühe hinzu. Als Hintergrund
    können Sie Bilder oder Videos hinzufügen.</p>


