<h5>
    Hinzufügung vom Modul Media parallax
</h5>
<ol class="index-list">
    <li>Es ist empfohlen, die Klasse oder den Index zum Inhalt der HTML Seite hinzuzufügen. Sie können auch
        einzigartigen Tag, der nicht wiederholt wird, nutzen.
    </li>

    <li>Erstellen Sie das Bild mit der notwendigen Größe (am besten ist, wenn das Bild größer als Größen des Blocks, zu
        dem der Effekt hinzugefügt wird, ist). Wenn Sie das Video verwenden möchten, müssen Sie es dann in drei
        Formaten: .mp4,.webm und .ogv vorbereiten und auch das Vorschaubild, das die gleiche Größe hat, (.png, .jpg und
        .gif) hinzufügen
    </li>

    <li>
        <p>Klicken Sie auf den Button
            <strong>Add container (Container hinzufügen)</strong>
            in
            <strong>Add new block (Neuen Block hinzufügen)</strong>
            .
        </p>

