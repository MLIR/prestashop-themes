<h3>TM Mosaic Products</h3>

<p>Dieses Modul ermöglicht es Ihnen, Kategorien mit Produkten, Banner auf der Startseite anzuzeigen.</p>

<h5>Installation und Löschen</h5>

<p>Das Modul wird installiert und gelöscht wie jedes andere Modul.</p>

<h5>Modul Einstellungen</h5>

<p>Nach dem das Modul installiert wurde, können Sie zu den gewünschten Blöcken Kategorie Produkte hinzufügen, wo zuvor Banner.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-1.png" alt=""/>
</figure>

<h5>Hinzufügen/Löschen/Bearbeiten von Blöcken</h5>

<p>Um eine neue Kategorie hizuzufügen, drücken Sie den "Neu hinzufügen(Add new)" Knopf.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-2.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Kategorie wählen(Select category)</strong> - wählen Sie eine Kategorie zu der Sie Produkte, Banner.</li>
    <li><strong>Benutzerdefinierten Namen verwenden (Use custom name)</strong> - benutzerdefinierte Kategoriestatus.</li>
    <li><strong>Benutzerdefinierte Name</strong> - Kategorientitel.</li>
     <li><strong>Status</strong> - Elementstatus.</li>
</ol>

<p>Um die neue Zeile hizuzufügen, drücken Sie den "Zeile hinzufügen" Knopf. Dann wählen Sie eine der acht Layouts im folgenden Fenster.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-3.png" alt=""/>
</figure>

<p>Um ein Layout hizuzufügen, wählen Sie eins von den acht Optionen, dann wird es hizugefügt.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-4.png" alt=""/>
</figure>

<p>Um Elemnte hinzuzufügen, klicken Sie auf einen der Layout-Zellen, Sie werden zwei Schaltflächen sehen, die Ihnen die verfügbaren Optionen für das Hinzufügen der Artikel eines bestimmten Typs anzeigen.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-5.png" alt=""/>
</figure>

<p>Nach dem Drücken einer der Tasten werden Sie das entsprechende Formular für den erwählten Typ sehen.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-6.png" alt=""/>
</figure>


<p>Nachdem Klicxken einer dieser Elemente, wird es zu den Layout-Zellen hinzugefügt. Auf diese Weise können Sie das Layout mit verschiedenen Blöcken füllen.</p>.

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-7.png" alt=""/>
</figure>

<p>Nachdem Speichern der Kategorie mit ausgewählten Layouts , wird es in dem Formular der zusätzlichen Kategorien angezeigt.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-8.png" alt=""/>
</figure>

<p>Um das Element zu entfernen, drücken Sie die Taste "Löschen"</p>

<h5>Banner hinzufügen/entfernen/bearbeiten</h5>

<p>Um ein neues Banner hinzuzufügen, klicken Sie auf den "Neuen Banner hinzufügen(Add new banner)". Dann, füllen Sie die erforderlichen Felder aus, im Fornular, dass erscheint.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-9.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Wählen Sie eine Datei (Add a file)</strong> - Wählen Sie eine Datei, dieses Feld ist erforderlich (zumindest für die Standardsprache).</li>
    <li><strong>Titel (Title)</strong> - Bannertitel,dieses Feld ist erforderlich (zumindest für die Standardsprache).</li>
    <li><strong>Url</strong> - Link-Pfad, dieses Feld ist erforderlich (zumindest für die Standardsprache).</li>
    <li><strong>Beschreibung</strong> - Bannerbeschreibung (zumindest für die Standardsprache).</li>
    <li><strong>Spezifische Klasse</strong> - CSS-Klasse des Blocks.</li>
</ol>