<h3>Modul TM HTML Content</h3>

<p>Das jeweilige Modul ermöglicht jeglichen Inhalt (Bild, HTML, Links, usw.) in den folgenden Positionen:
    displayNav, home, top, topColumn, left und right (displayLeftColumn/displayRightColumn), footer darzustellen. Es
    wird benutzt, um die Möglichkeiten des Designs zu erweitern und Werbung/Informationsblöcke zur Webseite
    hinzuzufügen. Es ist ein mehrsprachiges Modul, das Sie auch in dem mehrsprachigen Online-Shop nutzen können.</p>

<h5>Um den Inhalt hinzuzufügen, machen Sie das Folgende:</h5>


<h6>Gehen Sie in <strong>Modules->Front Office Features->TM HTML Content
        (Module->Funktionen front-office->TM HTML Content)</strong> (wenn es nicht installiert wurde,
    installieren Sie es).
</h6>
<ol class="index-list">
    <li>Drücken Sie auf den Button <strong>Add new (Neu hinzufügen)</strong>. </li>

    <li>Die Liste von den verfügbaren Sprachen. Sie können verfügbare Elemente für alle Sprachen anschauen,
        indem Sie Tabs von Sprachen umschalten. </li>

    <li>Die Liste von Elementen, die für eine bestimmte Position oder Sprache hinzugefügt wurden. </li>
</ol>

<figure class="img-polaroid">
    <img src="img/htmlcontent.png" alt="">
</figure>
<h6>Hinzufügen vom Element. Drücken Sie auf den Button <strong>Add item (Element
        hinzufügen)</strong> und füllen Sie die notwendigen Einstellungen (es gibt keine Pflichtfelder) aus.
</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Language (Sprache)</dt>
            <dd>wählen Sie die notwendige Sprache aus der DropDownListe.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Title (Titel)</dt>
            <dd>Titel für das Element.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use title in front (Namen auf der Webseite anzeigen)</dt>
            <dd>Titel des Elementes auf der Webseite darstellen.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hook (Block)</dt>
            <dd>Position, zu der das Element hinzugefügt wird.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image (Bild)</dt>
            <dd>Laden Sie das Bild von dem Computer hoch.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image width (Bildbreite)</dt>
            <dd>Bildbreite auf der Webseite.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image height (Bildhöhe)</dt>
            <dd>Bildhöhe auf der Webseite.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>URL</dt>
            <dd>Hinzufügung des Links (der gesamte Block wird mit dem Link).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Target blank</dt>
            <dd>Link in dem neuen Tab öffnen.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>HTML</dt>
            <dd>HTML Code (außer dem Link, der zum Feld URL hinzugefügt wurde) oder Text.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Save (Speichern)</dt>
            <dd>Button, um das Element zu speichern.</dd>
        </dl>
    </li>
</ol>
<p class="alert alert-info">Alle Elemente einer Sprache, die zur gleichen Position hinzugefügt wurden,
    werden als Liste im Control Panel auf der Webseite dargestellt.</p>
