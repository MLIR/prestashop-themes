<h3>TM Olark Chat</h3>

<p>Dieses Modul ermöglicht den Olark Chat auf dem Front-End Ihres Online-Shops
    anzuzeigen.</p>
<h5>Installierung und Entfernung des Moduls</h5>

<p>Dieses Modul wird wie ein anderes PrestaShop Modul installiert und entfernt.</p>
<h5>Moduleinstellungen</h5>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der
    Modulkonfigurationsseite einstellen.</p>

<p>Um den Olark Chat, der schon in der Demoversion ist, einzustellen, sollen Sie sich
    auf offiziellen Webseite <a href="http://www.olark.com/" target="_blank"
                                title="Регистрация Olark">der registrieren</a> und die
    ID Ihrer Webseite erhalten. Geben Sie Ihre ID ins Feld "Olark site ID" ein und
    öffnen Sie das Modul "Live mode". Danach speichern Sie die Einstellungen, indem Sie
    auf den Button <strong>Save</strong> drücken.</p>

