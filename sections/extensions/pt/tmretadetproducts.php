<h3>TM Related Products</h3>

<p>O módulo a seguir foi desenvolvido para incluir e mostrar produtos relacionados na
    página de produto.</p>
<h5>Instalar/desinstalar módulo</h5>

<p>Este módulo pode ser instalado/desinstalado como qualquer outro módulo do
    PrestaShop.</p>
<h5>Configurações</h5>

<p>Após instalado, o módulo pode ser configurado na página de configurações do
    módulo</p>

<ol class="index-list">
    <li>Mostrar preço do produto.</li>
    <li>Quantidade de produtos para mostrar.</li>
</ol>

<p>Você pode adicionar ou remover produtos relacionados para o produto principal nas
    configurações de produto <strong>Catálogo (Catalog)->Produtos (Products)</strong>.
</p>

<figure class="img-polaroid">
    <img src="img/tmrelatedproducts-1.png" alt="">
</figure>

<ol class="index-list">
    <li>Navegue para <strong>TM Related Products</strong>
    </li>
    <li>No campo de produtos comece a escrever o nome de um produto e escolha na
        lista;
    </li>
    <li>Para retirar um produto da lista, clique no X antes do produto;</li>
    <li>Depois de alterar as configurações você deve clicar nos botões <strong>Save
            and Stay</strong> ou em <strong>Save</strong></li>
</ol>

