<h3>TM Related Products</h3>

<p>Este módulo permite incluir produtos relacionados a qualquer produto de sua loja. Todos os produtos relacionados são exibidos na página de produtos em formato carousel, para os quais forem definidos.</p>

<h5>Installation and Uninstall</h5>

<p>Este módulo pode ser instalado ou desinstalado como qualquer outro módulo PrestaShop.</p>

<h5>Module settings</h5>

<p>Após instalar o módulo, você pode definí-lo na página de configurações.</p>

<p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-1.png" alt="" /></p>

<ol>
    <li><strong>Display price on products</strong> - exibição dos preços do produto.</li>
    <li><strong>Number of displayed products</strong> - a quantidade de produtos exibidos na página.</li>
</ol>

<p>Você pode incluir e apagar produtos relacionados nas configurações de produto: <b>Catalog->Products</b>.</p>

<ol>
    <li>Navegue até a aba <b>TM Related Products</b>: <p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-2.png" alt="" /></p></li>
    <li>No campo de busca de produtos, inclua o nome do produto desejado e escolha-o na lista.</li>
    <li>Para apagar um produto da lista, clique no ícone de "X" na frente do produto desejado.</li>
    <li>Quando você terminar, clique num dos botões para salvar: <b>Save and Stay</b> ou <b>Save</b>.</li>
</ol>