<h3>TM Homepage Products Carousel</h3>

<p>Esse módulo adiciona um slider/carrossel à página principal.</p>

<h5>Instalação e Remoção</h5>

<p>O módulo pode ser instalado e apagado como qualquer outro módulo do PrestaShop</p>

<h5>Configuração de Módulo</h5>

<p>Configure acessando a página de configurações do módulo.</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Use carousel</dt>
            <dd>enable/disable carousel</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Number of elements to display</dt>
            <dd>quantidade de itens para mostrar</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Items scroll</dt>
            <dd>quantidade de itens que entram em cena</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Width</dt>
            <dd>largura de um item na lista de slides</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Margin</dt>
            <dd>margem entre os itens</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel speed</dt>
            <dd>duração da animação de transição</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pause</dt>
            <dd>quantidade de tempo em que um slider fica parado (no caso do "Auto scroll" estar habilitado)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto scroll</dt>
            <dd>os slides ficam passando automáticamente</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Random</dt>
            <dd>inicia o slider num item aleatório</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel loop</dt>
            <dd>o slider volta para o primeiro item quando estiver no fim da lista de slides</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hide control on end</dt>
            <dd>esconde a navegação (botões de próximo/anterior) no último slide.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pager</dt>
            <dd>mostra paginação</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Control</dt>
            <dd>mostra botões de próximo/anterior</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto control</dt>
            <dd>mostra botões de tocar/parar</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto hover</dt>
            <dd>para o slider/carrossel quando o mouse passar em cima dos itens</dd>
        </dl>
    </li>
</ul>

