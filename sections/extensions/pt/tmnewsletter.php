<h3>TM Newsletter</h3>

<p>Esse módulo inclui uma notificação com a proposta do visitante incluir-se na lista de
    emails de sua loja. A confirmação de email pode ser alterada nas configurações do
    módulo. Você também pode ajustar o tempo entre lembretes para usuários cadastrados
    ou não. Usuários cadastrados podem escolher não ver mais esses lembretes.</p>
<h5>Instalção do módulo</h5>

<p>Esse módulo pode ser instalado e desinstalado como qualquer outro módulo do
    PrestaShop.</p>
<h5>Configurações</h5>

<p>Após instalado, você pode ajustá-lo na página de configurações do módulo.</p>

<ol class="index-list">
    <li>Live mode - ativa/desativa o módulo.</li>
    <li>Would you like to send a verification email after subscription? -
        ativa/desativa se será enviada confirmação de email.
    </li>
    <li>Timeout for guests - define a quantidade de tempo entre cada lembrete para
        usuários convidados.
    </li>
    <li>4Timeout for customers - define a quantidade de tempo entre cada lembrete para
        usuários cadastrados.
    </li>
</ol>
<p class="alert alert-warning">
    É preciso instalar e ativar "Newsletter block" para este módulo funcionar.
</p>

