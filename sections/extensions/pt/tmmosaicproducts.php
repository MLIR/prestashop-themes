<h3>TM Mosaic Products</h3>

<p>Este módulo permite exibir categorias com produtos, banners na página principal do site.</p>

<h5>Instalação e Remoção</h5>

<p>O módulo pode ser instalar ou apagado como qualquer outro módulo do PrestaShop.</p>

<h5>Configurações do módulo</h5>

<p>Após cada módulo ser instalado, você pode incluir os blocos necessários com categorias de produtos, banners incluídos anteriormente.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-1.png" alt=""/>
</figure>

<h5>Inclusão/Remoção/Alteração de blocos</h5>

<p>Para incluir uma nova categoria, clique no botão "Add new".</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-2.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Select category</strong> - escolha a categoria onde incluir produtos, banners.</li>
    <li><strong>Use custom name</strong> - estado personalizado da categoria.</li>
    <li><strong>Custom name</strong> - título da categoria.</li>
     <li><strong>Status</strong> - estado do item.</li>
</ol>

<p>Para incluir uma nova linha, clique no botão "Add row". Então escolha entre oito layouts apresentados na janela seguinte.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-3.png" alt=""/>
</figure>

<p>Para inlcuir o layout, clique em uma das oito opções, ele será incluído no formulário.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-4.png" alt=""/>
</figure>

<p>Para incluir itens, clique em uma das células do layout, você verá dois botões listando as opções disponíveis para inclusão de itens de determinado tipo.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-5.png" alt=""/>
</figure>

<p>Após clicar em um dos botões você verá um formulário para escolher o item do tipo especificado.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-6.png" alt=""/>
</figure>


<p>Após clicar em um dos itens, ele será incluído no layout. Você pode preencher o layout com diferentes blocos desta maneira.</p>.

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-7.png" alt=""/>
</figure>

<p>Após salvar a categoria com os layouts escolhidos, ela vai aparecer no forumlário de categorias adicionadas.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-8.png" alt=""/>
</figure>

<p>Para apagar um item, clique no botão "Delete".</p>

<h5>Inclusão/remoção/alteração de banner</h5>

<p>Para incluir um novo banner, clique em "Add new banner". Depois, preencha os campos necessários no formulário que aparecer.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-9.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Select a file</strong> - escolha um arquivo, este campo é obrigatório (ao menos no idioma padrão).</li>
    <li><strong>Title</strong> - título do banner, este campo é obrigatório (ao menos no idioma padrão).</li>
    <li><strong>Url</strong> - caminho do link, este campo é obrigatório (ao menos no idioma padrão).</li>
    <li><strong>Description</strong> - descrição do banner, este campo é obrigatório (ao menos no idioma padrão).</li>
    <li><strong>Specific class</strong> - classe CSS do bloco.</li>
</ol>