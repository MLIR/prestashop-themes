<h3>TM Google Map</h3>

<p>This module allows to add Google map with your stores location and short description
    about them on your front page. The map will show up the addresses of all stores
    related to your online shop. You can edit your stores details under
    Preferences->Store Contacts tab.</p>
<h5>Installation and Deleting</h5>

<p>The module is installed and deleted like any other Prestashop module.</p>
<h5>Module configuration</h5>

<p>After installation you can change its settings on the module configuration page.</p>

<p>You can configure the style and elements of your map.</p>

<p>Select the map style from the first list (all styles are taken from <a
        target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>). You can also add
    the style from this website. Find instructions on adding them at the end of this
    instruction.</p>

<p>Select the view type from the second list (satellite view or street view).</p>

<p>You can also select the zoom ratio for your map from 1 to 17.</p>

<p>Zoom option if scrolling on the map area.</p>

<p>You can disable the map control and ability to view the streets.</p>

<p>The module supports the multi-stores. You can change the map styles for each
    store.</p>
