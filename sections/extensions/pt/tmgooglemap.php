<h5>Adding custom styles</h5>

<p>You can add any custom style from <a target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>
    to your module. To do this, you should select the style from the website, copy
    JavaScript Style Array to .js file and save it in the module folder /js/styles/(for
    the file name use the style name replacing the spaces with underscores and removing
    all special characters. For example: for Subtle Grayscale style -
    subtle_grayscale.js file, for Apple Maps-esque style - apple_maps-esque.js file).
    Then, navigate to your module configuration page and select the required style from
    the styles drop-down list.</p>
