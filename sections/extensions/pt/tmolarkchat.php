
    <h3>TM Olark Chat</h3>

    <p>Módulo para mostrar o bate-papo Olark em sua loja.</p>
    <h5>Instalar/desinstalar módulo</h5>

    <p>Este módulo pode ser instalado/desinstalado como qualquer outro módulo do
        PrestaShop.</p>
    <h5>Configurações</h5>

    <p>Após instalado, o módulo pode ser configurado na página de configurações do
        módulo.</p>

    <p>Para configurar o bate-papo Olark, que você pode ver na página de demonstração do
        tema, você precisa se cadastrar no <a href="http://www.olark.com/" target="_blank"
                                              title="Olark Registration">site oficial</a> e
        pegar o número de ID para o seu site. Inclua o seu número de ID no campo "Olark site
        ID" e configure em "Live mode". Agora grave as alterações ao clicar no botão
        <strong>Salvar</strong>.</p>
