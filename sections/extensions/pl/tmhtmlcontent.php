<h3>TM HTML Content</h3>

<p>Ten moduł pozwala na wyświetlanie jakichkolwiek treści (zdjęcie, HTML, linki, itp.) w następujących pozycjach:
    displayNav, home, top, topColumn, w lewo i w prawo (displayLeftColumn / displayRightColumn), footer. Jest on
    stosowany w celu rozszerzenia możliwości projektowania i dodawania bloków reklam / informacji do sklepu. Jest to
    moduł wielojęzyczny i może również pracować na stronie typu Multistore.</p>

<h5>Aby dodać treść, należy wykonać następujące kroki:</h5>

<h6>Przejdź do
    <strong>Modules->Front Office Features->TM HTML Content</strong> (jeśli nie jest zainstalowany, zainstaluj go).
</h6>


<ol class="index-list">
    <li>Kliknij przycisk <strong>Add new</strong>.</li>

    <li>Lista dostępnych języków. Możesz przeglądać dostępne elementy dla wszystkich języków, przełączając zakładki
        języków. </li>

    <li>Lista elementów dodanych do konkretnego miejsca i języka.</li>
</ol>
<figure class="img-polaroid">
    <img src="img/htmlcontent.png" alt="">
</figure>

<h6>Dodawanie elementów. Kliknij przycisk
    <strong>Add item</strong> i wypełnij wymagane ustawienia (żadne pola nie są obowiązkowe).</h6>


<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Language</dt>
            <dd>wybierz wymagany język z rozwijanej listy.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Title</dt>
            <dd>tytuł elementu.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use title in front</dt>
            <dd>wyświetlenie tytułu elementu na frontendzie (tytuł będzie widoczny z przodu elementu).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hook</dt>
            <dd>miejsce, w którym elementem został dodany.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image</dt>
            <dd>prześlij obraz z komputera.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image width</dt>
            <dd>szerokość obrazu widocznego na frontendzie.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image height</dt>
            <dd>wysokość obrazu widocznego na frontendzie.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>URL</dt>
            <dd>dodaj link (cały blok będzie zapakowany w link).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Target blank</dt>
            <dd>otwórz link w nowej karcie.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>HTML</dt>
            <dd>kod HTML (z wyjątkiem linka, jeśli dodałeś go w polu URL) lub tekst.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Save</dt>
            <dd>kliknij, aby zapisać element.</dd>
        </dl>
    </li>
</ol>
<p class="alert alert-info"> Wszystkie elementy jednego języka, dodanego do tego samego miejsca, zostaną wyświetlone w
    postaci listy backend i frontend.</p>
