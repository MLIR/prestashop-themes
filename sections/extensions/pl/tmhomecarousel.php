    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Margin</dt>
            <dd>margines między elementami z listy karuzeli</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel speed</dt>
            <dd>czas trwania przejścia elementu</dd>
