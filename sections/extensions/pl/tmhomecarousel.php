<h3>TM Homepage Products Carousel</h3>

<p>Moduł dodaje karuzelę do zakładek głównej strony.</p>

<h5>Instalacja oraz Usuwanie</h5>

<p>Moduł może być instalowany i usuwany jak każdy inny moduł Prestashop.</p>

<h5>Konfiguracja Modułu</h5>

<p>Ustawienia zainstalowanego modułu można skonfigurować na stronie konfiguracji modułu.</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
