<h3>TM Social Login</h3>

<p>Moduł ten pozwala na rejestrację i zalogowanie się przy pomocy stron sieci
    społecznościowych jak Facebook lub Google.</p>
<h5>Instalacja/odinstalowanie modułu</h5>

<p>Ten moduł może być zainstalowany/odinstalowany tak jak każdy inny moduł
    PrestaShop.</p>
<h5>Ustawienia modułu</h5>

<p>Po zainstalowaniu, moduł ten może być skonfigurowany na stronie konfiguracyjnej
    modułu.</p>

<p>Aby otrzymać dane wymagane do skonfigurowania modułu, musisz stworzyć odpowiednią
    aplikację w wymaganym źródle.</p>

<p>Nie jest konieczne używanie obu wariantów logowania (Google, Facebook) jednocześnie,
    możesz używać tylko jednego z nich.</p>
<h4>Pola Facebooka</h4>
<ol class="index-list">
    <li>ID Twojej Aplikacji.</li>
    <li>Sekretny klucz Twojej Aplikacji.</li>
</ol>
<h4>Pola Google</h4>
<ol class="index-list">
    <li>ID Twojej Aplikacji.</li>
    <li>Sekretny klucz Twojej Aplikacji.</li>
    <li>Ścieżka przekierowania po zalogowaniu <strong>(twój sklep
            URL+index.php?fc=module&module=tmsociallogin&controller=googlelogin)</strong>.
    </li>
</ol>

<p class="alert alert-info">Więcej informacji otrzymasz pod następującymi linkami: <a
        href="https://developers.google.com/console/help/" target="_blank">Google
        App</a> oraz <a href="https://developers.facebook.com/apps/" target="_blank">Facebook
        App</a>.</p>

<p class="alert alert-warning">
    Ścieżka przekierowania musi zgadzać się z tą, podaną w czasie tworzenia Google App.</p>

