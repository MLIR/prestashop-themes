<h3>TM Related Products</h3>

<p>Ten moduł umożliwia dodawanie produktów powiązanych z dowolnym produktem sklepu. Wszystkie powiązane produkty są wyświetlane w karuzeli na stronach produktów, do których oni są przyporządkowane.</p>

<h5>Instalacja i Odinstalowanie</h5>

<p>Moduł jest instalowany i odinstalowany, w taki sam sposób jak każdy inny moduł PrestaShop.</p>

<h5>Ustawienia modułu</h5>

<p>Po zainstalowaniu modułu, można skonfigurować go na stronie konfiguracji modułu.</p>

<p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-1.png" alt="" /></p>

<ol>
    <li><strong>Wyświetlanie cen na produktach (Display price on products)</strong> - wyświetlanie ceny produktu.</li>
    <li><strong>Ilość wyświetlanych produktów (Number of displayed products)</strong> - ilość produktów, wyświetlanych na stronie.</li>
</ol>

<p>Można dodawać i usuwać powiązane produkty w ustawieniach produktów: <b>Katalog->Produkty (Catalog->Products)</b>.</p>

<ol>
    <li>Należy przejść na kartę <b>TM Related Products</b>: <p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-2.png" alt="" /></p></li>
    <li>W polu wyszukiwania produktów, należy podać nazwę potrzebnego produktu, a następnie wybrać go z listy.</li>
    <li>Aby usunąć produkt z listy, należy nacisnąć ikonę "krzyżyk" naprzeciwko potrzebnego produktu.</li>
    <li>Po zakończeniu, należy naciśnąć jeden z przycisków do zapisywania: <b>Zapisz i Zostań (Save and Stay)</b> lub <b>Zapisz (Save)</b>.</li>
</ol>