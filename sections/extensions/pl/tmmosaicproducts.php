<h3>TM Mosaic Products</h3>

<p>Ten moduł pozwala na wyświetlanie kategorii z produktami, banerami na głównej stronie witryny.</p>

<h5>Instalacja i Usuwanie</h5>

<p>Instalacja oraz usuwanie modułu są takie same jak każdego innego modułu PrestaShop.</p>

<h5>Ustawienia modułu</h5>

<p>Po zakończeniu instalacji modułu, można dodać wymagane bloki z produktami kategoria, wcześniej dodane banery.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-1.png" alt=""/>
</figure>

<h5>Dodawanie/usunięcie/edytowanie Bloków</h5>

<p>W celu dodania nowej kategorii, należy nacisnąć przycisk "Dodaj nową".</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-2.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Wybierz kategorię (Select category)</strong> - należy wybrać kategorię, aby dodać do niej produkty, bannery.</li>
    <li><strong>Użyj niestandardowej nazwy (Use custom name)</strong> - niestandardowy status kategorii.</li>
    <li><strong>Niestandardowa nazwa (Custom name)</strong> - nazwa kategorii.</li>
     <li><strong>Status</strong> - status elementu.</li>
</ol>

<p>W celu dodania nowego wiersza, należy nacisnąć przycisk "Dodaj wiersz". Następnie należy wybrać jeden z ośmiu układów przedstawionych w poniższym oknie.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-3.png" alt=""/>
</figure>

<p>W celu dodania układu, należy kliknąć na jedną z ośmiu opcji aby go wybrać, a wtedy on zostanie dodany do formularza.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-4.png" alt=""/>
</figure>

<p>W celu dodania elementów, należy kliknąć na jedną z komórek układu, pojawią się dwa przycisków z listą dostępnych opcji, umożliwiających dodawanie elementu określonego typu.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-5.png" alt=""/>
</figure>

<p>Po naciśnięciu jednego z przycisków pojawi się formularz dotyczący wyboru elementu wyznaczonego typu.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-6.png" alt=""/>
</figure>


<p>Po naciśnięciu na jednym z elementów, zostanie dodany do komórki układu. W ten sposób można wypełnić układy z różnymi blokami.</p>.

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-7.png" alt=""/>
</figure>

<p>Po zapisaniu kategorii z wybranymi układami, będzie ona podana w formie dodanych kategorii.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-8.png" alt=""/>
</figure>

<p>W celu usunięcia elementu, należy naciśnąć przycisk "Usuń".</p>

<h5>Dodawanie/usunięcie/edytowanie Banera</h5>

<p>W celu dodania nowego banera, należy kliknąć na przycisk "Dodaj nowy baner". Następnie należy wypełnić wymagane pola w wyświetlonym formularze.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-9.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Wybierz plik (Select a file)</strong> - należy wybrać plik, to pole jest wymagane (przynajmniej dla domyślnego języka).</li>
    <li><strong>Nazwa (Title)</strong> - nazwa banera, to pole jest wymagane (przynajmniej dla domyślnego języka).</li>
    <li><strong>Url</strong> - ścieżka łącza, to pole jest wymagane (przynajmniej dla domyślnego języka).</li>
    <li><strong>Opis (Description)</strong> - opis banera (przynajmniej dla domyślnego języka).</li>
    <li><strong>Szczególne klasy (Specific class)</strong> - klasa CSS bloka.</li>
</ol>