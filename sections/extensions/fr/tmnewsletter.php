<h3>TM Newsletter</h3>

<p>Le module affiche une notification avec une proposition visant à inscrire aux
    newsletters de boutique dans une fenêtre pop-up. Le contrôle électronique peut être
    activé dans les paramètres du module au moyen d'email de confirmation. Vous pouvez
    également régler le délai d'attente entre les rappels pour les utilisateurs
    enregistrés et non enregistrés. Les utilisateurs enregistrés peuvent renoncer aux
    rappels de façon permanente.</p>
<h5>Installer / Désinstaller le Module</h5>

<p>Ce module peut être installé / désinstallé comme tout autre module PrestaShop.</p>
<h5>Paramètres du module</h5>

<p>Après le module a été installé, vous pouvez le configurer sur la page de
    configuration du module.</p>

<ol class="index-list">
    <li>Mode en direct - activer / désactiver le module.</li>
    <li>Voulez-vous envoyer l'email de vérification après la souscription?- Activer /
        désactiver l'envoi d' un email de confirmation.
    </li>
    <li>Délai d'attente pour les hôtes - définir le délai pour le popup rappelant à
        la subscription pour les hôtes.
    </li>
    <li>Délai d'attente pour les clients - définir le délai pour le popup rappelant à
        la subscription pour les clients.
    </li>
</ol>
<p class="alert alert-warning">
    Il est nécessaire d'installer et activer "Newsletter block" pour le bon travail de module.
</p>

