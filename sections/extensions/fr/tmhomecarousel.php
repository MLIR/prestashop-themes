<h3>Carrousel de produits TM de la page d'accueil</h3>

<p>Le module ajoute un carrousel pour les onglets de la page principale.</p>

<h5>Installation et Suppression</h5>

<p>Le module peut être installé et supprimé comme tout autre module PrestaShop.</p>

<h5>Configuration de Module</h5>

<p>Les paramètres du module installé peuvent être configurés sur la page de configuration du module.</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Use carousel</dt>
            <dd>activer / désactiver le carrousel</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Number of elements to display</dt>
            <dd>le nombre d'éléments pour afficher</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Items scroll</dt>
            <dd>le nombre d'éléments pour faire défiler</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Width</dt>
            <dd>la largeur d'un élément de la liste de carrousel</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Margin</dt>
            <dd>la marge entre les éléments de la liste de carrousel</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel speed</dt>
            <dd>la durée de la transition d'un élément</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pause</dt>
            <dd>la quantité de temps entre chaque transition automatique (si l'option "Auto scroll" est activée)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto scroll</dt>
            <dd>activer le défilement automatique du carrousel</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Random</dt>
            <dd>damarer le carrousel sur un élément aléatoire</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel loop</dt>
            <dd>le carrousel retourne au début lorsque tous les éléments ont été défilés</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hide control on end</dt>
            <dd>cacher les boutons (suivant / précédent) de la navigation sur la dernière diapositive</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pager</dt>
            <dd>montrer la pagination</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Control</dt>
            <dd>montrer les boutons Précédent / Suivant</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto control</dt>
            <dd>montrer les boutons Marche / Pause</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto hover</dt>
            <dd>mettre en pause le carrousel lorsque la souris passe sur les éléments de carrousel</dd>
        </dl>
    </li>
</ul>

