<h3>TM Related Products</h3>

<p>Ce module vous permet d'ajouter des produits liés à tout produit de votre boutique. Tous les produits liés sont affichés dans un carrousel sur les pages produits auxquels ils sont assignés.</p>

<h5>Installation et Désinstallation</h5>

<p>Le module peut être installé et désinstallé comme tout autre module PrestaShop.</p>

<h5>Paramètres du module</h5>

<p>Après l'installation du module, vous pouvez le configurer sur la page de configuration du module.</p>

<p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-1.png" alt="" /></p>

<ol>
    <li><strong>Afficher le prix sur les produits</strong> - afficher le prix du produit.</li>
    <li><strong>Nombre de produits affichés</strong> - le nombre de produits affichés sur la page.</li>
</ol>

<p>Vous pouvez ajouter et supprimer des produits liés dans les paramètres du produit: <b>Catalogue -> Produits (Catalog->Products)</b>.</p>

<ol>
    <li>Allez sur l'onglet<b>TM Related Products</b>: <p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-2.png" alt="" /></p></li>
    <li>Dans le champ de recherche de produits, entrez le nom du produit  et sélectionnez le dans la liste.</li>
    <li>Pour supprimer un produit de la liste, appuyez sur l'icône "Cross" à coté du produit.</li>
    <li>Lorsque vous avez terminé, appuyez sur un des boutons pour enregistrer: <b>Enregistrer et rester</b> ou <b>Enregistrer</b> (<b>Save and Stay</b> or <b>Save</b>).</li>
</ol>