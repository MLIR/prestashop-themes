<h3>TM Mosaic Products</h3>

<p>Ce module vous permet d'afficher les catégories avec des produits, des bannières sur la page d'accueil du site</p>

<h5>Installation et Suppression</h5>

<p>Le module peut peut être installé et supprimé comme tout autre module PrestaShop.</p>

<h5>Paramètres du module</h5>

<p>Une fois le module a été installé, vous pouvez ajouter les blocs nécessaires avec les produits de la catégorie, bannières.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-1.png" alt=""/>
</figure>

<h5>Ajout/suppression/modification de Blocs</h5>

<p>Pour ajouter une nouvelle catégorie, appuyez sur le bouton "Ajouter" ("Add new").</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-2.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Choisir une catégorie (Select category)</strong> - choisisssez la catégorie pour laquelle vous souhaitez ajouter les produits, les bannières.</li>
    <li><strong>Utiliser le nom personnalisé (Use custom name)</strong> - statut de la catégorie personnalisée.</li>
    <li><strong>Nom personnalisé (Custom name)</strong> - titre de catégorie.</li>
     <li><strong>Statut (Status)</strong> - état de l'élément.</li>
</ol>

<p>Pour ajouter une nouvelle ligne, appuyez sur le bouton "Ajouter une ligne" ("Add row"). Ensuite, choisissez l'une des huit mises en page présentées dans la fenêtre suivante.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-3.png" alt=""/>
</figure>

<p>Pour ajouter la mise en page, cliquez sur l'une des huit options à choisir, et elle sera ajoutée.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-4.png" alt=""/>
</figure>

<p>Pour ajouter des éléments, cliquez sur l'une des cellules de mise en page, vous verrez deux boutons énumérant les options disponibles pour ajouter l'élément de certain type.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-5.png" alt=""/>
</figure>

<p>Après avoir appuyé sur l'un des boutons, vous verrez le formulaire pour choisir l'élément du type choisi.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-6.png" alt=""/>
</figure>


<p>Après avoir cliqué sur l'un des éléments, il sera ajouté à la cellule de mise en page. De cette façon vous pouvez remplir la mise en page avec les différents blocs.</p>.

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-7.png" alt=""/>
</figure>

<p>Après avoir enregistré la catégorie avec des mises en page choisies, elle apparaîtra sous la forme des catégories ajoutées.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-8.png" alt=""/>
</figure>

<p>Afin de supprimer l'élément, appuyez sur le bouton "Supprimer" ("Delete").</p>

<h5>Bannière ajout/suppression/modification</h5>

<p>Afin d'ajouter une nouvelle bannière, cliquez sur le bouton "Ajouter une nouvelle bannière" ("Add new banner"). Ensuite, remplissez les champs obligatoires du formulaire qui apparaît.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-9.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Choisir un fichier (Select a file)</strong> - choisir un fichier, ce champ est obligatoire (au moins pour la langue par défaut).</li>
    <li><strong>Titre (Title)</strong> - titre de la bannière, ce champ est obligatoire (au moins pour la langue par défaut).</li>
    <li><strong>Url (Url)</strong> - lien, ce champ est obligatoire (au moins pour la langue par défaut).</li>
    <li><strong>Description (Description) </strong> - description de la bannière (au moins pour la langue par défaut).</li>
    <li><strong>Classe spécifique (Specific class)</strong> - classe CSS de bloc.</li>
</ol>