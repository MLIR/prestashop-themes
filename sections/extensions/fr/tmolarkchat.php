<h3>TM Olark Chat</h3>

<p>Le module pour afficher Olark Chat sur le frontale de votre boutique.</p>
<h5>Le module installer / désinstaller</h5>

<p>Ce module peut être installé / désinstallé comme tout autre module PrestaShop.</p>
<h5>Paramètres du module</h5>

<p>Après le module a été installé, vous pouvez le configurer sur la page de
    configuration du module.</p>

<p>Pour configurer Olark Chat, que vous pouvez voir sur une démonstration en direct de
    la modèle, vous devez vous inscrire au site officiel <a href="http://www.olark.com/"
                                                            target="_blank"
                                                            title="Olark Registration"></a>pour
    obtenir l'ID de votre site web. Entrez votre ID au champ "ID de Site Olark" et
    définissez le module sous "Mode en direct". Maintenant enregistrez les paramètres en
    cliquant sur le bouton <strong>Enregistrer</strong>.</p>

