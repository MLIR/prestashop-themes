<h3>TM Media Parallax 2.0</h3>
<p>Ce module est conçu pour vous permettre d'ajouter l'effet de parallaxe aux éléments DOM de la page. Il vous permet de créer parallaxe multi-couches. Il fournit également une possibilité d'ajouter une vidéo d'arrière-plan (vous pouvez le télécharger à partir de votre ordinateur au serveur) ou ajouter une vidéo YouTube.</p>
<h4 id="tmmediaparallax-mainpanel">TM Media Parallax Panneau principal</h4>
<p>Panneau principal de module vous permet d'ajouter, de modifier ou de supprimer des éléments de parallaxe. </p>
<figure class="img-polaroid"><img src="img/main-panel.jpg" alt="" /></figure>
<h4 id="tmmediaparallax-add">Création d'un nouvel élément de parallaxe</h4>
<p>Afin de créer un nouvel élément de parallaxe, appuyez sur l'icône 'plus'(1), en haut à droite du panneau principal.</p>
<figure class="img-polaroid"><img src="img/main-panel-add-item.jpg" alt="" /></figure>
<p>Ensuite, vous verrez le formulaire suivant.</p>
<figure class="img-polaroid"><img src="img/add-item.jpg" alt="" /></figure>
<h4>Options de formulaire pour ajouter des éléments de parallaxe</h4>
<ul class="index-list">
    <li>Sélecteur (Selector) - sélecteur CSS de l'élément avec effet de parallaxe. Par exemple, '#header .nav'</li>
    <li>Statut d'élément (Item status) - statut d'élément (parallaxe ne sera pas affichée s'il est désactivé)</li>
    <li>Vitesse (Speed) - valeur de chiffre de 0 à 2, qui définit la vitesse du bloc de parallaxe</li>
    <li>Offset (Offset) - décalage de couche principale sur l'axe Y</li>
    <li>Inverse (Inverse) - direction de parallaxe</li>
    <li>Disparition graduelle (Fade) - cette option permet de configurer la disparition graduelle de bloc principal quand la page défile vers le bas</li>
    <li>Forcer toute la largeur (Forced full width) - toute la largeur du bloc de parallaxe</li>
</ul>
Appuyez sur 'Enregistrer' après avoir rempli tous les champs obligatoires.
