<h4 id="file-manager">Téléchargement de la vidéo et des images au serveur</h4>
<p>Pour télécharger la vidéo ou une image au serveur via le Gestionnaire de fichiers, vous devez d'abord appuyer sur icône 'file' (1) à côté du champ nécessaire.</p>
<figure class="img-polaroid"><img src="img/upload-file.jpg" alt="" /></figure>
<p>Dans la fenêtre suivante, dans la ligne 'actions', choisissez 'upload' (2)</p>
<figure class="img-polaroid"><img src="img/upload-file-2.jpg" alt="" /></figure>
<p>Ensuite, vous verrez une autre fenêtre pop-up pour télécharger le fichier image ou vidéo.</p>
<h4 id="layout-info">Avertissements et recommandations</h4>
<ul class="index-list">
    <li>Les appareils mobiles (smartphones, tablettes) ont effet de parallaxe désactivé par défaut, la couche d'image est utilisé comme un arrière-plan à sa place</li>
    <li>Pleine largeur du bloc parallaxe affecte la vitesse de chargement des pages</li>
    <li>La petite taille de la vidéo est recommandée</li>
</ul>