<h3>Produits Connexes TM</h3>

<p>Le module donné a été conçu afin d'ajouter et afficher les produits connexes sur la
    page du produit.</p>

<h5>Module installer / désinstaller</h5>

<p>Ce module peut être installé / désinstallé comme tout autre module PrestaShop.</p>

<h5>Paramètres du module</h5>

<p>Après le module a été installé, vous pouvez le configurer sur la page de
    configuration du module.</p>
<ol class="index-list">
    <li>Afficher des prix des produits.</li>
    <li>Quantité de produits à afficher.</li>
</ol>

<figure class="img-polaroid">
    <img src="img/tmrelatedproducts-1.png" alt="">
</figure>
<p>Vous pouvez ajouter ou supprimer des produits connexes pour le produit principal dans
    les paramètres du produit <strong>Catalog->Products</strong>.</p>

<ol class="index-list">
    <li>Naviguez vers <strong>Produits Connexes TM</strong>
    </li>
    <li>Dans le champ de recherche de produits, commencez à entrer le titre du
        produit et le choisissez parmi les variantes offertes;
    </li>
    <li>Pour retirer le produit de la liste cliquez sur le X avant que le produit;
    </li>
    <li>Une fois toute la configuration ont été apportée cliquez sur le bouton
        <strong>Enregistrer et Rester</strong> ou <strong>Enregistrer</strong>.
    </li>
</ol>

