<h3><a href="http://special.olark.com/cDqdW ">TM Olark Chat</a></h3>

<p>Un modulo per inserire una Olark Chat nel front-end del tuo negozio.</p>
<h5>Installazione/disinstallazione del modulo</h5>

<p>Questo modulo va installato o disinstallato come tutti gli altri moduli
    PrestaShop.</p>
<h5>Impostazioni modulo</h5>

<p>Dopo che il modulo è stato installato, sarà possibile configurarlo nella apposita
    pagina di configurazione del modulo.</p>

<p>Per configurare Olark Chat, che puoi vedere nella demo live del template, devi prima
    di tutto registrarti sul <a href="http://www.olark.com/" target="_blank"
                                title="Olark Registration">sito ufficiale</a> e ottenere
    un identificativo (ID) per il tuo sito web. Inserisci il tuo ID nel campo "Olark
    site ID" e imposta il modulo in modalità "Live mode". Poi salva le nuove
    impostazioni cliccando sul pulsante <strong>Save</strong>.</p>


