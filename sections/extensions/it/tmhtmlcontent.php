    <img src="img/htmlcontent-1.png" alt="">
</figure>


<h6>Modificare e rimuovere l'elemento</h6>

<p>Quando aggiungi l'elemento, verrà mostrato nella lista corrispondente.</p>

<ol class="index-list">
    <li>La lingua in cui sono stati aggiunti gli elementi.</li>

    <li>La posizione dove sono stati aggiunti gli elementi.</li>

    <li>Pulsante per le modifiche con menù a tendina.</li>

    <li>Pulsanet Elimina.</li>
</ol>
