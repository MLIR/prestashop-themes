<h3>TM HTML Content</h3>

<p>Questo modulo permette di visualizzare qualunque contenuto (immagine, HTML, link, ecc.) nelle seguenti posizioni:
    displayNav, home, top, topColumn, destra e sinistra (displayLeftColumn/displayRightColumn), footer. È utilizzato per
    allargare le possibilità di design e per aggiungere blocchi con pubblicità/informazioni al tuo store. È un modulo
    multilingua e può anche funzionare nel sito web multistore.</p>

<h5>Per aggiungere contenuti segui questa procedura:</h5>


<h6>Vai su
    <strong>Modules->Front Office Features->TM HTML Content</strong> (installalo se non è ancora installato).
</h6>


<ol class="index-list">
    <li>Clicca su <strong>Add new</strong>.</li>

    <li>Lista di lingue disponibili. Puoi visualizzare gli elementi disponibili per tutte le lingue cambiando la scheda
        delle lingue. </li>

    <li>Lista di elementi aggiunti per specifiche posizioni e lingue.</li>
</ol>
<figure class="img-polaroid">
    <img src="img/htmlcontent.png" alt="">
</figure>


<h6>Aggiungere l'elemento. Clicca su
    <strong>Add item</strong> e inserisci le impostazioni appropriate (nessuno dei campi è obbligatorio).
</h6>


<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Language</dt>
            <dd>seleziona la lingua desiderata dal menù a tendina.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Title</dt>
            <dd>titolo dell'elemento.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use title in front</dt>
            <dd>visualizza il titolo dell'elemento sul frontend (il titolo apparirà davanti all'elemento).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hook</dt>
            <dd>posizione in cui l'elemento è aggiunto.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image</dt>
            <dd>carica l'immagine dal tuo computer.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image width</dt>
            <dd>larghezza immagine mostrata sul frontend.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image height</dt>
            <dd>lunghezza immagine mostrata sul frontend..</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>URL</dt>
            <dd>aggiungi il link (tutto il blocco verrà avvolto nel link).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Target blank</dt>
            <dd>apri il link nella nuova scheda.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>HTML</dt>
            <dd>codice HTML (tranne il link se lo hai aggiunto nel campo URL) o testo.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Save</dt>
            <dd>clicca per salvare l'elemento.</dd>
        </dl>
    </li>
</ol>
<p class="alert alert-info">Tutti gli elementi di una lingua aggiunta nella stessa posizione appariranno come lista nel
    backend e nel frontend.</p>
