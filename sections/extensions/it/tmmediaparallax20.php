<h4 id="file-manager">Caricamento video e immagini al server</h4>
<p>Al fine di caricare video o un'immagine per server tramite File Manager, in primo luogo è necessario premere 'file'(1) accanto al campo necessario.</p>
<figure class="img-polaroid"><img src="img/upload-file.jpg" alt="" /></figure>
<p>Nella finestra successiva, nella linea di 'azioni', scegliere 'upload'(2)</p>
<figure class="img-polaroid"><img src="img/upload-file-2.jpg" alt="" /></figure>
<p>Poi si vedrà ancora una finestra pop-up, di caricare immagini o file video qui.</p>
<h4 id="layout-info">Avvertenze e raccomandazioni</h4>
<ul class="index-list">
    <li>I dispositivi mobili (smartphone, tablet) hanno effetto di parallax disabilitata per default, il livello dell'immagine blocco superiore viene utilizzato invece come sfondo </li>
    <li>Larghezza forzata del blocco di parallax influenza la velocità di caricamento delle pagine più critiche di una normale larghezza del blocco di carico</li>
    <li>Minore è la dimensione del video caricato, meglio è.</li>
</ul>