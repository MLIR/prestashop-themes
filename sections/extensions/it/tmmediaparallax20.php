<h3>TM Media Parallax 2.0</h3>
<p>Questo modulo è progettato per consentire di aggiungere effetto di parallax agli elementi DOM della pagina. Esso consente di creare parallax multistrato. Esso fornisce anche la possibilità di aggiungere un video di sfondo personalizzato (si può caricare dal computer al server) o aggiungere un video di YouTube.</p>
<h4 id="tmmediaparallax-mainpanel">TM Media Parallax Pannello Principale</h4>
<p>Pannello modulo principale che consente di aggiungere, modificare o rimuovere elementi di parallax. </p>
<figure class="img-polaroid"><img src="img/main-panel.jpg" alt="" /></figure>
<h4 id="tmmediaparallax-add">La creazione di un nuovo elemento di parallax</h4>
<p>Al fine di creare un nuovo elemento di parallax, premere 'plus'(1) icona nell'angulo in alto a destra del pannello principale.</p>
<figure class="img-polaroid"><img src="img/main-panel-add-item.jpg" alt="" /></figure>
<p>Poi si vedrà il seguente modulo.</p>
<figure class="img-polaroid"><img src="img/add-item.jpg" alt="" /></figure>
<h4>Opzioni modulo per l'aggiunta di elementi di parallax</h4>
<ul class="index-list">
    <li>Selettore - selettore CSS della voce con effetto di parallax applicata. Per esempio, '#header .nav'</li>
    <li>Stato articulo - stato articulo (parallax non verrà visualizzato se disabilitato)</li>
    <li>Valore della cifra da 0 a 2, che definisce principale velocità blocco di parallax - velocità</li>
    <li>Compensare - trattino strato principale sull'asse Y</li>
    <li>Inverse - direzione di parallax</li>
    <li>Dissulvenza - questa opzione consente di configurare blocco dissulvenza principale a pagina scorrimento</li>
    <li>Costretto larghezza - ha costretto tutta la larghezza del blocco di parallax</li>
</ul>
Premere 'Salva' dopo aver compilato tutti i campi obbligatori.
