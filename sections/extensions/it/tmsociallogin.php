<h3>TM Social Login</h3>

<p>Questo modulo ti consente di far registrare e accedere i tuoi clienti attraverso le
    loro pagine social, come Facebook e Google.</p>

<h5>Installazione/disinstallazione del modulo</h5>

<p>Questo modulo va installato o disinstallato come tutti gli altri moduli
    PrestaShop</p>

<h5>Impostazioni modulo</h5>

<p>Dopo che il modulo è stato installato, sarà possibile configurarlo nella apposita
    pagina di configurazione del modulo.</p>

<p>Per ottenere i dati richiesti per la configurazione del modulo, devi creare
    un'applicazione corrispondente sulla risorsa necessaria.</p>

<p>Non è necessario usare entrambe le possibilità per accedere simultaneamente (Google e
    Facebook); puoi usarne anche solo una.</p>

<h4>Campi Facebook</h4>

<ol class="index-list">
    <li>ID della tua App.</li>
    <li>Codice segreto della tua App.</li>
</ol>
<h4>Campi Google</h4>

<ol class="index-list">
    <li>ID della tua App.</li>
    <li>Codice segreto della tua App.</li>
    <li>Reindirizza il percorso dopo l'accesso <strong>(URL del tuo negozio
            URL+index.php?fc=module&module=tmsociallogin&controller=googlelogin)</strong>.

    </li>
</ol>


<p class="alert alert-info">Puoi trovare altri dettagli seguendo questi link: <a
        href="https://developers.google.com/console/help/" target="_blank">Google
        App</a> e <a href="https://developers.facebook.com/apps/" target="_blank">Facebook
        App</a>.</p>
<p class="alert alert-warning">
    Il percorso di reindirizzamento deve coincidere con quello che hai indicato durante la creazione della
    app Google.</p>

