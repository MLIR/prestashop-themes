<h3>TM Newsletter</h3>

<p>Questo modulo permette di far apparire in una finestra pop-up una notifica con invito
    a iscriversi alla newsletter del negozio online. La verifica delle email può essere
    abilitata nelle impostazioni del modulo attraverso conferma via posta elettronica.
    Si può anche regolare il timeout per promemoria tra utenti registrati e non
    registrati. Gli utenti registrati possono disabilitare i promemoria in maniera
    permanente.</p>
<h5>Installazione/disinstallazione modulo</h5>

<p>Questo modulo può essere installato e disinstallato come tutti gli altri moduli
    PrestaShop.</p>
<h5>Impostazioni modulo</h5>

<p>Dopo che il modulo è stato installato puoi configurarlo sulla pagina di
    configurazione.</p>

<ol class="index-list">
    <li>1. Live mode - abilita/disabilita il modulo.</li>
    <li>2. Would you like to send a verification email after subscription? -
        abilita/disabilita l'invio di email di conferma.
    </li>
    <li>3. Timeout for guests - imposta l'intervallo di timeout per il pop-up di
        promemoria per ricordare ai visitatori di iscriversi.
    </li>
    <li>4. Timeout for customers - imposta l'intervallo di timeout per il pop-up di
        promemoria per ricordare ai clienti di iscriversi.
    </li>
</ol>
<p class="alert alert-warning">
    Per il corretto funzionamento del modulo è necessario installare e abilitare "Newsletter block".
</p>

