<h3>TM Mosaic Products</h3>

<p>Questo modulo permette di visualizzare le categorie con i prodotti, le bandiere sulla home page del sito.</p>

<h5>Installazione ed eliminazione</h5>

<p>Il modulo viene installato e cancellato come qualsiasi altro modulo PrestaShop.</p>

<h5>Impostazioni del modulo</h5>

<p>Dopo che il modulo è stato installato, è possibile aggiungere i blocchi necessari con i prodotti di categoria, le bandiere aggiuntive.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-1.png" alt=""/>
</figure>

<h5>Aggiunta/rimozione/modifica Blocchi</h5>

<p>Per aggiungere una nuova categoria, premere il tasto "Aggiungi nuovo" ("Add new").</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-2.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Seleziona categoria (Select category)</strong> - scegliere una categoria per aggiungere prodotti, banner.</li>
    <li><strong>Utilizzare nome personalizzato (Use custom name)</strong> - stato categoria personalizzata.</li>
    <li><strong>Nome personalizzato (Custom name)</strong> - categoria titolo.</li>
     <li><strong>Stato (Status)</strong> - stato elemento.</li>
</ol>

<p>Per aggiungere la nuova riga, premere il tasto "Aggiungi riga" ("Add row"). Quindi scegliere uno degli otto schemi presentati nella finestra seguente.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-3.png" alt=""/>
</figure>

<p>Per aggiungere il layout, clicca su una delle otto opzioni per raccoglierlo, e sarà aggiunto alla forma successivamente.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-4.png" alt=""/>
</figure>

<p>Per aggiungere elementi, fare clic su una delle celle di layout, vedrete due pulsanti che elencano le opzioni disponibili per aggiungere l'articolo di un certo tipo.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-5.png" alt=""/>
</figure>

<p>Dopo aver premuto uno dei tasti si vedrà il modulo per la scelta della voce del tipo scelto.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-6.png" alt=""/>
</figure>


<p>Dopo aver fatto clic su uno degli elementi, verrà aggiunto alla cella layout. È possibile compilare il layout con diversi blocchi nello stesso modo.</p>.

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-7.png" alt=""/>
</figure>

<p>Dopo aver salvato la categoria con layout scelto, verrà visualizzato sotto forma di categorie aggiunte.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-8.png" alt=""/>
</figure>

<p>Al fine di rimuovere l'elemento, premere il pulsante "Elimina" ("Delete").</p>

<h5>Banner aggiunta/rimozione/modifica</h5>

<p>Per aggiungere un nuovo banner, cliccare sul pulsante "Aggiungi nuovo banner" ("Add new banner"). Successivamente, compilare i campi obbligatori nel modulo che apparirà.</p>

<figure class="img-polaroid">
    <img src="img/tmmosaicproducts-9.png" alt=""/>
</figure>

<ol class="index-list">
    <li><strong>Selezionare un file (Select a file)</strong> - scegliere un file, questo campo è obbligatorio (almeno per la lingua predefinita).</li>
    <li><strong>Titolo (Title)</strong> - titolo banner, questo campo è obbligatorio (almeno per la lingua predefinita).</li>
    <li><strong>Url (Url)</strong> - percorso del collegamento, questo campo è obbligatorio (almeno per la lingua predefinita).</li>
    <li><strong>Descrizione (Description)</strong> - bandiera descrizione (almeno per la lingua predefinita).</li>
    <li><strong>Classe specifica (Specific class)</strong> - classe CSS blocco.</li>
</ol>