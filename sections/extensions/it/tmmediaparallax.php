<h3>Modulo Media Parallax</h3>

<h4>Cos'è l'effetto parallasse?</h4>

<p>Lo scorrimento parallasse è una speciale tecnica di scorrimento in cui le immagini nello sfondo si muovono più
    lentamente rispetto alle immagini in primo piano, creando così un illusione di profondità anche, ad esempio, in un
    videogioco 2D, per creare una sensazione di 'immersione'. Gli sviluppatori web iniziarono a introdurre l'effetto
    parallasse nel 2011, sfruttando l'HTML5 e il CSS3. Utilizzare il parallasse per i siti web è una tecnica di design
    sempre più popolare ed è visto come un modo semplice ma efficace per rappresentare la fluidità del web. Inoltre, i
    sostenitori della tecnica usano gli sfondi in parallasse come uno strumento per meglio coinvolgere l'utente e
    migliorare l'esperienza globale offerta dal sito web. Lo scorrimento in parallasse permette di creare un'esperienza
    di navigazione dinamica e interattiva, migliorando anche l'aspetto visivo.</p>

<p>Questo modulo permette di aggiungere un effetto parallasse al tuo sito web senza particolari sforzi. È possibile
    aggiungere in sfondo sia immagini che video.</p>


