<h5>
    Aggiungere il modulo Media parallax
</h5>
<ol class="index-list">
    <li>È necessario aggiungere l'id o la classe nel contenuto della tua pagina HTML. Si può anche usare un tag unico,
        che non deve essere ripetuto. </li>

    <li>Crea l'immagine delle dimensioni necessarie (è consigliabile creare l'immagine di dimensioni più grandi rispetto
        al blocco in cui andrà inserito l'effetto). Se vuoi aggiungere un video, dovrai prepararlo in 3 formati:
        .mp4,.webm e .ogv, e anche aggiungere l'immagine di anteprima delle stesse dimensioni (.png, .jpg and
        .gif) </li>

    <li>
        <p>Clicca su
            <strong>Add container</strong>
            in
            <strong>Add new block</strong>
            .
        </p>

        <figure class="img-polaroid">
            <img src="img/paralax-1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Nella schermata che appare, inserisci tutte le impostazioni necessarie.</p>


        <h6>
            <strong>Impostazioni:</strong>
        </h6>

        <ol class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Selector</dt>
                    <dd>l'elemento a cui l'effetto verrà aggiunto (id o tag unico).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Parallax type</dt>
                    <dd>il tipo di effetto che verrà utilizzato (immagine o video).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Media files</dt>
                    <dd>per caricare tutti i file necessari.</dd>
                </dl>
            </li>
            <li>Clicca su
                <strong>Save</strong>
                .
            </li>
        </ol>
