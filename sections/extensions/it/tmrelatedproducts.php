<h3>TM Related Products</h3>

<p>Il modulo consente di aggiungere prodotti relativi a tutto il deposito dei prodotti. Tutti i prodotti correlati vengono visualizzati
    in una giostra sulla pagina del prodotto con con la quale è in collegamento.</p>

<h5>Installazione e rimozione</h5>

<p>Il modulo viene installato o rimosso come qualsiasi altro modulo di PrestaShop.</p>

<h5>Configurazione del modulo</h5>

<p>Dopo aver installato il modulo, è possibile configurarlo direttamente sulla pagina di configurazione del modulo.</p>

<p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-1.png" alt="" /></p>

<ol>
    <li><strong>Display price on products</strong> - Mostra prezzo del prodotto.</li>
    <li><strong>Number of displayed products</strong> - Il numero di prodotti esposti.</li>
</ol>

<p>Aggiungere o rimuovere i prodotti relativi al prodotto principale è possibile impostare su <b>Catalog->Products</b>.</p>

<ol>
    <li>Fare clic sulla scheda <b>TM Related Products</b><p class="img-polaroid"><img class="presta_scr2" src="img/tmrelatedproducts-2.png" alt="" /></p></li>
    <li>Nella casella di ricerca, iniziare a digitare il nome del prodotto desiderato e quindi scegliere tre le opzioni proposte</li>
    <li>Per rimuovere un prodotto dalla lista, fare clic sulla croce di fronte ad un prodotto.</li>
    <li>Dopo aver effettuato tutte le modifiche, fare clic su uno dei pulsanti di salvataggio <b>Save and Stay</b> oppure <b>Save</b>.</li>
</ol>