<h3>Downloading</h3>
<p>To install PrestaShop, you need to download the PrestaShop engine package from the
    official website at <a target="_blank" href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>
<p>Just insert your name and e-mail into the Download box input and click the <strong>'Download'</strong> button. </p>
<p class="alert alert-warning">Please make sure your version of PrestaShop engine matches the template requirements. All
    versions of PrestaShop are available at <a class="spec_link" target="_blank" href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.
</p>