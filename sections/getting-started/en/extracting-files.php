<h3>Extracting Files</h3>
<p>Once the PrestaShop engine package is downloaded, you will need to extract its files and folders from the archive.</p>
<p>This can be done with any archive manager like <strong>WinZip (Windows)</strong>, <strong>StuffitExpander (Mac)</strong>, etc.</p>
<p>Please check the following tutorials on how to extract files from the archives with
    <a target="_blank" href="/help/extract-archived-files-windows-os.html">WinZip (Windows)</a> and <a target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander (Mac)</a>.
</p>
<p class="alert alert-warning">Please note: if your hosting control panel has the option to <strong>'Extract'</strong> files, you can use it to extract the files directly on your server after the .zip file is uploaded.
</p>
