<h3>Editing software</h3>

<p>To feel comfortable working with your PrestaShop template, we recommend that you
    download all applications required. You can see the list of required software on the
    template preview page.
    Requirements may vary depending on the template, we're going to tell you in general
    what's needed:</p>
<ol class="index-list">
    <li>
        <p>First of all, you need the right applications to extract the password protected sources_#########.zip
            archive. You can use WinZip 9+ (Windows) and Stuffit Expander 10+ (Mac).</p>
    </li>
    <li>
        <p>You may also need Adobe Photoshop application. It's used to edit .PSD source files and it's necessary if
            you want to edit template design graphics and images.</p>
    </li>
    <li>
        <p>To edit the template source code files, you need some code editor like Adobe Dreamweaver, Notepad++,
            Sublime Text, etc.</p>
    </li>
    <li>
        <p>To upload files to the hosting server, you may need an FTP Manager like Total Commander, FileZilla,
            CuteFTP, etc.</p>
    </li>
</ol>
