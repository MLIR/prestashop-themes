<h3>Using the local server</h3>
<p>You can run PrestaShop locally on your computer using the local server. To create a local hosting server, please use
    the localhost applications, such as WAMP, AppServ, XAMP, etc. Any of those can be easily installed as any other
    program and used to run PrestaShop.</p>
