    <h3>Скачивание</h3>

    <p>Чтобы установить PrestaShop, скачайте движок PrestaShop с официального сайта <a
            target="_blank" href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
    </p>

    <p>Введите имя и электронную почту в поле скачивания и нажмите кнопку
        <strong>'Скачать'</strong>. </p>

    <p class="alert alert-warning">Пожалуйста, убедитесь, что версия PrestaShop соответствует требованиям шаблона. Все
        версии PrestaShop доступны по ссылке <a
            class="spec_link" target="_blank"
            href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.</p>
