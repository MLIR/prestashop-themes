<h3>Загрузка файлов движка PrestaShop</h3>

<p>В случае, если вы распаковали архив локально (на своем компьютере), вам потребуется
    загрузить файлы на хостинг.</p>

<p>Для этого можно использовать файловый менеджер панели управления хостинга или
    воспользоваться одним из FTP приложений, например: FileZilla, TotalCommander,
    CureFTP и т.д.</p>

<p>Вы также можете посмотреть подробную видео инструкцию <a target="_blank"
                                                            href="/help/how-upload-files-server-2.html">Как
        загрузить файлы на сервер с помощью FTP-клиента</a> и <a target="_blank"
                                                                 href="/help/how-to-upload-files-to-a-server-using-cpanel-webhost-manager-whm.html">Как
        загрузить файлы с помощью cPanel (WebHost менеджер, WHM)</a>.</p>
