<h3>Использование локального сервера</h3>

<p> PrestaShop можно установить на персональном компьютере при помощи локального
    сервера. Для создания локального хостинг сервера необходимо использовать программное
    обеспечение WAMP, AppServ, MAMP и т.п. Любое из них устанавливается как обычная
    программа и поддерживает PrestaShop.</p>

