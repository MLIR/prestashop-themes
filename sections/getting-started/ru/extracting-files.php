<h3>Разархивирование файлов</h3>

<p>По завершению скачивания движка PrestaShop, необходимо распаковать файлы и папки из
    архива.</p>

<p>Это можно сделать любой программой-архиватором, например <strong>WinZip(PC)</strong>,
    <strong>StuffitExpander(MAC)</strong>, и т.д.</p>

<p>Воспользуйтесь следующими туториалами, чтобы распаковать архивы при помощи <a
        target="_blank"
        href="/help/extract-archived-files-windows-os.html">WinZip(PC)</a> и <a
        target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander(MAC)</a>.
</p>

<p class="alert alert-warning">Внимание: если в контрольной панели хостинга есть опция для работы с файлами <strong>'Извлечь'</strong>,
    ее можно использовать для разархивирования файлов непосредственно на сервере после загрузки .zip архивов.</p>