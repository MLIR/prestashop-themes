<h3>Installation de l'engine PrestaShop</h3>

<p>Une fois que vous avez fini avec l'upload des fichiers, vous pouvez passer à
    l'installation de l'engine PrestaShop. Ouvrez votre navigateur web et tapez 'votre
    nom de domaine/chemin vers votre répertoire PrestaShop' dans la barre d'adresse,
    puis rechargez la page. Vous devriez voir l'écran de bienvenue. Veuillez suivre les
    instructions ci-dessous pour installer PrestaShop.</p>

<h4>Assistant d'Installation (Choisissez votre langue)</h4>

<p>Choisissez la langue d'installation</p>

<p class="alert alert-warning">NB: la langue d'installation n'affectera pas la langue de votre boutique. Par défaut,
    PrestaShop est en anglais. Vous pouvez installer des packages de localisation supplémentaires pour changer la
    langue de votre boutique. </p>

<p>Une fois que c'est fait, cliquez sur le bouton <strong>'Next'</strong>.</p>

