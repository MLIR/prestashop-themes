    <img src="img/01-install.png" alt="">
</figure>

<h4>Assistant d'Installation (Accords de Licence)</h4>

<ol class="index-list">
    <li>Veuillez accepter l'accord de licence.</li>

    <li>Une fois que c'est fait, cliquez sur le bouton <strong>'Next'</strong>.</li>
</ol>

<figure class="img-polaroid">
    <img src="img/01-install-1.png" alt="">
</figure>

<h4>Compatibilité du Système </h4>

<p>Vérifiez les paramètres de PHP et les droits sur les répertoires. En cas d'erreur,
    veuillez contacter votre hébergeur pour être assisté.</p>

<p>Si tout est correct, cliquez sur le bouton <strong>'Next'</strong>.</p>

<figure class="img-polaroid">
    <img src="img/02-install.png" alt="">
</figure>

<h4>Configuration de la boutique</h4>


<p>Cette étape nécessite que vous entriez les paramètres de votre boutique comme le nom,
    l'activité, le pays, le fuseau horaire, etc.</p>

<p>Tous ces paramètres peuvent être modifiés plus tard dans votre panneau
    d'administration PrestaShop.</p>

<figure class="img-polaroid">
    <img src="img/04-install.png" alt="">
</figure>

<h4>Configuration du Système</h4>


<p>Si vous disposez déjà d'une base de données MySQL, veuillez entrer les informations
    relatives à la base dans les champs appropriés ci-dessous.</p>

<p>Autrement, vous pouvez obtenir ces informations sur le panneau de contrôle de votre
    hébergeur ou en le contactant directement.</p>

<p>Une fois que c'est fait, cliquez sur le bouton <strong>'Next'</strong>.</p>

<figure class="img-polaroid">
    <img src="img/03-install.png" alt="">
</figure>

<h4>L'installation est terminée</h4>

<p>Voici l'étape finale de l'installation.</p>

<p class="alert alert-danger">Pour des raisons de sécurité, vous devez supprimer le répertoire
    <strong>/install/</strong> de votre serveur d'hébergement.</p>

<figure class="img-polaroid">
    <img src="img/05-install.png" alt="">
</figure>

<p class="alert alert-info">À partir de la version 1.5.4, PrestaShop s'occupe du renommage du dossier
    d'administration pour vous: le nom devient le mot "admin" suivi de 4 chiffres aléatoires, par exemple
    "admin8814." Le renommage est fait dès que vous atteignez le dossier "admin" pour la première fois: PrestaShop
    le renomme puis accède correctement au dossier renommé.<br/>
    <br/>
    Connectez-vous au back-office de PrestaShop en allant sur votre dossier "admin" nouvellement renommé et
    commencez à remplir votre catalogue avec vos données.</p>

<h6>Si vous rencontrez des difficultés, veuillez consulter le <a target="_blank"
                                                                href="http://doc.prestashop.com/display/PS16/Getting+Started">manuel
        officiel d'installation de PrestaShop</a>.</h6>
