<h3>Utilisation d'un serveur local</h3>

<p>Vous pouvez lancer PrestaShop localement sur votre ordinateur à l'aide d'un serveur
    local. Pour créer un serveur d'hébergement local, veuillez utiliser les applications
    localhost telles que WAMP, AppServ, MAMP, etc. Toutes ces applications peuvent être
    facilement installées comme tout autre programme et utilisées pour lancer
    PrestaShop.</p>

