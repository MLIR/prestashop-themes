<h3>Téléchargement</h3>

<p>Pour installer PrestaShop, vous devrez télécharger le package de l'engine PrestaShop
    depuis le site officiel sur <a target="_blank"
                                   href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>

<p>Entrez juste votre nom et votre e-mail et cliquez sur le bouton
    <strong>'Download'</strong>. </p>

<p class="alert alert-warning">Assurez-vous que votre version de l'engine PrestaShop est conforme aux besoins du
    template. Toutes les versions de PrestaShop sont disponibles sur <a
        class="spec_link" target="_blank"
        href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.</p>
