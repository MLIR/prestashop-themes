<h3>Upload des fichiers de l'engine PrestaShop</h3>

<p>Si vous avez extrait les fichiers localement sur votre ordinateur, vous devrez les
    uploader sur votre serveur d'hébergement.</p>

<p>Vous pouvez le faire à l'aide du Gestionnaire de Fichiers du panneau de contrôle de
    votre hébergeur ou d'applications tierces de Gestion de FTP telles que FileZilla,
    TotalCommander, CuteFTP, etc.</p>

<p>Vous pouvez consulter le tutoriel vidéo détaillé sur <a target="_blank"
                                                           href="/help/how-upload-files-server-2.html">comment
        uploader des fichiers sur un serveur à l'aide d'un gestionnaire FTP</a> et <a
        target="_blank"
        href="/help/how-to-upload-files-to-a-server-using-cpanel-webhost-manager-whm.html">comment
        uploader des fichiers sur un serveur à l'aide de cPanel (WebHost Manager, WHM)</a>.
</p>