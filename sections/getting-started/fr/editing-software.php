<h3>Logiciel d'édition</h3>

<p>Pour être à l'aise en travaillant avec votre template PrestaShop, nous vous
    recommandons de télécharger toutes les applications requises. Vous pouvez voir la
    liste des logiciels requis sur la page de prévisualisation du template.
    Les besoins peuvent varier selon le template, voici ce qui est généralement
    requis:</p>
<ol class="index-list">
    <li>
        <p>Tout d'abord, il vous faut les bonnes applications pour extraire l'archive sources_#########.zip
            protégée par un mot de passe. Vous pouvez utiliser WinZip 9 et + (Windows) et Stuffit Expander 10 et +
            (Mac).</p>
    </li>
    <li>
        <p>Vous pouvez aussi avoir besoin de l'application Adobe Photoshop. Elle sert à modifier les fichiers
            source .PSD et elle est nécessaire si vous voulez modifier les graphiques et images du design du
            template.</p>
    </li>
    <li>
        <p>Pour modifier les fichiers du code source du template, il vous faut un éditeur de code comme Adobe
            Dreamweaver, Notepad++, Sublime Text, etc.</p>
    </li>
    <li>
        <p>Pour uploader les fichiers sur le serveur d'hébergement, vous pouvez avoir besoin du Gestionnaire FTP
            comme Total Commander, FileZilla, CuteFTP, etc.</p>
    </li>
</ol>
