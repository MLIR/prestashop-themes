<h3>Extraction des Fichiers</h3>

<p>Une fois le package de l'engine PrestaShop téléchargé, vous devrez extraire ses
    fichiers et dossiers de l'archive.</p>

<p>Vous pouvez le faire avec n'importe quel gestionnaire d'archive comme <strong>WinZip(PC)</strong>,
    <strong>StuffitExpander(MAC)</strong>, etc.</p>

<p>Veuillez consulter les tutoriels suivants sur comment extraire les fichiers des
    archives avec <a target="_blank"
                     href="/help/extract-archived-files-windows-os.html">WinZip(PC)</a>
    et <a target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander(MAC)</a>.
</p>

<p class="alert alert-warning">NB: Si le panneau de contrôle de votre hébergeur propose l'option
    d'<strong>'Extraire'</strong> les fichiers, vous pouvez l'utiliser pour extraire les fichiers directement sur
    votre serveur une fois le fichier .zip uploadé. </p>
