<h3>Installazione motore PrestaShop</h3>

<p>Una volta terminato il caricamento dei file potrai avviare l'installazione del motore
    PrestaShop. Apri il browser e digita il nome di dominio/percorso directory
    PrestaShop nella barra degli indirizzi e poi aggiorna la pagina. Adesso dovresti
    vedere la schermata di benvenuto. Per installare PrestaShop, segui queste
    istruzioni:</p>
<h4>Assistente Installazione (Seleziona la lingua)</h4>


<p>Qui puoi selezionare la lingua utilizzata per l'installazione</p>

<p class="alert alert-warning">Nota: la lingua scelta per l'installazione non determina la lingua per il negozio vero e
    proprio. La lingua predefinita di PrestaShop è l'inglese. Per cambiare la lingua si possono installare pacchetti
    di localizzazione aggiuntivi. </p>

<p>Una volta fatto, clicca su <strong>'Next'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/01-install.png" alt="">
</figure>
<h4>Assistente Installazione (Accordo di Licenza)</h4>


<ol class="index-list">
    <li>Leggi l'accordo di licenza.</li>

    <li>Una volta fatto, clicca su <strong>'Next'</strong>.</li>
</ol>

<figure class="img-polaroid">
    <img src="img/01-install-1.png" alt="">
</figure>

<h4>Compatibilità Sistema</h4>


<p>Verifica le impostazioni PHP e i permessi cartella. In caso di errori, contatta il
    tuo provider per chiedere assistenza.</p>


<p>Se tutto sembra a posto, clicca su <strong>'Next'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/02-install.png" alt="">
</figure>
<h4>Configurazione Negozio</h4>


<p>In questa fase dovrai inserire impostazioni di base del negozio, come nome, attività,
    paese, fuso orario, ecc.</p>

<p>Ognuna di queste impostazioni potrà anche essere modificata in seguito dal pannello
    di amministrazione di PrestaShop.</p>

<figure class="img-polaroid">
    <img src="img/04-install.png" alt="">
</figure>
<h4>Configurazione Sistema</h4>


<p>Se hai già un database MySQL, inserisci i dettagli del database nei rispettivi
    campi.</p>

<p>Altrimenti puoi trovare i dettagli del database sul pannello di controllo hosting
    oppure contattando il tuo provider.</p>


<p>Una volta finito, clicca su <strong>'Next'</strong>.</p>

<figure class="img-polaroid">
    <img src="img/03-install.png" alt="">
</figure>
<h4>Installazione completata</h4>

<p>Questa è la fase finale dell'installazione.</p>

<p class="alert alert-danger">Per ragioni di sicurezza dovrai eliminare la directory <strong>/install/</strong> sul
    tuo server.</p>

<figure class="img-polaroid">
    <img src="img/05-install.png" alt="">
</figure>

<p class="alert alert-info">A partire dalle versioni 1.5.4, PrestaShop rinominerà automaticamente la cartella di
    amministrazione in "admin" seguito da 4 cifre a caso, ad es. "admin8814." L'operazione di riassegnazione del
    nome avviene appena accedi alla cartella "admin" per la prima volta: PrestaShop la rinomina e poi ti fa accedere
    correttamente alla cartella rinominata.<br/>
    <br/>
    Accedi al back office di PrestaShop andando sulla cartella "admin" appena rinominata e inizia a inserire i tuoi
    dati nel catalogo.</p>

<h6>Se incontri difficoltà puoi consultare il <a target="_blank"
                                                 href="http://doc.prestashop.com/display/PS16/Getting+Started">manuale
        ufficiale di installazione PrestaShop</a>.</h6>
