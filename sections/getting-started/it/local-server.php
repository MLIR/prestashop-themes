<h3>Usare il server locale</h3>

<p>Puoi eseguire PrestaShop localmente sul tuo computer usando il server locale. Per
    creare un server locale, utilizza applicazioni localhost, come WAMP, AppServ, MAMP,
    ecc. Sono tutti programmi normalmente installabili che potrai utilizzare per
    eseguire PrestaShop.</p>

