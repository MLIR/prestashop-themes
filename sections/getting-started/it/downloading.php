<h3>Scaricare</h3>

<p>Per installare PrestaShop devi scaricare il pacchetto del motore PrestaShop dal sito
    ufficiale all'indirizzo <a target="_blank"
                               href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>

<p>Basta inserire il tuo nome e indirizzo e-mail nella casella Download e poi cliccare
    su <strong>'Download'</strong>. </p>

<p class="alert alert-warning">Verifica che la tua versione del motore PrestaShop corrisponda ai requisiti del
    template. Tutte le versioni di PrestaShop sono disponibili su <a
        class="spec_link" target="_blank"
        href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.</p>
