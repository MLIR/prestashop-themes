<h3>Software per l'Editing</h3>

<p>Per poter lavorare sul template PrestaShop con la massima comodità, ti consigliamo di
    scaricare tutte le applicazioni necessarie. La lista del software necessario è
    consultabile sulla pagina di anteprima del template.
    I requisiti possono variare a seconda del template, ma in generale ciò che servirà
    dovrebbe essere quanto segue:</p>
<ol class="index-list">
    <li>
        <p>Prima di tutto devi avere un'applicazione per estrarre l'archivio sources_#########.zip, protetto da
            password. Si può usare WinZip 9+ (per Windows) o Stuffit Expander 10+ (per Mac).</p>
    </li>
    <li>
        <p>Potresti anche aver bisogno di Adobe Photoshop, da utlizzare per modificare i file sorgente .PSD o per
            modificare immagini e design del template, se lo desideri.</p>
    </li>
    <li>
        <p>Per modificare i file sorgente del template avrai bisogno di un editor di codice, come Adobe
            Dreamweaver, Notepad++, Sublime Text, ecc.</p>
    </li>
    <li>
        <p>Per caricare i file sul server potresti aver bisogno di un gestore FTP Manager, come Total Commander,
            FileZilla, CuteFTP, ecc.</p>
    </li>
</ol>
