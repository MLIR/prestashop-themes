<h3>Carga de los archivos del motor de PrestaShop</h3>

<p>Si ha extra&iacute;do los archivos de forma local en el equipo, tendr&aacute; que
    subirlos a su servidor de alojamiento.</p>

<p>Esto se puede hacer mediante el panel de control del hosting o utilzando un Gestor de
    FTP de terceros como FileZilla, TotalCommander, CuteFTP, etc.</p>

<p>Puede echar un vistazo al video tutorial detallado sobre <a target="_blank"
                                                               href="/help/how-upload-files-server-2.html">c&oacute;mo
        subir archivos al servidor mediante un gestor de FTP</a> and <a target="_blank"
                                                                        href="/help/how-to-upload-files-to-a-server-using-cpanel-webhost-manager-whm.html">c&oacute;mo
        subir archivos a un servidor utilizando cPanel (WebHost Manager, WHM)</a>.</p>
