<h3>Usando el servidor local</h3>

<p>Puede ejecutar PrestaShop localmente en su ordenador con el servidor local. Para
    crear un servidor de alojamiento local, por favor, utilice las aplicaciones de
    localhost, como WAMP, AppServ, XAMP, etc. Cualquiera de ellas se puede instalar f&aacute;cilmente
    y se utilizan para ejecutar PrestaShop.</p>

