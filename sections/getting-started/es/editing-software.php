<h3>Software de edici&oacute;n</h3>

<p>Para sentirse c&oacute;modo trabajando con su plantilla de PrestaShop, le
    recomendamos que descargue todas las aplicaciones necesarias. Puede ver la lista de
    software necesario en la p&aacute;gina de vista previa de plantilla.
    Los requisitos pueden variar dependiendo de la plantilla, por norma general usted
    necesita:</p>
<ol class="index-list">
    <li>
        <p>En primer lugar, usted necesita las aplicaciones adecuadas para extraer el archivo sources_#########.zip
            que est&aacute; protegido por contrase&ntilde;a. Puede utilizar WinZip 9+ (Windows) y Stuffit Expander
            10+ (Mac).</p>
    </li>
    <li>
        <p>Usted tambi&eacute;n podr&iacute;a necesitar la aplicaci&oacute;n Adobe Photoshop. Se utiliza para
            editar los archivos de c&oacute;digo fuente .PSD, es necesaria si desea editar los gr&aacute;ficos e im&aacute;genes
            de la plantilla.</p>
    </li>
    <li>
        <p>Para editar los archivos de c&oacute;digo fuente de la plantilla, necesitas alg&uacute;n editor de c&oacute;digo
            como Adobe Dreamweaver, Notepad++, Sublime Text, etc.</p>
    </li>
    <li>
        <p>Para cargar archivos en el servidor de alojamiento, es posible que necesite un Gestor FTP como Total
            Commander, FileZilla, CuteFTP, etc.</p>
    </li>
</ol>