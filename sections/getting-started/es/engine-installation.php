<h4>Configuraci&oacute;n de la tienda</h4>

<p>Este paso requiere que introduzca los ajustes de la tienda como el nombre de Tienda,
    la actividad, el pa&iacute;s, la zona horaria, etc.</p>

<p>Todos estos ajustes se pueden cambiar m&aacute;s adelante en el panel de administraci&oacute;n
    de PrestaShop.</p>

