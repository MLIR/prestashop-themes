<h3>Instalaci&oacute;n del motor de PrestaShop</h3>

<p>Cuando haya terminado de cargar los archivos, puede iniciar la instalaci&oacute;n del
    motor de PrestaShop. Abra su navegador Web y escriba el 'nombre del dominio/ruta a
    la carpeta de PrestaShop' en la barra de direcciones, a continuaci&oacute;n, vuelva
    a cargar la p&aacute;gina. Deber&iacute;a ver la pantalla de bienvenida. Por favor,
    siga las siguientes instrucciones para instalar PrestaShop.</p>
<h4>Asistente de Instalaci&oacute;n (Elija el idioma)</h4>

<p>Por favor seleccione el idioma de instalaci&oacute;n</p>

<p class="alert alert-warning">Tenga en cuenta: La opci&oacute;n de instalaci&oacute;n de idioma no cambiar&aacute;
    el idioma de su tienda. Por defecto PrestaShop se ofrece en Ingl&eacute;s. Puede instalar los paquetes de
    traducci&oacute;n adicionales para cambiar el idioma de la tienda. </p>

<p>Cuando haya terminado, haga clic en el bot&oacute;n <strong>'Siguiente'</strong>.</p>

