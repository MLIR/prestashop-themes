<h4>Configuraci&oacute;n del Sistema</h4>

<p>Si ya tiene una base de datos de MySQL, por favor indique los detalles de de la base
    de datos en los campos correspondientes.</p>

<p>Si no los sabe usted puede conseguir los detalles de la base de datos desde el panel
    de control de hosting o poni&eacute;ndose en contacto con su proveedor de
    hosting.</p>

<p>Cuando haya terminado, haga clic en el bot&oacute;n <strong>'Siguiente'</strong>.</p>

