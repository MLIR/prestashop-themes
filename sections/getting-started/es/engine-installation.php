<h4>La instalaci&oacute;n est&aacute; completa</h4>

<p>Este es el paso final de la instalaci&oacute;n.</p>

<p class="alert alert-danger">Por razones de seguridad es necesario eliminar la carpeta <strong>/install/</strong>
    del servidor de alojamiento.</p>

<figure class="img-polaroid">
    <img src="img/05-install.png" alt="">
</figure>

<p class="alert alert-info">Desde la versi&oacute;n 1.5.4, PrestaShop se encarga de cambiar el nombre de la carpeta
    de administraci&oacute;n por ti: usa la palabra "admin" y a&ntilde;ade 4 d&iacute;gitos al azar, es decir, por
    ejemplo "admin8814." El cambio de nombre se produce cuando llega a la carpeta "admin" por primera vez:
    PrestaShop cambiar&aacute;
    el nombre y luego acceder&aacute; a la carpeta renombrada.<br/>
    <br/>
    Inicie sesi&oacute;n en el panel de administraci&oacute;n de PrestaShop para ir a la carpeta "admin" reci&eacute;n
    renombrada y empiece a llenar el cat&aacute;logo con sus datos.</p>

