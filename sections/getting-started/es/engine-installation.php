<h4>Compatibilidad del Sistema</h4>

<p>Compruebe la configuraci&oacute;n de PHP y los permisos del directorio. En caso de
    cualquier error, por favor p&oacute;ngase en contacto con su proveedor de hosting
    para obtener m&aacute;s ayuda.</p>

<p>Si todo es correcto, haga clic en el bot&oacute;n <strong>'Siguiente'</strong>.</p>

