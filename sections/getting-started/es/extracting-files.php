<h3>Extracci&oacute;n de archivos</h3>

<p>Una vez que el paquete del motor PrestaShop es descargado, tendr&aacute; que extraer
    sus archivos y carpetas.</p>

<p>Esto se puede hacer con cualquier gestor de archivos como <strong>WinZip(PC)</strong>,
    <strong>StuffitExpander(MAC)</strong>, etc.</p>

<p>Por favor, consulte los siguientes tutoriales sobre c&oacute;mo extraer el contenido
    de los archivos comprimidos con <a target="_blank"
                                       href="/help/extract-archived-files-windows-os.html">WinZip(PC)</a>
    y <a target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander(MAC)</a>.
</p>

<p class="alert alert-warning">Tenga en cuenta: si su panel de control de hosting tiene la opci&oacute;n de <strong>'Extraer'</strong>
    archivos, usted puede utilizarla para extraer los archivos directamente en el servidor despu&eacute;s de que se
    ha cargado el archivo .zip.</p>
