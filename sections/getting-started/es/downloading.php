<h3>Descargando</h3>

<p>Para instalar PrestaShop necesita descargar el paquete del motor de PrestaShop desde
    el sitio web oficial en <a target="_blank"
                               href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>

<p>S&oacute;lo tiene que insertar su nombre y correo electr&oacute;nico en el cuadro de
    descarga y hacer clic en el bot&oacute;n <strong>'Descargar'</strong>. </p>

<p class="alert alert-warning">Por favor, aseg&uacute;rese de que su versi&oacute;n del motor de PrestaShop coincide con
    los requisitos de la plantilla. Todas las versiones de PrestaShop est&aacute;n disponibles en <a
        class="spec_link" target="_blank"
        href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.</p>