<h3>Instalando o PrestaShop</h3>

<p>Quando terminar com o envio dos arquivos, você pode começar a instalação do
    PrestaShop. Abra seu navegador e digite 'nome do domínio/caminho para o diretório do
    PrestaShop no servidor' na sua barra de endereços, depois disso recarregue a página
    atual. Você deve ver a tela de boas vindas. Siga as instruções abaixo para instalar
    o PrestaShop</p>
<h5>Assistente de instalação (Escolha seu idioma)</h5>


<p>Escolha o idioma de instalação</p>

<p class="alert alert-warning">Se liga: o idioma da instalação não vai afetar o idioma da sua loja. Por padrão, o
    PrestaShop é configurado em inglês. Você pode instalar pacotes de localização adicionais para mudar o idioma da
    loja. </p>

<p>Quando terminar, clique em <strong>'Próximo (Next)'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/01-install.png" alt="">
</figure>
<h5>Assistente de Instalação (Acordo de Licenças)</h5>


<ol class="index-list">
    <li>Leia o acordo de licenças.</li>
    <li>Quando terminar, clique em <strong>'Próximo (Next)'</strong>.</li>
</ol>
<figure class="img-polaroid">
    <img src="img/01-install-1.png" alt="">
</figure>
<h5>System Compatibility</h5>


<p>Check the PHP settings and directory permissions. In case of any errors, please
    contact your hosting provider for further assistance.</p>

<p>If everything is correct, clique em <strong>'Próximo (Next)'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/02-install.png" alt="">
</figure>

<h5>Configuração da Loja</h5>


<p>Nesse passo você precisa preencher configurações da loja como nome, atividade, país,
    fuso horário, etc.</p>

<p>Todas essas configurações podem ser alteradas mais tarde no painel administrativo do
    PrestaShop.</p>

<figure class="img-polaroid">
    <img src="img/04-install.png" alt="">
</figure>
<h5>Configurações do Sistema</h5>


<p>Se você já tiver um banco de dados MySQL, preencha os detalhes do banco nos campos
    abaixo.</p>

<p>Do contrário, você pode pegar os detalhes do seu banco de dados do seu painel
    administrativo no seu servidor de hospedagem, ou entrando em contato com o provedor
    de hospedagem.</p>

<p>Quando terminar, clique em <strong>'Próximo (Next)'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/03-install.png" alt="">
</figure>

<h5>Instalação terminada</h5>

<p>Esse é o último passo da instalação</p>


<p class="alert alert-danger">Por razões de segurança, você precisa apagar o diretório <strong>/install/</strong>
    no seu servidor de hospedagem.</p>

<figure class="img-polaroid">
    <img src="img/05-install.png" alt="">
</figure>
<p class="alert alert-info">À partir da versão 1.5.4, o PrestaShop cuida de renomear a pasta administrativa para
    você: se torna a palavra "admin" acrescida de 4 números aleatórios, por exemplo "admin8814". Essa mudança no
    nome acontece quando você acessa a pasta admin pela primeira vez: o PrestaShop renomeia a pasta e redireciona
    seu navegador para a pasta correta.<br/>
    <br/>
    Faça login no escritório do PrestaShop indo até a sua pasta "admin" recém renomeada e comece a preencher seu
    catálogo com dados dos seus produtos.</p>

