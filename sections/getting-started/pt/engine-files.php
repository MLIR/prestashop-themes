<h3>Enviando os arquivos base do PrestaShop</h3>

<p>Se você extraiu os arquivos localmente no seu computador, você vai precisar enviá-los
    para o servidor de hospedagem.</p>

<p>Você pode fazer isso usando o gerenciador de arquivos do seu painel de hospedagem ou
    usando um Gerenciador de FTP como o FileZilla, TotalCommander, CuteFTP, etc.</p>

<p>Você pode dar uma olhada num tutorial detalhado em <a target="_blank"
                                                         href="/help/how-upload-files-server-2.html">how
        to upload files to server using FTP manager</a> e <a target="_blank"
                                                             href="/help/how-to-upload-files-to-a-server-using-cpanel-webhost-manager-whm.html">how
        to upload files to a server using cPanel (WebHost Manager, WHM)</a>.</p>
