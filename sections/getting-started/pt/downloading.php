<h3>Baixando</h3>

<p>Para instalar o PrestaShop, você precisa baixar o pacote do site oficial em <a
        target="_blank" href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>

<p>Preencha seu nome e email nos campos da caixa de download e clique no botão <strong>'Download'</strong>.
</p>

<p class="alert alert-warning">Certifique-se de que sua versão do PrestaShop seja compatível com o seu tema. Todas as
    versões do PrestaShop estão disponíveis em <a
        class="spec_link" target="_blank"
        href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.</p>