<h3>Usando um servidor local</h3>

<p>você pode rodar o PrestaShop localmente no seu computador usando um servidor local.
    Para criar um servidor local, use os aplicativos próprios para isso, como <strong>WAMP</strong>,
    <strong>AppServ</strong>, <strong>MAMP</strong> etc. Qualquer um desses pode ser
    facilmente instalado como qualquer outro programa e usá-lo para rodar o PrestaShop.
</p>

