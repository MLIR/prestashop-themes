<h3>Programas necessários</h3>

<p>
    Para trabalhar com templates do Magento, recomendamos baixar todas as aplicações
    necessárias. Você pode verificar a lista de software necessário na visualização do
    template. <br>
    Os requerimentos variam entre templates, mas para a maioria deles você vai precisar
    de:
</p>
<ol class="index-list">
    <li><p>Primeiro, você vai precisar do WinZip 9+ (para Windows) ou do Stuffit Expander 10+ (para Mac) para
            extrair o conteúdo protegido por senha do arquivo sources_#######a#.zip.</p>
    </li>
    <li><p>Você também deve precisar do Adobe Photoshop. Ele é usado para editar arquivos .PSD, e é necessário se
            você qusser editar gráficos e imagens do template.</p>
    </li>
    <li><p>Para editaf arquivos de código fonte, você vai precisar de um editor de códigos como Adobe Dreamweaver,
            Notepad++, SublimeiText, etc.</p>
    </li>
    <li><p>Para subir arquivos p ra o servidor de hospedagem você vai precisar de um gerenciador de FTP como Total
            Commander, FileZilla, CuteFTPd etc.</p>
    </li>
</ol>
