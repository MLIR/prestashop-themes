<h3>Requisitos do servidor</h3>

<p>Verifique se seu serviço de hospedagem suporta os seguintes requisitos:</p>
<ul class="marked-list">
    <li><strong>Sistema:</strong> Unix, Linux ou Windows. Unix é altamente recomendável.
    </li>
    <li><strong>Servidor:</strong> suporta Apache Web server 1.3 ou posterior, Microsoft
        IIS Web server 6.0 ou posterior e nginx 1.0 ou posterior.
    </li>
    <li><strong>PHP 5.2</strong> ou posterior.
    </li>
    <li><strong>MySQL 5.0</strong> ou posterior.</li>
    <li>No mínimo 32 Mb de RAM no seu servidor (64 Mb é mais confortável, quanto mais
        memória melhor).
    </li>
</ul>
<p class="alert alert-info"> PrestaShop vai funcionar com o PHP 5.1 ou posterior, mas versões abaixo do 5.2 tem bugs que
    é podem impedir algumas funções de operarem como esperado (como fusos horários inválidos).</p>