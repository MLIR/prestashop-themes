<h3>Extraindo os arquivos</h3>

<p>Após baixar o pacote de instalação, você precisa extrair os documentos e pastas do
    arquivo.</p>

<p>Isso pode ser feito com qualquer gerenciador de arquivos, como
    <strong>WinZip(PC)</strong>, <strong>StuffitExpander(MAC)</strong>, etc.</p>

<p>Dê uma olhada nos tutoriais abaixo se encontrar dificuldades em extrair esses
    arquivos com <a target="_blank" href="/help/extract-archived-files-windows-os.html">WinZip(PC)</a>
    e <a target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander(MAC)</a>.
</p>

<p class="alert alert-warning">Aliás: se o seu painel de controle da hospedagem tiver a opção de
    <strong>'Extrair'</strong> arquivos, você pode usá-la para extrair os arquivos diretamente no seu servidor onde
    o está o arquivo .zip que você enviou.</p>
