<h3>Sunucu gereksinimleri</h3>

<p>Sunucunuzun PrestaShop gereksinimlerini karşıladığına emin olunuz:</p>
<ul class="marked-list">
    <li><strong>Sistem:</strong> Unix, Linux veya Windows. Unix tavsiye edilmektedir.
    </li>
    <li><strong>Web sunucu:</strong> Apache Web sunucusu 1.3 veya üstü, Microsoft'un IIS
        Web sunucusu 6.0 veya üstü ve nginx 1.0 veya üstü.
    </li>
    <li><strong>PHP 5.2</strong> veya üstü.</li>
    <li><strong>MySQL 5.0</strong> veya üstü.</li>
    <li>Sunucunuzda en az 32 Mb RAM olmalı (64 Mb tavsiye edilir, ne kadar çok olursa o
        kadar iyi).
    </li>
</ul>
<p class="alert alert-info"> PrestaShop PHP 5.1 veya üstünde çalışır fakat 5.2'den düşük versiyonlarda, geçersiz zaman
    dilimi gibi bazı işlevsel hatalar bulunabilir.</p>