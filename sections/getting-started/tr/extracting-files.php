<h3>Dosyaları çıkarmak</h3>

<p>PrestaShop'u indirdikten sonra dosya ve klasörleri arşivden çıkartmanız gerekir.</p>

<p>Bu işlem <strong>WinZip(PC)</strong>, <strong>StuffitExpander(MAC)</strong> vs. gibi
    bir programla yapılabilir.</p>

<p>Dilerseniz dosyaların nasıl çıkarılacağıyla ilgili <a target="_blank"
                                                         href="/help/extract-archived-files-windows-os.html">WinZip(PC)</a>
    ve <a target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander(MAC)</a>
    adreslerinden bilgi alabilirsiniz.</p>

<p class="alert alert-warning">Dikkat: eğer hosting panelinizde dosyaları çıkarmak için <strong>'Extract'</strong>
    seçeneği bulunuyorsa buradan, sunucunuza yüklediğiniz .zip dosyasını seçerek dosya ve klasörleri direkt olarak
    sunucunuza da çıkartabilirsiniz.</p>

