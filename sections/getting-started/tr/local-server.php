<h3>Lokal sunucu kullanımı</h3>

<p>PrestaShop'u kendi bilgisayarınızdaki lokal sunucu üzerinden de kullanabilirsiniz.
    Lokal bir sunucu oluşturmak için WAMP, AppServ, MAMP vs. gibi bir localhost
    uygulaması kullanabilirsiniz. Bu uygulamalar, diğer programlar gibi kolayca
    kurulabilir ve PrestaShop'a ev sahipliği yapabilirler.</p>

