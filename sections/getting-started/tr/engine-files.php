<h3>PrestaShop dosyalarını yüklemek</h3>

<p>Eğer dosyaları bilgisayarınıza çıkarttıysanız, onları sunucunuza yüklemeniz
    gerekir.</p>

<p>Bu işlem sunucunuzun kontrol panelindeki File Manager (Dosya Yöneticisi) aracılığıyla
    veya FileZilla, TotalCommander, CuteFTP vs. gibi bir FTP programıyla
    yapılabilir.</p>

<p>Dilerseniz <a target="_blank" href="/help/how-upload-files-server-2.html">FTP
        programıyla dosya yüklemek</a> ve <a target="_blank"
                                             href="/help/how-to-upload-files-to-a-server-using-cpanel-webhost-manager-whm.html">cPanel'den
        (WebHost Manager, WHM) dosya yüklemek</a> adreslerinden detaylı bilgi alabilirsiniz.
</p>