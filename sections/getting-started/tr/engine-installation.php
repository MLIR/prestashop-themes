<h3>PrestaShop kurulumu</h3>

<p>Tüm dosyaları sunucuya yükledikten sonra artık PrestaShop'u kurmaya hazırsınız. Web
    tarayıcınızın adres barına 'domain adı/PrestaShop klasörü' yolunu yazın. Karşınıza
    selamlama ekranı çıkacak. Buradan PrestaShop kurulum adımlarını tamamlayın.</p>
<h4>Kurulum Asistanı (Dilinizi seçin)</h4>


<p>Kurulum için dil seçin</p>

<p class="alert alert-warning">Dikkat: kurulum dili, mağazanızın dilini etkilemez. PrestaShop, İngilizce olarak
    sunulmaktadır. Mağazanızda kullanılmasını istediğiniz dili, daha sonra dil paketleri yükleyerek
    değiştirebilirsiniz.</p>
<p>Hazır olduğunuzda <strong>'Next'</strong> düğmesine basın.</p>
