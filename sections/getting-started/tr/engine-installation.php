<h4>Kurulumun tamamlanması</h4>

<p>Bu, kurulumun son aşamasıdır.</p>

<p class="alert alert-danger">Güvenlik açısından, sunucunuzdaki <strong>/install/</strong> dizinini silmeniz
    gerekir.</p>

<figure class="img-polaroid">
    <img src="img/05-install.png" alt="">
</figure>

<p class="alert alert-info">1.5.4 versiyonundan itibaren PrestaShop, yönetici klasörünü sizin için yeniden
    adlandırmaktadır: yeni isim "admin" kelimesine ek olarak dört rakam eklemektedir örn. "admin8814." Bu
    değişiklik, sizin "admin" klasörüne ilk kez erişim sağlamanızdan sonra gerçekleşir. PrestaShop ismi değiştirir
    ve yeni dizine erişim sağlar.<br/>
    <br/>
    PrestaShop'un yönetim merkezine, yeniden adlandırılmış olan "admin" klasörüne girerek ulaşın ve kataloğunuza
    verilerinizi girmeye başlayın.</p>

