<h3>Yazılımı düzenlemek</h3>

<p>PrestaShop şablonunuzla daha rahat çalışabilmek için, gerekli olan tüm uygulamaları
    indirmenizi tavsiye ederiz. Bu uygulamaların listesini şablon ön izleme sayfasından
    görebilirsiniz.
    Gereksinimler şablona göre değişiklik gösterebilir, genel olarak ihtiyaç duyulanlar
    şunlardır:</p>
<ol class="index-list">
    <li><p>Öncelikle, şifreyle korunan sources_#########.zip arşivini açmak için doğru
            uygulamaya ihtiyacınız var. Windows için WinZip 9+ Mac için de Stuffit Expander
            10+ kullanabilirsiniz.
        </p>
    </li>
    <li><p>Ayrıca .PSD kaynak dosyalarını ve dilerseniz şablonunuzdaki grafik ve görselleri
            düzenlemek için Adobe Photoshop uygulamasına da ihtiyacınız olabilir.
        </p>
    </li>
    <li><p>Şablonun kaynak dosyalarını düzenlemek için, Adobe Dreamweaver, Notepad++,
            Sublime Text vs. gibi bir kod editörüne ihtiyacınız var.
        </p>
    </li>
    <li><p>Dosyaları hosting sunucusuna aktarmak için ise Total Commander, FileZilla,
            CuteFTP vs. gibi bir FTP programına ihtiyacınız var.
        </p>
    </li>
</ol>
