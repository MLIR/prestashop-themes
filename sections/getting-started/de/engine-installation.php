<h3>Installation der PrestaShop Engine</h3>

<p>Nachdem Sie alle Dateien hochgeladen haben, können Sie anfangen die PrestaShop Engine
    zu installieren. Öffnen Sie den Webbrowser und geben Sie den Domainnamen oder den
    Pfad zum Verzeichnis PrestaShop in der Adresszeile ein, dann aktualisieren Sie die
    Seite. Es sollte das Begrüßungsfenster angezeigt werden. Folgen Sie den folgenden
    Anleitungen, um PrestaShop zu installieren.</p>

<h4>Installer (Wählen Sie die Sprache)</h4>

<p>Wählen Sie die Installationssprache</p>

<p class="alert alert-warning">Achtung: die Installationssprache wirkt auf Spachen des Online-Shops nicht.
    Standardmäßig wird in PrestaShop Englisch installiert. Sie können zusätzliche Lokalisierungspakete installieren,
    um die Sprache des Online-Shops nach der Installation PrestaShop zu ändern </p>

<p>Nachdem Sie die Sprache ausgewählt haben, drücken Sie auf den Button
    <strong>'Weiter'</strong></p>

<figure class="img-polaroid">
    <img src="img/01-install.png" alt="">
</figure>

<h4>Installer (Lizenzvereinbarung)</h4>

<ol class="index-list">
    <li>Machen Sie sich mit der Lizenzvereinbarung bekannt.</li>

    <li> Dann drücken Sie auf den Button <strong>'Weiter'</strong>.</li>
</ol>

<figure class="img-polaroid">
    <img src="img/01-install-1.png" alt="">
</figure>

<h4>Systemkompatibilität </h4>

<p>Überprüfen Sie PHP Einstellungen und Verzeichnisberechtigungen. Falls es Fehler gibt,
    setzen Sie sich mit dem Hosting-Anbiert in Verbindung, damit er Ihnen helfen
    kann. </p>

<p>Wenn es keine Fehler gibt, drücken Sie auf den Button <strong>'Weiter'</strong>.</p>

<figure class="img-polaroid">
    <img src="img/02-install.png" alt="">
</figure>

<h4>Einstellung des Online-Shops</h4>


<p>Jetzt müssen die Einstellungen des Online-Shops wie Name, Tätigkeit, Land, Zeitzone
    usw. festgelegt werden.</p>

<p>Alle diesen Einstellungen können Sie später im Control Panel PrestaShop ändern.</p>

<figure class="img-polaroid">
    <img src="img/04-install.png" alt="">
</figure>

<h4>Systemeinstellungen</h4>

<p>Wenn Sie die Datenbank MySQL schon haben, geben Sie die Details in entsprechende
    Felder ein.</p>

<p>Ansonsten können Sie die Details der Datenbank im Control Panel des Hosting finden
    oder den Kontakt mit den Hosting-Anbieter aufnehmen.</p>

<figure class="img-polaroid">
    <img src="img/03-install.png" alt="">
</figure>

<p>Danach drücken Sie auf den Button <strong>'Weiter'</strong>.</p>

<h4>Installation ist abgeschloßen</h4>

<p>Der letzte Installationsschritt.</p>

<p class="alert alert-danger">Aus Sicherheitsgründen muss der Ordner <strong>/install/</strong> auf dem Server
    entfernt werden.</p>

<figure class="img-polaroid">
    <img src="img/05-install.png" alt="">
</figure>

<p class="alert alert-info">Ab der Version 1.5.4 benennt PrestaShop den Administrationsordner automatisch um: der
    Name sieht wie "admin" mit 4 Zufallszahlen, zum Beispiel, "admin8814" aus. Die Umbenennung wird durchgeführt,
    sobald Sie zum ersten Mal den Ordner "admin" im Browser öffnen: PrestaShop benennt ihn um und öffnet dann schon
    umbenannten Ordner.<br/>
    <br/>
    Loggen Sie sich ins Control PanelВ PrestaShop ein, indem Sie umbenanntes Verzeichnis "admin" nutzen, und fangen
    Sie an, den Katalog mit Ihren Daten zu füllen.</p>

