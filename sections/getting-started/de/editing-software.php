<h3>Software</h3>

<p>Um mit der Vorlage arbeiten zu können, müssen Sie alle notwendigen Anwendungen
    herunterladen. Die Liste von notwendigen Softwares befindet sich auf der
    Vorschauseite der Vorlage.
    Die Anforderungen an verschiedenen Vorlagen können sich unterscheiden, deswegen
    nennen wir nur die wichtigsten:</p>
<ol class="index-list">
    <li>
        <p>Vor allem brauchen Sie die richtigen Anwendungen, um passwortgeschütztes Archiv sources_#########.zip zu
            entpacken.Sie können WinZip 9+ (Windows) und Stuffit Expander 10+ (Mac) nutzen.</p>
    </li>
    <li>
        <p>Sie können auch das Programm Adobe Photoshop benötigen. Es wird benutzt, um die Quelldateien .PSD zu
            bearbeiten oder das grafische Design und Bilder der Vorlage zu ändern.</p>
    </li>
    <li>
        <p>Um den Quellcode der Vorlagendateien zu bearbeiten, müssen Sie den Code-Editor wie Adobe Dreamweaver,
            Notepad++, Sublime Text usw. nutzen.</p>
    </li>
    <li>
        <p>Um die Dateien auf den Hosting-Server hochzuladen, brauchen Sie den FTP-Kunden wie Total Commander,
            FileZilla, CuteFTP usw.</p>
    </li>
</ol>
