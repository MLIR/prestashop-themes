<h3>Verwendung des lokalen Servers</h3>

<p> PrestaShop kann auf dem Personalcomputer mit Hilfe von dem lokalen Server
    installiert werden. Um den lokalen Hosting-Server zu erstellen, sollen Sie die
    Softwares wie WAMP, AppServ, MAMP usw. nutzen. Jede von ihnen wird wie normales
    Programm installiert, das PrestaShop unterstützt.</p>

