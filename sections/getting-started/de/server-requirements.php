<h3>Anforderungen an Server</h3>

<p>Überzeugen Sie sich, bitte, dass Ihr Hosting-Server den PrestaShop Anforderungen
    entspricht:</p>

<ul class="marked-list">
    <li><strong>System:</strong> Unix, Linux oderи Windows. Empfehlenswert ist Unix.
    </li>
    <li><strong>Webserver:</strong> Apache Web server 1.3 oder höher, Microsoft's IIS
        Web server 6.0 oder höher, und nginx 1.0 oder höher.
    </li>
    <li><strong>PHP 5.2</strong> oder höher.
    </li>
    <li><strong>MySQL 5.0</strong> oder höher.</li>
    <li>Mindestens 32 MB Arbeitsspeicher auf dem Server (empfehlenswert ist 64 Mb, je
        mehr, desto besser).
    </li>
</ul>

<p class="alert alert-info"> PrestaShop funktioniert mit PHP 5.1 oder höher, weil die Versionen vor 5.2 Fehler
    enthalten. Aus diesem Grund funktionieren einige Funktionen nicht korrekt (zum Beispiel, falsches Datum der
    Zeitzonen).</p>