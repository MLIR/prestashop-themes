<h3>Hochladen von Dateien der PrestaShop Engine</h3>

<p>Wenn Sie das Archiv lokal (auf Ihrem Computer) entpackt haben, sollen Sie die Dateien
    aufs Hosting hochladen.</p>

<p>Dafür können Sie den Dateimanager im Control Panel des Hostings oder eine von den FTP
    Anwendungen wie: FileZilla, TotalCommander, CureFTP usw. nutzen.</p>

<p>Sie können auch die ausführliche Video-Anleitung anschauen <a target="_blank"
                                                                 href="/help/how-upload-files-server-2.html">Wie
        lädt man die Dateien auf den Server per FTP-Kunde hoch</a> und <a target="_blank"
                                                                          href="/help/how-to-upload-files-to-a-server-using-cpanel-webhost-manager-whm.html">Wie
        lädt man die Dateien per cPanel (WebHost Manager, WHM)</a> hoch.</p>
