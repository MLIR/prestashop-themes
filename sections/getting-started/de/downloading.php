<h3>Herunterladen</h3>

<p>Um PrestaShop zu installieren, laden Sie die PrestaShop Engine von der offiziellen
    Website herunter <a target="_blank"
                        href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>

<p>Geben Sie den Namen und die E-Mail-Adresse ins Downloadfeld ein und drücken Sie auf
    den Button <strong>'Herunterladen'</strong>. </p>

<p class="alert alert-warning">Überzeugen Sie sich, bitte, dass die Version PrestaShop den Vorlagenanforderungen
    entspricht. Alle Versionen PrestaShop sind unter dem Link <a
        class="spec_link" target="_blank"
        href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>verfügbar.</p>