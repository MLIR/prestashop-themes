<h3>Entpacken von Dateien</h3>

<p>Nachdem Sie die PrestaShop Engine heruntergeladen haben, sollen Sie die Dateien und
    Ordner aus dem Archiv entpacken.</p>

<p>Dies können Sie mit Hilfe von jeglichem Archivierungsprogramm, zum Beispiel, <strong>WinZip(PC)</strong>,
    <strong>StuffitExpander(MAC)</strong>, usw. machen.</p>

<p>Nutzen Sie die folgenden Tutorials, um die Archive mit Hilfe von <a target="_blank"
                                                                       href="/help/extract-archived-files-windows-os.html">WinZip(PC)</a>
    und <a target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander(MAC)</a>
    zu entpacken.</p>

<p class="alert alert-warning">Achtung: wenn es im Control Panel desHosting die Option zur arbeit mit Dateien <strong>'Entpacken'</strong>gibt,
    können Sie sie nutzen, um die Dateien auf dem Server nach dem Hochladen von .zip Archiven zu entpacken. </p>

