<h4>Zgodność z systemem operacyjnym</h4>


<p>Sprawdź ustawienia PHP i uprawnienia katalogu. W przypadku jakichkolwiek błędów,
    prosimy o kontakt z dostawcą usług hostingowych w celu uzyskania pomocy.</p>

<p>Jeśli wszystko jest w porządku, kliknij przycisk <strong>'Next'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/02-install.png" alt="">
</figure>

<h4>Konfiguracja sklepu</h4>


<p>Ten krok wymaga od Ciebie wprowadzenia ustawień, takich jak: nazwa sklepu,
    aktywności, kraju, strefy czasowej, itd.</p>

<p>Wszystkie te ustawienia można później zmienić w panelu administracyjnym
    PrestaShop.</p>

<figure class="img-polaroid">
    <img src="img/04-install.png" alt="">
</figure>
<h4>Konfiguracja systemu</h4>


<p>Jeśli masz już bazę danych MySQL, wpisz dane z bazy danych w odpowiednich polach
    poniżej.</p>

<p>W przeciwnym razie, możesz uzyskać dane z bazy danych z panelu kontrolnego hostingu
    lub kontaktując się z dostawcą usług hostingowych.</p>


<p>Gdy skończysz, kliknij przycisk <strong>'Next'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/03-install.png" alt="">
</figure>
<h4>Instalacja została zakończona</h4>

<p>Jest to ostatni etap instalacji.</p>


<p class="alert alert-danger">Ze względów bezpieczeństwa, należy usunąć katalog <strong>/install/</strong> na Twoim
    serwerze hostingowym.</p>

<figure class="img-polaroid">
    <img src="img/05-install.png" alt="">
</figure>
<p class="alert alert-info">Począwszy od wersji 1.5.4, PrestaShop zmienia nazwę folderu administracyjnego dla
    Ciebie: tworzy słowo "admin", a następnie wybiera 4 losowe cyfry, np: "admin8814." Zmiana nazwy następuje jak
    tylko dotrzesz do folderu "admin" po raz pierwszy: PrestaShop zmienia jego nazwę, a następnie pozwala uzyskać
    dostęp do folderu o zmienionej nazwie.<br/>
    <br/>
    Zaloguj się do PrestaShop back office, przechodząc do folderu z nową, zmienioną nazwą "admin" i zacznij
    wypełniać swój katalog swoimi danymi.</p>

