<h3>Instalacja silnika PrestaShop</h3>

<p>Gdy zakończysz przesyłanie plików, możesz rozpocząć instalację silnika PrestaShop.
    Otwórz przeglądarkę internetową i wpisz w "nazwę domeny / ścieżkę do katalogu
    PrestaShop" w pasku adresu, a następnie odśwież bieżącą stronę. Powinieneś zobaczyć
    ekran powitalny. Postępuj zgodnie z poniższymi instrukcjami, aby zainstalować
    PrestaShop.</p>
<h4>Kreator instalacji (Wybierz język)</h4>


<p>Proszę wybierz język instalacji</p>

<p class="alert alert-warning">Uwaga: Opcja wyboru języka instalacji nie ma wpływu na język sklepu. PrestaShop
    oferowany jest domyślnie w języku angielskim. Możesz zainstalować dodatkowe pakiety lokalizacji, aby zmienić
    język w sklepie. </p>
<figure class="img-polaroid">
    <img src="img/01-install.png" alt="">
</figure>

<p>Gdy zakończysz, kliknij przycisk <strong>'Next'</strong>.</p>
