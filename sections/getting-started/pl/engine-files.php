<h3>Przesyłanie plików silnika PrestaShop</h3>

<p>Po rozpakowaniu plików lokalnie na Twoim komputerze, należy wysłać je na Twój serwer
    hostingowy.</p>

<p>Można tego dokonać używając menadżera plików z panelu kontrolnego hostingu lub innych
    aplikacji FTP Manager jak: FileZilla, TotalCommander, CuteFTP, itp.</p>

<p>Sprawdź szczegółowy wideo tutorial na <a target="_blank"
                                            href="/help/how-upload-files-server-2.html">Jak
        przesłać pliki na serwer za pomocą menadżera FTP</a> oraz <a target="_blank"
                                                                     href="/help/how-to-upload-files-to-a-server-using-cpanel-webhost-manager-whm.html">
        Jak przesłać pliki do serwera przy użyciu cPanel (WebHost Manager, WHM)</a>.</p>