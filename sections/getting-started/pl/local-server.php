<h3>Korzystanie z serwera lokalnego</h3>

<p> Możesz używać Prestashop lokalnie na swoim komputerze przy użyciu serwera lokalnego.
    Aby stworzyć serwer lokalny, użyj aplikacji localhost, takiej jak: WAMP, AppServ,
    MAMP, itp. Każda z nich może być łatwo zainstalowana, jak każdy inny program i
    używana do uruchomienia PrestaShop.</p>

