<h3>Edytowanie oprogramowania</h3>

<p>Aby komfortowo pracowało się z szablonem Prestashop, zalecamy pobranie wszystkich
    wymaganych aplikacji. Możesz sprawdzić listę wymaganego oprogramowania na stronie
    podglądowej szablonu. Wymagania mogą się różnić w zależności od szablonu,
    przedstawimy generalnie co jest potrzebne:</p>
<ol class="index-list">
    <li>
        <p>Po pierwsze, potrzebujesz odpowiednich aplikacji do rozpakowania zabezpieczonego hasłem archiwum
            sources_#########.zip. Możesz użyć WinZip 9+ (Windows) lub Stuffit Expander 10+ (Mac).</p>
    </li>
    <li>
        <p>Możesz również potrzebować aplikacji Adobe Photoshop. Służy ona do edytowania plików źródłowych PSD i
            jest niezbędna, jeżeli chcesz edytować grafiki i obrazy szablonu.</p>
    </li>
    <li>
        <p>Aby edytować pliki źródłowe z kodem szablonu, będziesz potrzebować edytora kodu, takich jak: Adobe
            Dreamweaver, Notepad++, Sublime Text, itp.</p>
    </li>
    <li>
        <p>Aby przesłać pliki do serwera hostingowego, będziesz potrzebować FTP Managera jak Total Commander,
            FileZilla, CuteFTP, itp.</p>
    </li>
</ol>
