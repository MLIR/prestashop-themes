<h3>Rozpakowanie plików</h3>

<p>Gdy pakiet silnika PrestaShop zostanie pobrany, należy rozpakować jego pliki i
    foldery z archiwum.</p>

<p>Można tego dokonać używając każdego menadżera archiwum, jak:
    <strong>WinZip(PC)</strong>, <strong>StuffitExpander(MAC)</strong>, itp.</p>

<p>Proszę sprawdź następujące tutoriale pokazujące jak rozpakować pliki z archiwów <a
        target="_blank"
        href="/help/extract-archived-files-windows-os.html">WinZip(PC)</a> oraz <a
        target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander(MAC)</a>.
</p>

<p class="alert alert-warning">Uwaga: Jeżeli panel kontrolny Twojego hostingu ma możliwość <strong>'Rozpakowania'</strong>
    plików, można go użyć, aby rozpakować pliki bezpośrednio na serwerze po przesłaniu plików .zip. </p>