<h3>Wymagania serwera</h3>

<p>Prosimy upewnić się, że Twój serwer hostingowy spełnia wymagania PrestaShop:</p>
<ul class="marked-list">
    <li><strong>System:</strong> Unix, Linux lub Windows. Unix jest zalecany.</li>
    <li><strong>Web server:</strong> Apache Web server 1.3 lub nowszy, Microsoft's IIS
        Web server 6.0 lub nowszy, oraz nginx 1.0 lub nowszy.
    </li>
    <li><strong>PHP 5.2</strong> lub nowszy.
    </li>
    <li><strong>MySQL 5.0</strong> lub nowszy.</li>
    <li>Co najmniej 32 MB pamięci RAM na serwerze (64 Mb pamięci jest wystarczające, im
        więcej jednak, tym lepiej).
    </li>
</ul>
<p class="alert alert-info"> PrestaShop będzie pracował z PHP 5.1 lub nowszym, ale wersje niższe niż 5.2 mają błędy,
    które mogłyby uniemożliwić niektórym funkcjom działanie zgodnie z oczekiwaniami (jak błędna data strefy
    czasowej).</p>
