<h3>Pobieranie</h3>

<p>Aby zainstalować PrestaShop, należy pobrać pakiet silnika PrestaShop z oficjalnej
    strony internetowej <a target="_blank"
                           href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>

<p>Wystarczy wpisać swoje imię lub nazwisko oraz e-mail w polu Download i kliknąć
    przycisk <strong>'Download'</strong>. </p>

<p class="alert alert-warning">Proszę upewnij się, że wersja silnika PrestaShop odpowiada wymogom szablonów. Wszystkie
    wersje Prestashop są dostępne na <a
        class="spec_link" target="_blank"
        href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.</p>

