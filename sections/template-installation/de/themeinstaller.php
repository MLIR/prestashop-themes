<h3>Import des Themas</h3>

<p class="alert alert-warning">Achtung: diese Methode wird nicht Demodaten installieren.</p>

<h4>Vorbereitung der VorlageПодготовка
    шаблона.</h4>

<p>Bevor Sie anfangen, überzeugen Sie sich, dass Sie die Vorlagendateien heruntergeladen
    und entpackt haben. Sie können ausführliche Tutorials nutzen, um die Dateien aus dem
    Archiv der Vorlage mit Hilfe von <a target="_blank"
                                        href="/help/unzipping-your-template.html">WinZip</a>
    und <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>
    zu entpacken.</p>

<p>Nachdem Sie die Dateien entpackt haben, können Sie die Vorlage hochladen.</p>

<h4>Installation.</h4>

<p>Loggen Sie sich ins Control Panel PrestaShop ein und gehen Sie in <strong>Preferences
        -> Themes (Einstellungen -> Themata)</strong>. In der oberen Ecke drücken Sie den
    Button <strong>Add new theme (Neues Thema hinzufügen)</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-2.png" alt=""/>
</figure>


<p>Klicken Sie auf den Button <strong>Add file (Datei hinzufügen)</strong> im Bereich
    Import from your computer (Import von Ihrem Computer). Finden Sie die entpackte
    Vorlage und öffnen Sie den Ordner <strong>themeinstallator</strong>.</p>

<p>Wählen Sie die .zip Datei des Themas und drücken Sie <strong>Save
        (Speichern)</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-3.png" alt=""/>
</figure>


<p>Der Hochladen-Prozess wird gestartet. Dann werden Sie die Mitteilung sehen, dass das
    Thema erfolgreich hochgeladen wurden. Das Icon des neuen Themas wird in der Liste
    von installierten Themeten dargestellt.</p>
<figure class="img-polaroid">
    <img src="img/05-install-4.png" alt=""/>
</figure>


<p>Um das Thema zu aktivieren, positionieren Sie den Cursor über das Icon des Themas und
    drücken Sie <strong>Use this theme (Dieses Thema verwenden)</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-5.png" alt=""/>
</figure>


<p>Sie werden das Fenster mit der Information über verwendete im Thema Module und Anzahl
    von Modulen, die aktiviert oder deaktiviert werden, sehen. Um fortzufahren, klicken
    Sie <strong>Save (Speichern)</strong>.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6.png"
         alt=""/>
</figure>

<p>Danach wird das DropDownFenster mit der Mitteilung angezeigt, dass das Thema
    erfolgreich aktiviert wurde: das Thema wurde erfolgreich installiert und Bilder zu
    festgelegten im Thema Berechtigungen angepasst. Um die Installation anzuschließen,
    drücken Sie auf den Button <strong>Finish (Abschließen)</strong>.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6_1.png" alt="">
</figure>