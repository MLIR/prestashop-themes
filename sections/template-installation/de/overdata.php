<h3>Installation der Vorlage auf den aktuellen Online-Shop</h3>

<p class="alert alert-info">Diese Installationsmethode müssen Sie in dem Fall nutzen, wenn Sie schon eine aktuelle
    Webseite von PrestaShop haben und nur die Vorlage installieren möchten.</p>

<h4>Hochladen von Vorlagendateien und Aktivierung
    des Themas</h4>

<ol class="index-list">
    <li>Öffnen Sie den Ordner Ihrer Vorlage.</li>
    <li>Gehen Sie in den Ordner <strong>/theme/</strong>.</li>
    <li>Laden Sie die Ordner <strong>/modules/</strong> und <strong>/themes/</strong>
        ins Stammverzeichnis PrestaShop hoch.
    </li>
    <li>
        <p>Im Control Panel PrestaShop gehen Sie in <strong>Preferences > Themes
                (Einstellungen > Themen)</strong>, wählen Sie das installierte Thema (auch
            können Sie das aktuelle Thema wählen). Drücken Sie auf den Button <strong>Advanced
                settings (Erweiterte Einstellungen)</strong>.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme_add.png"/>
        </figure>
    </li>
    <li>
        <p>In dem DropDownFenster legen Sie das Verzeichnis mit dem neuen Thema fest und
            drücken Sie auf den Button <strong>Save (Speichern)</strong>.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme-select.png"/>
        </figure>
    </li>
</ol>
<h4>Installation von Modulen</h4>

<p>Jede Vorlage enthält bestimmte Einstellungen für Module. Damit die Module wie auf der
    Demoversion aussehen, müssen sie eingestellt werden. Alle Module werden im Control
    Panel PrestaShop eingestellt.</p>

<p>Im Control Panel PrestaShop wählen Sie <strong>Modules > Modules (Module >
        Module)</strong> im Hauptmenü.</p>

<p>Auf der Modulseite befindet sich die Liste von allen verfügbaren Modulen PrestaShop.
    Einige von ihnen müssen installiert werden.</p>

<p>Um das Modul zu installieren, finden Sie es in der Liste und drücken Sie auf den
    Button <strong>'Install (Installieren)'</strong>.</p>

<figure class="img-polaroid">
    <img src="img/06-modules.png" alt=""/>
</figure>


<p>Indem Sie die Links des Moduls nutzen, können Sie es aktivieren/deaktivieren (Links
    <strong>Enable (Aktivieren)</strong>|<strong>Disable (Deaktivieren)</strong>), auf
    die Vorgaben zurücksetzen (Link <strong>Reset (Zurücksetzen)</strong>), das Modul
    (Link <strong>Delete (Entfernen)</strong>) entfernen oder (Link <strong>Configure
        (Einstellen)</strong>) einstellen.</p>

<figure class="img-polaroid">
    <img src="img/08-modules.png" alt=""/>
</figure>


<p>Nachdem Sie <strong>notwendige Module</strong> aktiviert haben, sollen Sie sie
    <strong>in der richtigen Position platzieren.</strong></p>

<p>Im Control Panel PrestaShop wählen Sie <strong>Modules > Positions (Module >
        Positionen)</strong> im Hauptmenü. Um das Modul in der notwendigen Position zu
    platzieren, ziehen Sie es einfach im Block in der Liste der Module.</p>

<figure class="img-polaroid">
    <img src="img/09-modules.png" alt=""/>
</figure>


<h3>Anpassen von Bildern</h3>

<p>In PrestaShop kann man festgelegte Berechtigungen für Produktbilder, Kategorien,
    Hersteller usw. nutzen.</p>

<p>Normalerweise werden die Bildergrößen wie <strong>'home'</strong> für Bilder in der
    Kategorienliste <strong>'large'</strong> für Hauptproduktbild auf der Produktseite
    benutzt.</p>

<p>Um die Bildauflösungen einzustellen, öffnen Sie das Control Panel PrestaShop, gehen
    Sie in <strong>Preferences > Images (Einstellungen > Bilder)</strong> und
    installieren Sie die Bildergröße, wie es auf der Seite <strong>Configuration Page
        (Vorlageneinstellungen)</strong> Gemo geschrieben ist.</p>