<h3>Importation du thème</h3>

<p class="alert alert-warning">NB: cette méthode ne propose pas d'installation de données d'exemple.</p>

<h4>Préparation du template.</h4>

<p>Avant de commencer, assurez-vous d'avoir téléchargé le template et extrait les
    fichiers du package du template. Vous pouvez consulter les tutoriels détaillés sur
    comment extraire le package du template à l'aide de <a target="_blank"
                                                           href="/help/unzipping-your-template.html">WinZip</a>
    et de <a target="_blank"
             href="/help/how-unzip-template-mac.html">StuffitExpander</a>.</p>

<p>Une fois les fichiers extraits, vous pouvez passer à l'upload du template.</p>

<h4>Installation.</h4>

<p>Connectez-vous à votre panneau d'administration PrestaShop et allez dans <strong>Preferences
        -> Themes</strong>. En haut de la page cliquez sur le bouton <strong>Add new
        theme</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-2.png" alt=""/>
</figure>


<p>Cliquez sur le bouton <strong>Add file</strong> dans la section Import from your
    computer. Allez dans le package de template extrait auparavant et ouvrez le dossier
    <strong>themeinstallator</strong>.</p>

<p>Sélectionnez le fichier .zip de votre thème et cliquez sur <strong>Save</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-3.png" alt=""/>
</figure>


<p>Le processus d'upload va commencer. Vous verrez le message indiquant que le thème a
    été uploadé avec succès. L'aperçu de votre nouveau thème apparaîtra dans la liste
    des thèmes installés.</p>
<figure class="img-polaroid">
    <img src="img/05-install-4.png" alt=""/>
</figure>


<p>Pour activer le thème, placez le curseur sur l'icône d'aperçu du thème et cliquez sur
    <strong>Use this theme</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-5.png" alt=""/>
</figure>


<p>Vous devriez voir l'écran avec les informations sur les modules utilisés dans le
    thème, le nombre de modules activés et désactivés. Cliquez sur <strong>Save</strong>
    pour continuer.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6.png"
         alt=""/>
</figure>

<p>Vous verrez ensuite la pop-up indiquant que l'activation du thème a réussi: le thème
    a été correctement installé, les images ont été générées à nouveau, conformément aux
    dimensions du thème définies. Pour terminer l'installation cliquez sur le bouton
    <strong>Finish</strong>.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6_1.png" alt="">
</figure>