<h3>Şablonu varolan mağazaya kurmak</h3>

<p class="alert alert-info">Hali hazırda kullanmakta olduğunuz PrestaShop'a yeni bir şablon yükleyebilirsiniz.</p>
<h4>Şablon dosyalarını yüklemek ve tema
    aktivasyonu</h4>
<ol class="index-list">
    <li>Şablon paketini açın.</li>
    <li>Ardından <strong>/theme/manual_install/</strong> klasörüne gidin.</li>
    <li><strong>/modules/</strong> ve <strong>/themes/</strong> klasörlerini PrestaShop
        ana dizinine yükleyin.
    </li>
    <li>
        <p>PrestaShop yönetici panelinden <strong>Preferences > Themes</strong>a girin,
            yüklediğiniz temayı seçin (hali hazırda bulunan temayı da
            kullanabilirsiniz). Ardından <strong>Advanced settings</strong> e tıklayarak
            gelişmiş ayarlar bölümüne girin.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme_add.png"/>
        </figure>
    </li>
    <li>
        <p>Bu ekrandan yeni temanızın adına tıklayın ve <strong>Save</strong> e basarak
            kaydedin.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme-select.png"/>
        </figure>
    </li>
</ol>
<h4>Modül kurulumu</h4>

<p>Her şablonun kendine ait modül ayarları vardır. Modüllerin tıpkı demodaki gibi
    gözükmesi için, bazı ayarlar yapmanız gerekir. Tüm modüller PrestaShop yönetici
    panelinden ayarlanabilir.</p>

<p>PrestaShop yönetici panelinizdeki ana menüden <strong>Modules > Modules</strong>
    kısmına girin.</p>

<p>Modül sayfasından tüm kullanılabilir PrestaShop modüllerini görüntüleyebilirsiniz.
    Bazılarını kurmanız gerekebilir.</p>

<p>Bir modül kurmak için modülü seçip <strong>'Install'</strong> a basmanız yeterlidir.
</p>
<figure class="img-polaroid">
    <img src="img/06-modules.png" alt=""/>
</figure>


<p>Modül bağlantılarından istediğiniz modülü etkinleştirip/ etkisizleştirebilirsiniz
    (<strong>Enable</strong>|<strong>Disable</strong> bağlantıları) veya
    (<strong>Reset</strong> bağlantısı) ile ayarlarını sıfırlayabilirsiniz ya da
    isterseniz (<strong>Delete</strong> bağlantısı) ile silip,
    (<strong>Configure</strong> bağlantısı) ile ayarlarını değiştirebilirsiniz.</p>
<figure class="img-polaroid">
    <img src="img/08-modules.png" alt=""/>
</figure>


<p>İstediğiniz modülleri <strong>etkinleştirdikten</strong> sonra onları <strong>doğru
        pozisyona yerleştirmelisiniz.</strong></p>

<p>PrestaShop yönetici panelindeki üst menüden <strong>Modules > Positions</strong> a
    girin. Modülü istediğiniz alana konumlandırmak için, modül listesinden istediğiniz
    yere sürükleyin.</p>
<figure class="img-polaroid">
    <img src="img/09-modules.png" alt=""/>
</figure>


<h3>Görsel ayarları</h3>

<p>PrestaShop'ta ürün görselleriniz, kategori görselleriniz, imalatçı görselleriniz vs
    için önceden tanımlanmış boyutlandırmalar kullanabilirsiniz.</p>

<p>Örneğin <strong>'anasayfa'</strong> görseli için genelde kategori listesindeki ürün
    görselleriyle aynı boyutta olur, <strong>'large'</strong> (büyük) boyuttaki
    görseller ise, ürünün kendi sayfasında yer alır.</p>

<p>Ürün görselinin boyutunu ayarlamak için, PrestaShop yönetici panelinden <strong>Preferences
        > Images</strong> (Ayarlar > Görseller) bölümüne girin ve şablonun deomosunda yer
    alan <strong>Configuration Page</strong> (Ayar Sayfası) nı örnek alın.</p>
