<h3>Demo veriyle otomatik kurulum (Örnek veri kurucusu)</h3>

<p><strong>Örnek veri</strong> yi kurduktan sonra, PrestaShop mağazanız tıpkı şablonun
    demosundaki gibi görünecektir. İçinde demo ürünler, kategoriler, modüller ve mağaza
    ayarları yer alacaktır.</p>

<p class="alert alert-danger">Örnek veriyi, aktif olan websiteniz için kullanmayın! Bu, tüm ürün ve mağaza ayarlarınızın
    değiştirilmesine sebep olur.</p>
<p class="alert alert-warning">Elinizdeki PrestaShop'un Teknik Detaylar sekmesinde belirtilen versiyonla aynı olduğuna
    emin olun: 1.X.X.X.
    Ayrıca veritabanı uzantınızı <strong>ps_</strong> şeklinde olması gerekir. Eğer elinizdeki PrestaShop belirtilenle
    aynıysa, örnek veriyi yükleyin.</p>
<p>Örnek veri kurucusuyla örnek veri kurmak isterseniz, lütfen bu kılavuzdaki <a
        href="#themeinstaller">1. adımı</a> takip edin. Ardından, mağaza yönetici
    paneline girerek bu kurucuyu görebilirsiniz.</p>

<p>Bu şekilde bir kaç kolay adımla mağazanıza örnek veri girişi yapbailirsiniz. Bu
    adımları izleyin:</p>
<ol class="index-list">
    <li>
        <p>Yönetici panelinden <strong>"Install Demo Store"</strong> (Demo Mağaza Kur)
            tıklayın.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7.png" alt="">
        </figure>
    </li>
    <li>
        <p>Şablon paketinizin içindeki <strong>'sample_data'</strong> klasöründen örnek
            veri dosyalarını seçip yükleyin. Bunu <strong>"Lokal dosyalarda
                ara"</strong> düğmesine tıklayarak veya dosyaları tarayıcı penceresine
            sürükleyip bırakarak yapabilirsiniz.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Dosyaların yüklenmesini bekleyin.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_2.png" alt="">
        </figure>
    </li>
    <li>
        <p>Ardından <strong>"Continue Install"</strong> (Kuruluma devam et) e tıklayın.
        </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_3.png" alt="">
        </figure>
    </li>
    <li>
        <p>Bir kaç dakika içinde mağazanız ve diğer modülleriniz otomatik olarak
            ayarlanacaktır.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_4.png" alt="">
        </figure>
    </li>
    <li>
        <p>İşlem başarıyla tamamlandıktan sonra, son adıma geçebilir ve görsellerinizi
            yenileyebilirsiniz. Şimdi <strong>"Go To Regenerate Page"</strong> (Sayfayı
            Yenile) ye basın.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_5.png" alt="">
        </figure>
    </li>
    <li>
        <p>Görselleri yenilemek için <strong>"Regenerate thumbnails"</strong>
            (Görselleri yenile) ye basın. </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_6.png" alt="">
        </figure>
    </li>
</ol>
<p><strong>Tebrikler, demo mağazanız kuruldu.</strong></p>