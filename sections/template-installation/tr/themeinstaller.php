<h3>Tema aktarımı</h3>

<p class="alert alert-warning">Not: bu metod örnek veri kurulumu sağlamaz.</p>

<h4>Şablon hazırlığı.</h4>

<p>İşleme başlamadan önce, şablonu indirmiş olduğunuza ve şablon paketindeki dosyaları
    çıkartmış olduğunuza emin olun. Şablon paketindeki dosyaları <a target="_blank"
                                                                    href="/help/unzipping-your-template.html">WinZip</a>
    ve <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>
    programlarıyla çıkartabilirsiniz.</p>

<p>Dosyaları çıkardıktan sonra şablonu yükleme aşamasına geçebilirsiniz.</p>

<h4>Installation.</h4>

<p>PrestaShop yönetici panelinden <strong>Preferences -> Themes</strong> (Ayarlar/Tema)
    kısmına girin. Sayfanın üstünde yer alan <strong>Add new theme</strong> (Yeni tema
    ekle) düğmesine tıklayın.</p>
<figure class="img-polaroid">
    <img src="img/05-install-2.png" alt=""/>
</figure>


<p>Arından <strong>Add file</strong> (Dosya ekle) düğmesine tıklayın ve
    bilgisayarınızdan zipli şablon paketini seçip <strong>themeinstallator</strong>
    klasörünü açın.</p>

<p>.zip ile biten tema dosyasını seçin ve <strong>Save</strong> (Kaydet) e tıklayın.</p>
<figure class="img-polaroid">
    <img src="img/05-install-3.png" alt=""/>
</figure>


<p>Yükleme işlemi başlayacak ve bittiğinde mesaj göreceksiniz. Yeni temanız, kurulmuş
    temalar listesinde yerini alacak.</p>
<figure class="img-polaroid">
    <img src="img/05-install-4.png" alt=""/>
</figure>


<p>Temayı etkinleştirmek için, farenizi tema görüntüleme ikonunun üzerine getirin ve
    <strong>Use this theme</strong> (Bu temayı kullan) a tıklayın.</p>
<figure class="img-polaroid">
    <img src="img/05-install-5.png" alt=""/>
</figure>


<p>Karşınıza, temada kullanılabilecek olan aktif ve inaktif modülleri
    görüntüleyebileceğiniz bir ekran gelecek. Devam etmek için <strong>Save</strong>
    (Kaydet) e tıklayın.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6.png"
         alt=""/>
</figure>

<p>Ardından, temanın başarılı şekilde aktive edildiğine dair bir pencere açılacak. Tema
    sorunsuzca kurulmuş ve görseller yeni temaya göre düzenlenmiş duruma gelmiştir.
    Kurulumu bitirmek için <strong>Finish</strong> (Bitir) düğmesine tıklayın.</p>

