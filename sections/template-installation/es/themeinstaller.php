<h3>Importaci&oacute;n del tema</h3>

<p class="alert alert-warning">Nota: este m&eacute;todo no ofrece la instalaci&oacute;n de los datos de muestra.</p>
<h4>Preparaci&oacute;n de la
    plantilla.</h4>

<p>Antes de continuar, por favor aseg&uacute;rese de que usted ha descargado la
    plantilla y ha extra&iacute;do los archivos. Puedes consultar tutoriales detallados
    sobre c&oacute;mo extraer archivos del paquete de la plantilla mediante <a
        target="_blank" href="/help/unzipping-your-template.html">WinZip</a> and <a
        target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>.
</p>

<p>Cuando haya extra&iacute;do los archivos puede proceder a subir la plantilla.</p>
<h4>Instalaci&oacute;n.</h4>

<p>Acceda a su panel de administraci&oacute;n de PrestaShop y vaya a <strong>Preferencias
        -> Temas</strong>. En la parte superior de la p&aacute;gina haga clic en el bot&oacute;n
    <strong>A&ntilde;adir un nuevo tema</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-2.png" alt=""/>
</figure>


<p>Haga clic en el bot&oacute;n <strong>A&ntilde;adir archivo</strong> en Importar desde
    el apartado de su ordenador. Vaya al paquete de la plantilla que ya ha descomprimido
    y abra la carpeta <strong>themeinstallator</strong>.</p>

<p>Seleccione el archivo .zip del tema y haga clic en <strong>Guardar</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-3.png" alt=""/>
</figure>


<p>Se iniciar&aacute; el proceso de carga. Ver&aacute; el mensaje de que el tema se ha
    subido correctamente. Una vista previa del nuevo tema aparecer&aacute; en la lista
    de temas instalados.</p>
<figure class="img-polaroid">
    <img src="img/05-install-4.png" alt=""/>
</figure>


<p>Para activar el tema, pase el cursor sobre el icono de la vista previa del tema y
    haga clic en <strong>Usar este tema</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-5.png" alt=""/>
</figure>


<p>Deber&iacute;a ver la pantalla con la informaci&oacute;n de los m&oacute;dulos
    utilizados por el tema, el n&uacute;mero de m&oacute;dulos que est&aacute;n
    activados y desactivados. Haga clic en <strong>Guardar</strong> para continuar.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6.png" alt=""/>
</figure>

<p>Despu&eacute;s de esto, ver&aacute; la ventana emergente con el mensaje sobre la
    activaci&oacute;n exitosa del tema: el tema se ha instalado correctamente, las im&aacute;genes
    se han ajustado a las dimensiones del tema. Para terminar la instalaci&oacute;n haga
    clic en el bot&oacute;n <strong>Finalizar</strong> .</p>

