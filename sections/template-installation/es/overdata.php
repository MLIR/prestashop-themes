<h3>Instalaci&oacute;n de la plantilla sobre una tienda existente</h3>

<p class="alert alert-info">Usted puede usar este m&eacute;todo de instalaci&oacute;n si ya tiene un sitio web
    funcionando con PrestaShop y s&oacute;lo quiere instalar una nueva plantilla.</p>

<h4>Cargar los archivos de plantilla y la activar el
    tema</h4>
<ol class="index-list">
    <li>Abra el paquete de la plantilla.</li>
    <li>Luego, vaya a la carpeta <strong>/theme/manual_install/</strong>.</li>
    <li>Cargue las carpetas <strong>/modules/</strong> y <strong>/themes/</strong> al
        directorio ra&iacute;z de PrestaShop.
    </li>
    <li>
        <p>En su panel de administraci&oacute;n de PrestaShop vaya a <strong>Preferencias
                > Temas</strong>, seleccione el tema que ha instalado (tambi&eacute;n puede
            utilizar el tema que est&aacute; habilitado, esto no causar&aacute; ning&uacute;n
            problema para su tema actual). Haga clic en el bot&oacute;n <strong>Configuraci&oacute;n
                avanzada</strong> .</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme_add.png"/>
        </figure>
    </li>
    <li>
        <p>En la pantalla mostrada seleccione el directorio con el nuevo nombre del tema
            y haga clic en el bot&oacute;n <strong>Guardar</strong>.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme-select.png"/>
        </figure>
    </li>
</ol>
<h4>Instalaci&oacute;n de m&oacute;dulos</h4>

<p>Cada plantilla est&aacute; provista con algunos ajustes espec&iacute;ficos para los m&oacute;dulos.
    Para hacer que los m&oacute;dulos aparezcan igual que en la demostraci&oacute;n,
    deber&aacute; configurarlos. Todos los m&oacute;dulos se pueden configurar en el
    panel de administraci&oacute;n de PrestaShop.</p>

<p>En su panel de administraci&oacute;n de PrestaShop, seleccione <strong>M&oacute;dulos
        > M&oacute;dulos</strong> en el men&uacute; principal.</p>

<p>En la p&aacute;gina de m&oacute;dulos se puede ver la lista de m&oacute;dulos
    disponibles de PrestaShop. Puede que tenga que instalar algunos de ellos.</p>

<p>Para instalar un m&oacute;dulo, b&uacute;squelo en la lista y haga clic en el bot&oacute;n
    <strong>'Instalar'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/06-modules.png" alt=""/>
</figure>


<p>Utilizando los enlaces del m&oacute;dulo pueden activarlos o desactivarlos (enlaces
    <strong>Activar</strong>|<strong>Desactivar</strong>), restablecer sus ajustes por
    defecto (enlace <strong>Restablecer</strong>), eliminar un m&oacute;dulo (enlace
    <strong>Eliminar</strong>) o configurarlo (enlace <strong>Configurar</strong>).</p>
<figure class="img-polaroid">
    <img src="img/08-modules.png" alt=""/>
</figure>


<p>Despu&eacute;s de <strong>activar</strong> los m&oacute;dulos necesarios, tiene
    que <strong>asignarlos para corregir posiciones.</strong></p>

<p>En el panel de administraci&oacute;n de PrestaShop, seleccione <strong>M&oacute;dulos
        > Posiciones</strong> en el men&uacute; superior. Para poner un m&oacute;dulo en la
    posici&oacute;n requerida, simplemente arrastre a la secci&oacute;n de posici&oacute;n
    de la lista de m&oacute;dulos.</p>
<figure class="img-polaroid">
    <img src="img/09-modules.png" alt=""/>
</figure>


<h3>Configuraci&oacute;n de las im&aacute;genes</h3>

<p>En PrestaShop puede utilizar tama&ntilde;os de imagen predefinidas para las im&aacute;genes
    de productos, categor&iacute;as, fabricantes, etc.</p>

<p>Por ejemplo, las dimensiones de las im&aacute;genes de <strong>'home'</strong> se
    utilizan generalmente para las im&aacute;genes del producto en los listados por
    categor&iacute;a, <strong>'large'</strong> para la imagen principal del producto en
    la p&aacute;gina de informaci&oacute;n del producto.</p>

<p>Para configurar las dimensiones de imagen de los productos, abrir su panel de
    administraci&oacute;n de PrestaShop, vaya a <strong>Preferencias >
        Im&aacute;genes</strong> y establezca las dimensiones de imagen como se describe
    en <strong>P&aacute;gina de configuraci&oacute;n</strong> en la plantilla de
    demostraci&oacute;n.</p>