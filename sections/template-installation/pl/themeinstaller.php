<h3>Import motywu</h3>

<p class="alert alert-warning">Uwaga: Ta metoda nie zapewnia instalacji przykładowych danych.</p>
<h4>Przygotowanie szablonu.</h4>

<p>Zanim zaczniesz, upewnij się, że masz już pobrany szablon i rozpakowane pliki z
    pakietu szablonów. Możesz sprawdzić szczegółowe tutoriale jak rozpakować pliki z
    pakietu szablonów za pomocą programu <a target="_blank"
                                            href="/help/unzipping-your-template.html">WinZip</a>
    oraz <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>.
</p>

<p>Po rozpakowaniu plików, można przystąpić do przesyłania szablonu.</p>
<h4>Instalacja.</h4>

<p>Zaloguj się do Twojego panelu admina PrestaShop i przejdź do <strong>Preferences ->
        Themes</strong>. Na górze strony kliknij przycisk <strong>Add new theme</strong>.
</p>
<figure class="img-polaroid">
    <img src="img/05-install-2.png" alt=""/>
</figure>


<p>Kliknij przycisk <strong>Add file</strong> w sekcji Importuj z Twojego komputera.
    Przejdź do wcześniej rozpakowanego pakietu szablonu i otwórz plik <strong>themeinstallator</strong>.
</p>

<p>Wybierz plik .zip motywu i kliknij <strong>Save</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-3.png" alt=""/>
</figure>


<p>Proces przesyłania zostanie uruchomiony. Zobaczysz komunikat, kiedy motyw zostanie
    pomyślnie przesłany. Nowy podgląd motywu pojawi się na liście zainstalowanych
    motywów.</p>
<figure class="img-polaroid">
    <img src="img/05-install-4.png" alt=""/>
</figure>


<p>W celu uruchomienia motywu, skieruj kursor na ikonę podglądu motywu i kliknij
    <strong>Use this theme</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-5.png" alt=""/>
</figure>


<p>Powinieneś zobaczyć ekran z informacjami o modułach stosowanych w motywie, liczba
    modułów, które są włączane i wyłączane. Kliknij <strong>Save</strong>, aby
    kontynuować.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6.png"
         alt=""/>
</figure>

<p>Następnie pojawi się okienko pop-up z komunikatem o pomyślnej aktywacji motywu: motyw
    został zainstalowany pomyślnie, obrazy zostały ponownie wygenerowane do wymiarów
    określonych w motywie. Aby zakończyć instalację, kliknij przycisk
    <strong>Finish</strong>.</p>

