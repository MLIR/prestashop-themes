<h3>Instalacja szablonu na istniejącym sklepie</h3>

<p class="alert alert-info">Możesz użyć tej metody, jeśli masz uruchomioną stronę internetową PrestaShop i po prostu
    chcesz zainstalować nowy szablon.</p>

<h4>Przesyłanie plików szablonu oraz aktywacja
    motywu.</h4>
<ol class="index-list">
    <li>Otwórz pakiet szablonu.</li>
    <li>Następnie przejdź do folderu <strong>/theme/manual_install/</strong>.</li>
    <li>Prześlij folder <strong>/modules/</strong> oraz <strong>/themes/</strong> do
        katalogu głównego PrestaShop.
    </li>
    <li>
        <p>W panelu administratora Twojego PrestaShop przejdź do <strong>Preferences >
                Themes</strong>, wybierz motyw, który został zainstalowany (można również
            użyć aktywowany motyw, nie spowoduje to żadnych problemów dla Twojego
            aktywowanego motywu). Kliknij przycisk <strong>Advanced settings</strong>.
        </p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme_add.png"/>
        </figure>
    </li>
    <li>
        <p>Na ekranie, który się ukazał, wybierz katalog z nową nazwą motywu i kliknij
            przycisk <strong>Save</strong>.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme-select.png"/>
        </figure>
    </li>
</ol>
<h4>Instalacja modułu</h4>

<p>Każdy szablon jest wyposażony w pewne specyficzne ustawienia modułu. Aby moduły
    wyglądały tak samo jak na podglądzie demo, trzeba je skonfigurować. Wszystkie moduły
    można skonfigurować w panelu administratora PrestaShop.</p>

<p>W panelu administratora Twojego PrestaShop wybierz <strong>Modules >
        Modules</strong> z głównego menu.</p>

<p>Na stronie modułów możesz zobaczyć listę dostępnych modułów PrestaShop. Być może
    trzeba zainstalować niektóre z nich.</p>

<p>Aby zainstalować moduł, znajdź go na liście i kliknij przycisk
    <strong>'Install'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/06-modules.png" alt=""/>
</figure>


<p>Używając linków modułu, można je włączyć / wyłączyć (<strong>Enable</strong>|<strong>Disable</strong>
    links), przywrócić ich ustawienia domyślne (<strong>Reset</strong> link), usunąć
    moduł (<strong>Delete</strong> link) albo skonfigurować go
    (<strong>Configure</strong> link).</p>
<figure class="img-polaroid">
    <img src="img/08-modules.png" alt=""/>
</figure>


<p>Po <strong>aktywowaniu</strong> niezbędnych modułów, musisz <strong>przypisać je
        do właściwych miejsc.</strong></p>

<p>W panelu administratora Prestashop, wybierz <strong>Modules > Positions</strong> z
    górnego menu. Aby umieścić moduł w wymaganej pozycji, po prostu przeciągnij go do
    sekcji pozycji z listy modułów.</p>
<figure class="img-polaroid">
    <img src="img/09-modules.png" alt=""/>
</figure>


<h3>Konfiguracja obrazów.</h3>

<p>W PrestaShop można użyć wstępnie zdefiniowanych wymiarów obrazu dla zdjęć produktu,
    zdjęć kategorii, producentów, itp.</p>

<p>Na przykład, obrazy z wymiarów <strong>'home'</strong> są zazwyczaj wykorzystywane do
    obrazów listy kategorii, <strong>'large'</strong> dla obrazu głównego produktu na
    stronach produktów.</p>

<p>Aby skonfigurować wymiary obrazów produktów, otwórz panel administratora PrestaShop,
    przejdź do <strong>Preferences > Images</strong> i ustaw wymiary obrazu w sposób
    opisany w <strong>Configuration Page</strong> live demo szablonu.</p>