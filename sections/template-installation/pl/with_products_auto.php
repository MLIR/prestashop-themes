<h3>Automatyczna instalacja z danych demo (Instalator przykładowych danych)</h3>

<p>Po zainstalowaniu <strong>sample data</strong>, wygląd sklepu PrestaShop będzie taki
    sam, jak w szablonie live demo. Będzie on zawierał demo wszystkich produktów,
    kategorii, modułów i ustawienia sklepowe.</p>

<p class="alert alert-danger">Proszę nie używaj przykładowych danych na swojej aktualnej stronie internetowej! Zastąpią
    one wszystkie produkty sklepu i ustawienia sklepowe.</p>
<p class="alert alert-warning">Upewnij się, że Twoja wersja PrestaShop jest zgodna z wersją w zakładce Technical Details,
    wspomnianej w PrestaShop Engine: 1.X.X.X.
    Również, upewnij się, że prefix bazy danych to <strong>ps_</strong>. Tylko wtedy, gdy silnik PrestaShop zgadza się z
    wybraną wersją, zainstaluj dane przykładowe. </p>
<p>Aby zainstalować przykładowe dane za pomocą modułu instalatora przykładowych danych,
    należy postępować zgodnie z instrukcjami z kroku 1 <a href="#themeinstaller">(Theme
        import)</a> niniejszej instrukcji. Następnie, w panelu administracyjnym sklepu
    zobaczysz moduł instalatora przykładowych danych.</p>

<p>Korzystając z tego modułu, można łatwo zainstalować przykładowe dane sklepu w kilku
    prostych krokach. Wykonaj następujące czynności:</p>
<ol class="index-list">
    <li>
        <p>Z menu panelu administracyjnego kliknij pozycję <strong>"Install Demo
                Store"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7.png" alt="">
        </figure>
    </li>
    <li>
        <p>Wybierz i prześlij pliki z przykładowymi danymi z katalogu <strong>'sample_data'</strong>
            Twojego pakietu szablonów. Można to zrobić za pomocą przycisku <strong>"Browse
                local files"</strong> lub przeciągając pliki przykładowych danych do
            okna przeglądarki.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Poczekaj, aż pliki zostaną załadowane.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_2.png" alt="">
        </figure>
    </li>
    <li>
        <p>Następnie kliknij przycisk <strong>"Continue Install"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_3.png" alt="">
        </figure>
    </li>
    <li>
        <p>Następnie instalator danych przykładowych automatycznie skonfiguruje Twój
            sklep i inne moduły. Może to zająć kilka minut.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_4.png" alt="">
        </figure>
    </li>
    <li>
        <p>Gdy przykładowe dane zostaną pomyślnie zainstalowane, należy wykonać ostatni
            krok - odtworzenie obrazów. Kliknij przycisk <strong>"Go To Regenerate
                Page"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_5.png" alt="">
        </figure>
    </li>
    <li>
        <p>Aby odtworzyć obrazy, kliknij przycisk <strong>"Regenerate
                thumbnails"</strong>. </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_6.png" alt="">
        </figure>
    </li>
</ol>
<p><strong>Gratulacje, Twoja wersja demo sklepu została zainstalowana.</strong></p>