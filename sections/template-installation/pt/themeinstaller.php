<h3>Importar o tema</h3>

<p class="alert alert-warning">Nota: esse método não providencia dados de demonstração instalados.</p>

<h4>Preparação do template.</h4>

<p>Antes de prosseguir, certifique-se de que você já baixou o seu template e extraiu os
    arquivos do pacote do template. Você pode dar uma olhada em tutoriais sobre
    descompactação/extração de arquivos aqui: <a target="_blank"
                                                 href="/help/unzipping-your-template.html">WinZip</a>
    and <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>.
</p>

<p>Quando os arquivos são extraidos, você pode prosseguir com o envio do seu template
    para o servidor.</p>

<h4>Instalação.</h4>

<p>Entre no seu painel administrativo do PrestaShop e navegue até <strong>Preferências
        (Preferences) -> Temas (Themes)</strong>. No topo da página, clique em <strong>Adicionar
        novo tema (Add new theme)</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-2.png" alt=""/>
</figure>


<p>Clique em <strong>Adicionar arquivo (Add file)</strong> para importar do seu
    computador. Navegue até a pasta onde você descompactou os arquivos do template e
    abra a pasta <strong>themeinstallator</strong>.</p>

<p>Escolha o arquivo .zip do seu tema e clique em <strong>Salvar (Save)</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-3.png" alt=""/>
</figure>


<p>O processo de envio será iniciado. Você verá uma mensagem de que o tema foi enviado
    com sucesso. O preview do seu tema vai aparecer na lista de temas instalados.</p>
<figure class="img-polaroid">
    <img src="img/05-install-4.png" alt=""/>
</figure>


<p>Para ativar o tema, passe o a seta do mouse em cima do ícone de preview do tema e
    clique em <strong>Usar este tema (Use this theme)</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-5.png" alt=""/>
</figure>


<p>Você deve ver uma tela com informações dos módulos usados no seu tema e o número de
    módulos que estão desligados e ligados. Clique em <strong>Salvar (Save)</strong>
    para prosseguir.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6.png"
         alt=""/>
</figure>

<p>Depois disso, você verá uma mensagem com a ativação do tema: o tema foi instalado com
    sucesso, imagens serão geradas novamente para caber nas dimensões do tema. Para
    terminar a instalação clique em <strong>Terminar (Finish)</strong>.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6_1.png" alt="">
</figure>