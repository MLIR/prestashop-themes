<h3>Como instalar o template em um site já existente</h3>

<p class="alert alert-info">Você pode usar esse método se você já tem um site PrestaShop e quer apenas instalar um
    novo tema.</p>

<h4>Enviar arquivos e ativar o tema</h4>
<ol class="index-list">
    <li>Abra o pacote do template.</li>
    <li>Vá para a pasta <strong>/theme/manual_install/</strong>.</li>
    <li>Envie as pastas <strong>/img/</strong>, <strong>/modules/</strong> and <strong>/themes/</strong>
        para dentro do diretório raiz do PrestaShop.
    </li>
    <li>
        <p>No painel administrativo do PrestaShop navegue até <strong>Preferências
                (Preferences) > Temas (Themes)</strong>, escolha o tema que você instalou
            (você também pode usar o tema habilitado, isso não ccausará problemas).
            Clique em <strong>Configurações avançadas (Advanced settings)</strong>.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme_add.png"/>
        </figure>
    </li>
    <li>
        <p>Na tela que aparecer, escolha a pasta com seu novo tema e clique em <strong>Salvar
                (Save)</strong>.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme-select.png"/>
        </figure>
    </li>
</ol>
<h4>Instalar módulos</h4>

<p>Cada tema vem com alguns módulos específicos para configurar. Se quiser que os
    módulos fique iguais ao site de demonstração, você vai precisar configurá-los. Todos
    os módulos podem ser configurados através do painel administrativo do
    PrestaShop.</p>

<p>No painel, escolha <strong>Módulos (Modules) > Módulos (Modules)</strong> no menu
    principal.</p>

<p>Na página de módulos, você pode ver uma lista de módulos PrestaShop disponíveis.
    Talvez você precise instalar alguns deles.</p>

<p>Para instalar um módulo, localize-o na lista e clique no botão <strong>'Instalar
        (Install)'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/06-modules.png" alt=""/>
</figure>


<p>Os links dos módulos servem para ativar/desativar suas funcionalidades (clicando em
    <strong>Habilitar (Enable)</strong>|<strong>Desabilitar (Disable)</strong>),
    redefinir suas configurações para as configurações padrão, (<strong>Resetar
        (Reset)</strong>), apagar um módulo (<strong>Deletar (Delete)</strong>) ou
    configurá-los (<strong>Configurar (Configure)</strong>).</p>
<figure class="img-polaroid">
    <img src="img/08-modules.png" alt=""/>
</figure>


<p>2. Após <strong>ativar</strong> os módulos necessários, você precisa <strong>definir
        as posições corretas deles.</strong></p>

<p>No paienl, vá para <strong>Módulos (Modules) > Posições (Positions)</strong> no menu
    superior. Para mudar a posição de um módulo, clique e arraste até a posição desejada
    na lista de módulos.</p>
<figure class="img-polaroid">
    <img src="img/09-modules.png" alt=""/>
</figure>


<h3>Configurar imagens</h3>

<p>No PrestaShop você pode usar tamanhos pré-definidos para as imagens de produtos, de
    categorias, fabricantes, etc.</p>

<p>Por exemplo, imagens com o tamanho <strong>'home'</strong> em geral são para imagens
    de produto em listagens de categorias, <strong>'large'</strong> para a imagem
    principal do produto na página de informações dele.</p>

<p>Para configurar o tamanho da imagem do produto, abra o seu painel do Prestashop, vá
    para <strong>Preferências (Preferences) > Imagens (Images)</strong> e mude os
    tamanhos das imagens como está descrito na página de <strong>Configurações
        (Configuration Page)</strong> no site de amostra do tema.</p>