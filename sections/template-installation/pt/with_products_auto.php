<h3>Instalação automática com dados de amostra (Instalador de demonstração)</h3>

<p>Após instalar os <strong>dados de amostra</strong>, você terá a loja do PrestaShop
    idêntica ao demo que você viu ao comprar o template. Ela terá todos os produtos de
    demonstração, categorias, módulos e configurações da loja.</p>

<p class="alert alert-danger">Não use esses dados de amostra num site que está no ar! Isso vai substituir todos os
    produtos e configurações da sua loja.</p>
<p class="alert alert-warning">Cartifique-se que sua versão da engine do PrestaShop é compatível com a especificada na
    aba de detalhes técnicos (Technical Details), mencionada como PrestaShop Engine: 1.X.X.X.
    <br/>Também certifique-se que o prefixo do seu banco de dados é <strong>ps_</strong>. Apenas instale os dados de
    amostra se sua loja da Prestashop é a mesma especificada.</p>
<p>Para instalar os dados de amostra usando o módulo instalador de dados de amostra,
    siga as instruções do passo 1 <a href="#themeinstaller">(Theme import)</a> desse
    manual. Então, no painel administrativo de sua loja, você verá o módulo instalador
    de dados de amostra (Sample data installer module).</p>

<p>Ao usar este módulo você pode facilmente instalar dados de amostra na loja em poucos
    passos simples. Faça o seguinte:</p>
<ol class="index-list">
    <li>
        <p>No menu do painel administrativo clique em <strong>"Instalar Loja de
                Demonstração (Install Demo Store)"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7.png" alt="">
        </figure>
    </li>
    <li>
        <p>Marque e envie os dados de amostra do diretório
            <strong>'sample_data'</strong> no pacote do template. Você pode fazer isso
            usando o botão <strong>"Procurar arquivos locais (Browse local
                files)"</strong> ou arrastando os dados de amostra para dentro da janela
            do seu navegador.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Espere terminar o envio.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_2.png" alt="">
        </figure>
    </li>
    <li>
        <p>Então clique em <strong>"Continuar Instalação (Continue Install)"</strong>.
        </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_3.png" alt="">
        </figure>
    </li>
    <li>
        <p>O instalador vai configurar sua loja e os outros módulos. Isso pode levar um
            tempo. </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_4.png" alt="">
        </figure>
    </li>
    <li>
        <p>Quando os dados de amostra forem instalados corretamente, você precisa fazer
            mais uma coisa - regerar as imagens. Clique em <strong>"Ir para a página de
                regeração (Go To Regenerate Page)"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_5.png" alt="">
        </figure>
    </li>
    <li>
        <p>Para regerar as imagens, clique em <strong>"Regerar miniaturas (Regenerate
                thumbnails)"</strong> button. </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_6.png" alt="">
        </figure>
    </li>
</ol>
<p><strong>Parabéns, sua loja de demonstração está instalada.</strong></p>