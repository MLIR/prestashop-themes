<h3>Импорт темы</h3>

<p class="alert alert-warning">Внимание: данный метод не устанавливает демо-данные.</p>

<h4>Подготовка шаблона.</h4>

<p>Перед тем, как начать, убедитесь, что вы уже скачали и распаковали файлы шаблона. Вы
    можете воспользоваться детальными туториалами, как извлечь файлы из архива шаблона
    при помощи <a target="_blank" href="/help/unzipping-your-template.html">WinZip</a> и
    <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>.</p>

<p>После извлечения файлов можно перейти к загрузке шаблона.</p>

<h4>Установка.</h4>

<p>Войдите в панель управления PrestaShop и перейдите в <strong>Preferences -> Themes
        (Настройки -> Темы)</strong>. В верхнем углу нажмите кнопку <strong>Add new theme
        (Добавить новую тему)</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-2.png" alt=""/>
</figure>


<p>Щелкните кнопку <strong>Add file (Добавить файл)</strong> в разделе Import from your
    computer (Импортировать с компьютера). Перейдите к разархивированному шаблону и
    откройте папку <strong>themeinstallator</strong>.</p>

<p>Выберите файл .zip темы и нажмите <strong>Save (Сохранить)</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-3.png" alt=""/>
</figure>


<p>Начнется процесс загрузки. Затем появится сообщение, что тема успешно загружена.
    Иконка новой темы появится в списке установленных тем.</p>
<figure class="img-polaroid">
    <img src="img/05-install-4.png" alt=""/>
</figure>


<p>Чтобы активировать тему, наведите курсор мыши на иконку предпросмотра темы и нажмите
    <strong>Use this theme (Использовать эту тему)</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-5.png" alt=""/>
</figure>


<p>Появится экран с информацией об используемых в теме модулях, количестве модулей,
    которые отключаются и включаются. Чтобы продолжить, нажмите <strong>Save
        (Сохранить)</strong>.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6.png"
         alt=""/>
</figure>

<p>После этого появится выпадающее окно с уведомлением об успешной активации темы: тема
    была успешно установлена, картинки регенерированы до заданных в теме разрешений. Для
    завершения установки нажмите кнопку <strong>Finish (Завершить)</strong>.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6_1.png" alt="">
</figure>