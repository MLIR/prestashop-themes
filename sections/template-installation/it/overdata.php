<h3>Installare il template su di un negozio pre-esistente</h3>

<p class="alert alert-info">Puoi usare questo metodo di installazione se hai già un sito PrestaShop attivo e vuoi
    soltanto installare un template nuovo.</p>

<h4>Caricare i file del template e attivare il
    tema</h4>
<ol class="index-list">
    <li>Apri il pacchetto template.</li>
    <li>Vai sulla cartella <strong>/theme/manual_install/</strong>.</li>
    <li>Carica le cartelle <strong>/modules/</strong> e <strong>/themes/</strong> sulla
        directory root di PrestaShop.
    </li>
    <li>
        <p>Dal tuo pannello di amministrazione PrestaShop vai su <strong>Preferences >
                Themes</strong>, seleziona il tema che hai installato (puoi anche usare il
            tema abilitato, non causerà alcun problema per il tema abilitato). Clicca su
            <strong>Advanced settings</strong>.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme_add.png"/>
        </figure>
    </li>
    <li>
        <p>Sulla schermata che apparirà, seleziona la directory con il tuo nuovo tema e
            clicca su <strong>Save</strong>.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme-select.png"/>
        </figure>
    </li>
</ol>
<h4>Installazione Moduli</h4>

<p>Ogni template include alcune impostazioni specifiche per i moduli. Per fare in modo
    che i moduli abbiano lo stesso aspetto dell'anteprima dimostrativa dovrai
    configurarli. Tutti i moduli possono essere configurati dal pannello di
    amministrazione di PrestaShop.</p>

<p>Dal tuo pannello di amministrazione PrestaShop, seleziona <strong>Modules >
        Modules</strong> dal menù principale.</p>

<p>Sulla pagina Modules potrai vedere la lista di moduli PrestaShop disponibili.
    Potrebbe essere necessario installarne alcuni.</p>

<p>Per installare un modulo, individualo sulla lista e clicca su
    <strong>'Install'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/06-modules.png" alt=""/>
</figure>


<p>Usando i link del modulo potrai attivarli e disattivarli
    (<strong>Enable</strong>|<strong>Disable</strong>), riportare le impostazioni ai
    valori predefiniti (<strong>Reset</strong>), eliminare un modulo
    (<strong>Delete</strong>) oppure configurarlo (<strong>Configure</strong>).</p>
<figure class="img-polaroid">
    <img src="img/08-modules.png" alt=""/>
</figure>


<p>Dopo aver <strong>attivato</strong> i moduli necessari, dovrai <strong>assegnarli
        alle posizioni corrette.</strong></p>

<p>Nel pannello di amministrazione di PrestaShop, vai su <strong>Modules >
        Positions</strong> dal menù in alto. Per mettere un modulo nella posizione corretta,
    trascinalo nella posizione desiderata nella lista dei moduli.</p>
<figure class="img-polaroid">
    <img src="img/09-modules.png" alt=""/>
</figure>


<h3>Configurare le immagini</h3>

<p>Su PrestaShop si possono usare dimensioni predefinite per le immagini dei prodotti,
    le immagini di categoria, i produttori, ecc.</p>

<p>Ad esempio, le immagini di dimensioni <strong>'home'</strong> vengono solitamente
    utilizzate per le immagini prodotti negli elenchi di categoria,
    <strong>'large'</strong> per le immagini principali del prodotto nella pagina
    principale del prodotto.</p>

<p>Per configurare le dimensioni di immagine del prodotto apri il pannello di
    amministrazione di PrestaShop, vai su <strong>Preferences > Images</strong> e
    imposta le idimensioni delle immagini come descritto su <strong>Configuration
        Page</strong> nella live demo del template.</p>