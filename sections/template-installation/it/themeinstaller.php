<h3>Importa il tema</h3>

<p class="alert alert-warning">Nota: questo metodo non fornisce dati campioni per l'installazione.</p>

<h4>Preparazione Template.</h4>

<p>Prima di procedere verifica di aver già scaricato il template e di averne estratto i
    file. Puoi consultare le guide dettagliate su come estrarre i file del pacchetto
    template usando <a target="_blank"
                       href="/help/unzipping-your-template.html">WinZip</a> e <a
        target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>.
</p>

<p>Una volta estratti i file, puoi procedere con il caricamento del template.</p>

<h4>Installazione.</h4>

<p>Accedi al pannello di amministrazione PrestaShop e vai su <strong>Preferences ->
        Themes</strong>. In alto sulla pagina, clicca su <strong>Add new theme</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-2.png" alt=""/>
</figure>


<p>Clicca su <strong>Add file</strong> nell'area "Import from your computer". Naviga sul
    pacchetto template estratto in precedenza e apri la cartella <strong>themeinstallator</strong>.
</p>

<p>Seleziona il file .zip del tuo template e clicca su <strong>Save</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-3.png" alt=""/>
</figure>


<p>A questo punto inizierà la procedura di upload. Poi vedrai il messaggio che il tema è
    stato caricato con successo - "Successful upload". La nuova anteprima del tuo tema
    apparirà nella lista di temi installati.</p>
<figure class="img-polaroid">
    <img src="img/05-install-4.png" alt=""/>
</figure>


<p>Per attivare il tema, porta il puntatore sull'icona di anteprima del tema e clicca su
    <strong>Use this theme</strong>.</p>
<figure class="img-polaroid">
    <img src="img/05-install-5.png" alt=""/>
</figure>


<p>Adesso dovresti vedere la schermata con le informazioni sui moduli utilizzati nel
    tema, il numero di moduli attualmente attivati o disattivati. Clicca su
    <strong>Save</strong> per continuare.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6.png"
         alt=""/>
</figure>

<p>A questo punto vedrai un messaggio che ti comunica della riuscita attivazione del
    tema: il tema è stato intallato correttamente e le immagini sono state rigenerate
    alle dimensioni definite. Per terminare l'installazione, clicca su
    <strong>Finish</strong>.</p>

<figure class="img-polaroid">
    <img src="img/05-install-6_1.png" alt="">
</figure>
