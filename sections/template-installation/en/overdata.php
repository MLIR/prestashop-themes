<h3>Installing template over existing store</h3>

<p class="alert alert-info">You can use this method of installation if you have a running PrestaShop website and just
    want to install a new template.</p>
<h4>Uploading template files and theme
    activation</h4>
<ol class="index-list">
    <li>Open the template package.</li>
    <li>Then go to the <strong>/theme/manual_install/</strong> folder.</li>
    <li>Upload <strong>/modules/</strong> and <strong>/themes/</strong> folders to
        PrestaShop root directory.
    <li>
        <p>In your PrestaShop admin panel navigate to <strong>Preferences >
                Themes</strong>, select the theme you installed (you can also use the
            enabled theme, it will not cause any issues for your enabled theme). Click
            <strong>Advanced settings</strong> button.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme_add.png"/>
            </p></figure>
    </li>
    <li>
        <p>On the appeared screen select the directory with your new theme name and
            click <strong>Save</strong> button.</p>

        <figure class="img-polaroid">
            <img alt="" src="img/theme-select.png"/>
            </p></figure>
    </li>
</ol>
<h4>Modules installation</h4>

<p>Each template is provided with some specific module settings. To make the modules
    appear the same as on the demo preview, you will need to configure them. All modules
    can be configured in the PrestaShop admin panel.</p>

<p>In your PrestaShop admin panel, select <strong>Modules > Modules</strong> from the
    main menu.</p>

<p>On the Modules page you can see the list of available PrestaShop modules. You may
    need to install some of them.</p>

<p>To install a module, locate it in the list and click the <strong>'Install'</strong>
    button.</p>
<figure class="img-polaroid">
    <img src="img/06-modules.png" alt=""/>
</figure>


<p>Using the module links you can activate/deactivate them
    (<strong>Enable</strong>|<strong>Disable</strong> links), reset their settings to
    default (<strong>Reset</strong> link), delete a module (<strong>Delete</strong>
    link) or configure it (<strong>Configure</strong> link).</p>
<figure class="img-polaroid">
    <img src="img/08-modules.png" alt=""/>
</figure>


<p>After <strong>activating</strong> the necessary modules, you need to <strong>assign
        them to correct positions.</strong></p>

<p>In PrestaShop admin panel, select <strong>Modules > Positions</strong> from the top
    menu. To place a module in the required position, just drag it to the position
    section of the modules list.</p>
<figure class="img-polaroid">
    <img src="img/09-modules.png" alt=""/>
</figure>


<h3>Configuring images</h3>

<p>In PrestaShop you can use predefined image dimensions for product images, category
    images, manufacturers, etc.</p>

<p>For example, images of the <strong>'home'</strong> dimensions are usually used for
    the product images on category listings, <strong>'large'</strong> for main product
    image on the product info page.</p>

<p>To configure product image dimensions, open your PrestaShop admin panel, go to
    <strong>Preferences > Images</strong> and set the image dimensions as described on
    <strong>Configuration Page</strong> at the template live demo.</p>