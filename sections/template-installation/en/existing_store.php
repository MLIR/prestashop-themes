<h3>Installing template over existing store</h3>
<p class="alert alert-info">You can use this method of installation if you have a running PrestaShop website and just want to install a new template.</p>
<p class="alert alert-info"><span>Modules configuration table can be found on the template livedemo page. Find the 'Sitemap' link in top or in footer menu and go the Sitemap -> Pages -> Pages Configuration.</span></p>
<h4>Uploading template files and theme activation</h4>
<ol class="index-list">
    <li>Open the template package.</li>
    <li>Then go to the <strong>/theme/manual_install/</strong> folder.</li>
    <li>Upload <strong>/modules/</strong> and <strong>/themes/</strong> folders to PrestaShop root directory.
    <li>
        <p>In your PrestaShop admin panel navigate to <strong>Preferences > Themes</strong>, select the theme you installed (you can also use the enabled theme, it will not cause any issues for your enabled theme). Click <strong>'Advanced settings'</strong> button (1).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-1.png"/>
        </figure>
    </li>
    <li>
        <p>On the appeared screen select the directory with your new theme name (1) and click <strong>'Save'</strong> button (2).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-2.png"/>
        </figure>
    </li>
</ol>