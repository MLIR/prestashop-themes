<h3>Adding languages to online store</h3>
<p>Once the multi-language template is installed, you will be able to add all languages provided with it (English, French, Spanish, German, Russian) to the online store platform. It can be done this way: </p>
<p>Go to <strong>'Localization > Translations'</strong> in the admin panel, find <strong>'ADD/UPDATE A LANGUAGE'</strong> and add languages by choosing them from the drop-down menu <small>(1)</small> and clicking the <strong>'Add or update a language'</strong> button <small>(2)</small>.</p>
<figure class="img-polaroid">
    <img alt="" src="img/01-language-update.png"/>
</figure>
<p>If you do not want to have some languages, you can delete them <small>(2)</small> or turn them off <small>(1)</small> in the <strong>'Localization > Languages'</strong> menu.</p>
