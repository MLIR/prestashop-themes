<h3>Contenu TM HTML</h3>

<p>Ce module permet d'afficher n'importe quel contenu (image, HTML, liens, etc.) aux positions suivantes: displayNav,
    home, top, topColumn, left and right (displayLeftColumn/displayRightColumn), footer. Il sert à étendre les options
    de design et à ajouter des blocs de publicité/information à votre boutique. C'est un module multilingue et il peut
    aussi fonctionner dans les sites à plusieurs boutiques.</p>

<h5>Pour ajouter le contenu, vous devez suivre les étapes ci-dessous:</h5>


<h6>Allez dans
    <strong>Modules->Front Office Features->TM HTML Content</strong> (si ce n'est pas installé, installez-le).
</h6>


<ol class="index-list">
    <li>Cliquez sur le bouton <strong>Add new</strong>.</li>

    <li>La liste des langues disponibles. Vous pouvez voir les éléments disponibles pour toutes les langues en naviguant
        entre les onglets des langues. </li>

    <li>La liste d'éléments ajoutés pour une position et une langue précises. </li>
</ol>
<figure class="img-polaroid">
    <img src="img/htmlcontent.png" alt="">
</figure>
<h6>Ajout de l'élément. Cliquez sur le bouton
    <strong>Add item</strong> et remplissez les champs de paramètres (aucun des champs n'est obligatoire).
</h6>


<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Language</dt>
            <dd>sélectionnez le langue requise dans la liste déroulante.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Title</dt>
            <dd>titre de l'élément.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use title in front</dt>
            <dd>afficher le titre de l'élément en avant (le titre s'affichera devant l'élément).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hook</dt>
            <dd>la position où l'élément est ajouté.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image</dt>
            <dd>uploader l'image depuis votre ordinateur.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image width</dt>
            <dd>largeur de l'image présentée.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image height</dt>
            <dd>longueur de l'image présentée.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>URL</dt>
            <dd>ajouter le lien (le bloc tout entier sera enveloppé par le lien).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Target blank</dt>
            <dd>ouvrir le lien dans un nouvel onglet.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>HTML</dt>
            <dd>code HTML (sauf le lien si vous l'avez déjà ajouté dans le champ URL) ou texte.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Save</dt>
            <dd>cliquez pour enregistrer l'élément.</dd>
        </dl>
    </li>
</ol>
<p class="alert alert-info">Tous les éléments d'une langue ajoutée à la même position seront affichés sous forme de
    liste en back-end et en front-end.</p>
<figure class="img-polaroid">
    <img src="img/htmlcontent-1.png" alt="">
</figure>

<h6>Modifier et supprimer l'élément</h6>

<p>Quand vous ajoutez l'élément, il s'affiche dans la liste correspondante.</p>


<ol class="index-list">
    <li>La langue dans laquelle les éléments ont été ajoutés.</li>

    <li>La position à laquelle les éléments ont été ajoutés.</li>

    <li>Le bouton de Modification avec la liste déroulante.</li>

    <li>Le bouton de Suppression.</li>
</ol>
<figure class="img-polaroid">
    <img src="img/htmlcontent-2.png" alt="">
</figure>

<h5><strong>Modification</strong></h5>


<h6>Cliquez sur le bouton <strong>Edit</strong> pour modifier l'élément. </h6>

<p>Dans l'écran apparu, modifiez tous les champs requis.</p>


<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Enable</dt>
            <dd>afficher l'élément en front-end.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image title</dt>
            <dd>nom de l'élément.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use title in front</dt>
            <dd>afficher le nom en front-end.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hook to which the image should be attached</dt>
            <dd>la position de l'élément</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Load your image</dt>
            <dd>l'image de l'élément.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image width</dt>
            <dd>largeur de l'image en front-end.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image height</dt>
            <dd>la hauteur de l'image en front-end.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Target link</dt>
            <dd>assigner un lien à l'élément.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Open link in a new tab/page</dt>
            <dd>Ouvrir le lien dans un nouvel onglet.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Optional HTML code</dt>
            <dd>modifier le code HTML.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Save</dt>
            <dd>enregistrer les changements.</dd>
        </dl>
    </li>
</ol>
