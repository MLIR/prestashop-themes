<h3>Compte d'en-tête TM</h3>

<p>Ce module affiche le formulaire de connexion et quelques éléments disponibles de
    compte d'utilisateur dans l'en-tête du site. Il permet de connecter à votre compte
    en restant sur la page active. Le module sert de bloc déroulante.</p>
<h5>Installation et Suppression</h5>

<p>Le module peut être installé et supprimé comme tout autre module PrestaShop.</p>
<h5>Configuration de Module</h5>

<p>Le module n'a pas de paramètres supplémentaires.</p>
