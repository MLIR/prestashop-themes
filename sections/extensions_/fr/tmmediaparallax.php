<h3>Module Media Parallax</h3>

<h4>Qu'est-ce que l'effet parallax?</h4>

<p>Le défilement Parallax est une technique spéciale de défilement en infographie, où les images d'arrière-plen se
    déplacent plus lentement par rapport à la caméra que les images au premier plan, créant ainsi une illusion de
    profondeur dans un jeu vidéo en 2D et augmentant l'immersion. Les web designers ont commencé à incorporer le
    défilement parallax en 2011, en HTML5 et en CSS3. Les sites web avec arrière-plan en parallax deviennent une
    stratégie de plus en plus populaire, car les avocats le présentent comme un moyen simple d'accéder à la fluidité sur
    le Web. De plus, les partisans utilisent des arrière-plans en parallax comme un outil pour mieux engager les
    utilisateurs et améliorent l'expérience générales qu'offre le site. Le défilement Parallax permet la création d'une
    expérience utilisateur dynamique et intéractive pouvant potentiellement améliorer l'attrait visuel.</p>

<p>Ce module permet d'ajouter des effets parallax à votre site web sans aucun effort ni compétence supplémentaire. Il
    est possible d'ajouter à la fois des images et une vidéo en arrière-plan.</p>


