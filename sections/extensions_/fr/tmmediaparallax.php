<h5>
    Ajout d'un Média module parallax
</h5>
<ol class="index-list">
    <li>Vous devez ajouter l'id ou la classe dans le contenu de votre page HTML. Vous pouvez également utiliser une
        balise unique, cet élément ne doit pas être répété.. </li>

    <li>Créez l'image de taille requise (il est préférable de créer une image de taille supérieure à celle du bloc où
        l'image sera ajoutée). Dans le cas où vous voulez ajouter une vidéo, vous devez la préparer en 3 formats:
        .mp4,.webm et .ogv, et vous devez aussi ajouter une image d'aperçu de la même taille (.png, .jpg et .gif) </li>

    <li>
        <p>Cliquez sur le bouton
            <strong>Add container</strong>
            dans
            <strong>Add new block</strong>
            .
        </p>

        <figure class="img-polaroid">
            <img src="img/paralax-1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Sur l'écran qui apparaît, ajoutez tous les paramètres requis.</p>


