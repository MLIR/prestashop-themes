<h3>Модуль Media Parallax</h3>

<h4>Что такое эффект Параллакс (parallax)?</h4>

<p>Параллакс-скроллинг - это специальная техника скроллинга в компьютерной графике, при которой изображения на заднем
    фоне двигаются медленнее тех, что на переднем фоне, создавая, таким образом, иллюзию глубины и эффект погружения в
    двухмерной видео-игре. Веб-дизайнеры начали внедрять параллакс-скроллинг в 2011, используя HTML5 и CSS3. Веб-сайты с
    параллакс-фоном становятся все более популярной стратегией, в его пользу говорит возможность запросто добавить
    подвижности сайту. К тому же, сторонники параллакса используют такой фон, как средство привлекать пользователей и
    улучшать общее впечатление о сайте. Параллакс-скроллинг, улучшая внешний вид сайта, позволяет создавать
    динамический, интерактивный интерфейс для пользователя.</p>

<p>Данный модуль добавит эффект параллакс на сайт, не требуя каких-либо дополнительных навыков и усилий. В качестве фона
    можно добавлять картинки и видео.</p>

