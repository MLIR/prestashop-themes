<h5>
    Добавление модуля Media parallax
</h5>
<ol class="index-list">
    <li>Рекомендуется добавлять класс или индекс в содержимое HTML страницы. Вы также можете использовать уникальный
        тег, который не будет повторяться.
    </li>

    <li>Создайте изображение необходимого размера (желательно, чтобы картинка была больше размеров блока, куда будет
        добавлен эффект). Если вы хотите использовать видео, его нужно подготовить в 3 форматах: .mp4,.webm и .ogv, а
        также добавить картинку предпросмотра такого же размера (.png, .jpg и .gif)
    </li>

    <li>
        <p>Щелкните кнопку
            <strong>Add container (Добавить контейнер)</strong>
            в
            <strong>Add new block (Добавить новый блок)</strong>
            .
        </p>

        <figure class="img-polaroid">
            <img src="img/paralax-1.png" alt="">
        </figure>
    </li>
    <li>
        <p>В появившемся экране задайте все требуемые настройки. </p>

        <h6>
            <strong>Настройки:</strong>
        </h6>

        <ol class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Selector (Селектор)</dt>
                    <dd>элемент, к которому будет добавлен эффект (индекс или уникальный тег).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Parallax type (Тип параллакса)</dt>
                    <dd>тип эффекта, который будет использоваться (картинка или видео).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Media files (Медиа файлы)</dt>
                    <dd>загрузите все необходимые файлы.</dd>
                </dl>
            </li>
            <li>Нажмите кнопку
                <strong>Save (Сохранить)</strong>
                .
            </li>
        </ol>

        <figure class="img-polaroid">
            <img src="img/paralax-2.png" alt="">
        </figure>
    </li>
</ol>

