<h3>TM Homepage Products Carousel</h3>

<p>Данный модуль добавляет карусель в табы на главной странице.</p>

<h5>Установка и удаление модуля</h5>

<p>Модуль устанавливается и удаляется как любой другой модуль PrestaShop.</p>

<h5>Настройки модуля</h5>

<p>После установки модуля вы можете настраивать его на странице конфигурации модуля.</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Use carousel (Использовать карусель)</dt>
            <dd>включить/выключить карусель</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Number of elements to display (Количество отображаемых элементов)</dt>
            <dd>количество отображаемых элементов</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Items scroll (Количество прокручиваемых элементов)</dt>
            <dd>количество прокручиваемых элементов за один ход карусели</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Width (Ширина элементов)</dt>
            <dd>ширина элемента списка в карусели</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Margin (Отступ между элементами)</dt>
            <dd>отступ элемента списка в карусели</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel speed (Скорость карусели)</dt>
            <dd>скорость перехода элементов</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pause (Пауза)</dt>
            <dd>пауза между переходами (если "Auto scroll" разрешен)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto scroll (Автопролистывание)</dt>
            <dd>автоматический ход карусели</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Random (Случайный порядок)</dt>
            <dd>старт карусели со случайного элемента</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel loop (Зацикливание карусели)</dt>
            <dd>карусель возвращается на начало, если закончились элементы списка</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hide control on end (Скрыть элементы управления при отсутствии необходимости в них)</dt>
            <dd>скрыть кнопки управление (следующий/предыдущий)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pager (Пагинация)</dt>
            <dd>отображать управление с помощью пагинации</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Control (Элементы управления)</dt>
            <dd>отображать кнопки (следующий/предыдущий)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto control (Элементы управления)</dt>
            <dd>отображать кнопки (проигрывать/пауза)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto hover (Пауза при наведении курсора)</dt>
            <dd>пауза карусели при наведении на элемент списка</dd>
        </dl>
    </li>
</ul>

