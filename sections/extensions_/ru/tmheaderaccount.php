<h3>TM Header Account</h3>

<p>Данный модуль предназначен для вывода формы входа или списка доступных элементов
    личного кабинета пользователя в шапке сайта. Данный модуль позволяет осуществить
    вход без перезагрузки страницы. Реализован модуль в виде выпадающего блока.</p>
<h5>Установка и удаление модуля</h5>

<p>Модуль устанавливается и удаляется как любой другой модуль PrestaShop.</p>
<h5>Настройки модуля</h5>

<p>Модуль не имеет дополнительных настроек</p>
