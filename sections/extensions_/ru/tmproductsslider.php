<p>После установки модуля в настройках продукта появится дополнительная закладка
    (Каталог - Продукт - "любой продукт" - TM Products Slider).</p>

<p>Здесь можно отметить будет ли продукт отображаться в слайдере или нет. После
    изменений нажмите одну из кнопок для сохранения (Сохранить/Сохранить и
    остаться).</p>

<p>Модуль поддерживает режим мульти-магазина. Вы можете менять настройки одновременно
    для всех магазинов, группы магазинов или для каждого магазина отдельно.</p>

