<h3>TM Manufacturers Block</h3>

<p>Данный модуль отображает информацию о производителях доступных в магазине. Список производителей может состоять из
    картинки и названия. Список может также отображаться в виде карусели.</p>

<h5>Установка и удаление модуля</h5>

<p>Модуль устанавливается и удаляется как любой другой модуль Prestashop.</p>

<h5>Настройки модуля</h5>

<p>После установки модуля вы можете настраивать его на странице конфигураций модуля.</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Display name</dt>
            <dd>отображать название производителя</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Display image</dt>
            <dd>отображать изображение производителя</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Number of elements to display</dt>
            <dd>количество отображаемых элементов</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image Type</dt>
            <dd>тип картинки, используемой для производителя (возможность выбрать из картинок которые привязаны к
                производителям)
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use carousel</dt>
            <dd>отображать список в виде карусели</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Visible items</dt>
            <dd>количество видимых элементов</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Items scroll</dt>
            <dd>количество изменяемых элементов за один ход карусели</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Slide Width</dt>
            <dd>ширина элемента списка в карусели</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Slide Margin</dt>
            <dd>отступ элемента списка в карусели</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Auto scroll</dt>
            <dd>автоматический переход между элементами карусели</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Carousel speed</dt>
            <dd>скорость перехода элементов</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Pause</dt>
            <dd>пауза между переходами(если "Auto scroll" разрешен)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Random</dt>
            <dd>старт карусели со случайного элемента</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Carousel loop</dt>
            <dd>зацикливание карусели, карусель возвращается на начало, если закончились элементы списка</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Hide control on end</dt>
            <dd>скрыть кнопки управления (следующий/предидущий)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Pager</dt>
            <dd>отображать управление с помощью пагинации</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Control</dt>
            <dd>отображать кнопки(следующий/предидущий)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Auto control</dt>
            <dd>отображать кнопки(проигрывать/пауза)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>* Auto hover</dt>
            <dd>пауза карусели при наведении на элемент списка</dd>
        </dl>
    </li>
</ul>
<p><strong>
        <small>* - настройки действуют только если разрешена карусель</small>
    </strong></p>
<p>Модуль поддерживает режим мульти-магазина.</p>

