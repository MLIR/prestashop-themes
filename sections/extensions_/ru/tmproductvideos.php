<h3>TM Product Videos</h3>

<p>Данный модуль позволяет добавлять неограниченное количество видео к любому продукту, на любом языке и для любого
    магазина.</p>

<h5>Установка и удаление модуля</h5>

<p>Модуль устанавливается и удаляется как любой другой модуль Prestashop. Настроек никаких не имеет. Может работать в
    режиме мульти-магазина.</p>

<p>После установки модуля в настройках продукта появится дополнительная закладка (Каталог - Продукт - "любой продукт" -
    TM Product Videos)</p>

<p>Для того, чтобы добавить видео к продукту, заполните необходимые поля (поле для ввода ссылки является обязательным
    для заполнения). Модуль - мультиязычный, по-этому потребуется заполнять формы для всех языков, к которым относится
    добавляемое видео. Если поле "ссылка" не будет заполнено для какого-то из языков, то видео к этому языку добавлено
    не будет. Модуль предназначен для отображение видео с Youtube и Vimeo, по-этому использовать нужно только ссылки
    этих ресурсов. После заполнения нужных полей нажмите одну из кнопок для сохранения (Сохранить/Сохранить и
    остаться)</p>

