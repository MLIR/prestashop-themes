<h6>Примеры правильных ссылок</h6>

<p>Для коректного отображения видео ссылки должны иметь такой формат:</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Youtube</dt>
            <dd><code>https://www.youtube.com/v/K6VcTyaym90</code></dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Vimeo</dt>
            <dd><code>http://player.vimeo.com/video/66140585</code></dd>
        </dl>
    </li>
</ul>

<p>Если у продукта уже есть какие-то видео, которые относятся к данному языку, то ниже отображается список с этими
    видео.</p>

<p>Данный список полностью автономный, для сохранения/удаления или изменения позиции не нужно нажимать общую кнопку
    (Сохранить/Сохранить и остаться). Используйте кнопки которые расположены в блоках с видео. Для изменения порядка
    вывода видео просто перетягивайте видео в списке, меняя их позицию. Для включения/выключения используйте кнопку
    рядом с видео. Для сохранения изменений, введите коректные данные в нужные поля рядоим с видео и нажмите кнопку
    "обновить видео".</p>

<p>Для удаления видео со списка нажмите "Удалить видео".</p>

<p>При переключении языков будет отображаться только список, который относится к данному языку.</p>

<p>Для того, чтобы просмотреть видео в увеличеном размере, нажмите иконку "Лупа" под интересующим вас видео.</p>
