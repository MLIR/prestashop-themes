</figure>

<h4>TM Mega Menu HTML</h4>

<p>В этом пункте мы рассмотрим, как создавать блоки с пользовательским HTML в Mega Menu.</p>

<p>В модуле есть таблица с перечнем всех доступных блоков, в которую вы можете добавить новый блок, нажав
    кнопку + в правом верхнем углу таблицы.</p>

<h6>В появившейся форме заполните необходимые поля.</h6>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Enter HTML item name (Добавить название HTML элемента)</dt>
            <dd>название HTML блока, обязательно для заполнения на основном языке.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Specific class (Особый класс)</dt>
            <dd>CSS класс для блока.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>HTML content (HTML содержимое)</dt>
            <dd>HTML контент.</dd>
        </dl>
    </li>
</ul>
<p>Добавленный блок появится в списке блоков, а также в списке доступного контента при создании блоков
    меню.</p>

