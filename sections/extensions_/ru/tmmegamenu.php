<h6>Сложное меню состоит из рядов и колонок в рядах.</h6>

<p><strong>Ряд</strong> - это часть структуры Mega Menu. В нем размещены колонки меню. Удалить уже существующий ряд можно нажав
    соответствующую кнопку. Ряд удалится с содержащимся в нем контентом. Количество рядов не ограничено.</p>

<p><strong>Колонка</strong> - это часть структуры Mega Menu, которая размещена внутри рядов. (Класс) Ширина колонки не может быть
    меньше 2 и больше 12. Для корректного отображения меню суммарная ширина колонок не должна превышать 12.
    Ширина (класс) это тип колонки Bootstrap, и соответствует тем же значениям.</p>

<p>Для создания меню нажмите кнопку Add row (Добавить ряд)</p>

<figure class="img-polaroid">
    <img src="img/tmmegamenu-tabs-add-form-row.png" alt="">
</figure>

<p>В появившейся форме нажмите кнопку Add column (Добавить колонку)</p>

<figure class="img-polaroid">
    <img src="img/tmmegamenu-tabs-add-form-row-col.png" alt="">
</figure>

<p>Появится всплывающее окно, в котором нужно указать класс колонки (от 2 до 12). После этого появится сама
    колонка, в которой нужно добавить необходимые настройки,</p>

<p>здесь вы можете поменять класс колонки, добавить CSS класс, а также нужный контент из предоставленного
    списка. Добавление/удаление контента работает при помощи двойного щелчка по нужному элементу, или при помощи
    выделения нужного/нужных элементов и нажатии соответствующей кнопки.</p>

<p>Удалить блок можно нажав кнопку Remove block (Удалить блок) в нижней части блока.</p>

<p>При удалении ряда удаляются все колонки, которые он содержит.</p>

<figure class="img-polaroid">
    <img src="img/tmmegamenu-tabs-add-form-row-col-content.png" alt=""/>
</figure>

<p>Для перемещения доступного контента в выбранное меню используйте двойной щелчок или выделите нужный контент и
    нажмите соответствующую кнопку.</p>

<p>Информация неактивного меню сохраняется при переключении между типами меню и вы в любое время можете изменить
    тип меню.</p>

<figure class="img-polaroid">
