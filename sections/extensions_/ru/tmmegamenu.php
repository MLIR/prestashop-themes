<h3>TM Mega Menu</h3>

<p>Модуль предназначен для создания сложных меню. В модуле предусмотрена возможность отображения одного пункта (без
    подменю), пункта с подменю и пункта с подменю в виде многорядного и многоколоночного блока. Модуль может
    использоваться в разных позициях, но при этом будет отображать тот же контент. Доступные позиции:
    top(displayTop), left(displayLeftColumn), right(displayRightColumn), footer(displayFooter). В меню можно
    добавлять категории (с подкатегориями), CMS категории (с подкатегориями), производителей (по одному и списком),
    поставщиков (по одному и списком), магазины (если используется несколько магазинов), ссылки на любой продукт,
    блок с информацией о продукте, пользовательские ссылки не ограниченные по количеству, пользовательские HTML
    блоки не ограниченные по количеству, и баннера. Подробнее см. ниже.</p>

