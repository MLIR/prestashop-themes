<h6>Редактирование и удаление элемента</h6>

<p>Когда вы добавляете элемент, он отображается в соответственном списке.</p>

<ol class="index-list">
    <li>Язык, в который элемент был добавлен.</li>

    <li>Позиция, в которую элемент был добавлен.</li>

    <li>Кнопка Edit (Редактировать) в выпадающем списке.</li>

    <li>Кнопка Delete (Удалить).</li>
</ol>
