<h3>Модуль TM HTML Content</h3>

<p>Данный модуль позволяет отображать любое содержимое (картинку, HTML, ссылки, и т.п.) в следующих позициях:
    displayNav, home, top, topColumn, left и right (displayLeftColumn/displayRightColumn), footer. Он используется для
    расширения возможностей дизайна и добавляет рекламные/информационные блоки на сайт. Это мультиязычный модуль,
    который также можно использовать и в мультимагазине.</p>

<h5>Чтобы добавить содержимое, сделайте следующее:</h5>


<h6>Перейдите в <strong>Modules->Front Office Features->TM HTML Content
        (Модули->Функции front-office->TM HTML Content)</strong> (если он не установлен, установите его).
</h6>

<ol class="index-list">
    <li>Нажмите кнопку <strong>Add new (Добавить новый)</strong>.</li>

    <li>Список доступных языков. Вы можете просмотреть доступные элементы для всех языков, переключая вкладки
        языков. </li>

    <li>Список элементов, добавленных для определенной позиции и языка.</li>
</ol>
<figure class="img-polaroid">
    <img src="img/htmlcontent.png" alt="">
</figure>

<h6>Добавление элемента. Нажмите кнопку <strong>Add item (Добавить
        элемент)</strong> и заполните нужные настройки (обязательных полей нет).</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Language (Язык)</dt>
            <dd>выберите требуемый язык из выпадающего списка.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Title (Название)</dt>
            <dd>название элемента.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use title in front (Показывать название на сайте)</dt>
            <dd>отображать заголовок элемента на сайте.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hook (Блок)</dt>
            <dd>позиция, в которую будет добавлен элемент.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image (Изображение)</dt>
            <dd>Загрузите изображение с компьютера.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image width (Ширина изображения)</dt>
            <dd>ширина изображения на сайте.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image height (Высота изображения)</dt>
            <dd>высота изображения на сайте.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>URL</dt>
            <dd>добавление ссылки (весь блок будет обернут в ссылку).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Target blank</dt>
            <dd>открывать ссылку в новой вкладке.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>HTML</dt>
            <dd>HTML код (за исключением ссылки, добавленной в поле URL) или текст.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Save (Сохранить)</dt>
            <dd>кнопка сохранения элемента.</dd>
        </dl>
    </li>
</ol>
<p class="alert alert-info">Все элементы одного языка, добавленные в одинаковую позицию, будут отображаться в
    виде списка в панели управления и на сайте.</p>

