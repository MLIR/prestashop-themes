<h3>Media Parallax Module</h3>

<h4>Czym jest efekt parallax?</h4>

<p>Przewijanie Parallax jest specjalną techniką przewijania używaną w grafice komputerowej, w której tło obrazów jest
    poruszane przez aparat wolniej niż na pierwszym planie obrazu, tworząc wrażenie głębi w grach wideo 2D.
    Projektowanie stron internetowych zawierających przewijanie Parallax rozpoczęto w 2011 roku, przy użyciu HTML5 i
    CSS3. Strony ze środowiskiem Parallax stają się coraz bardziej popularną strategią, zwolennicy twierdzą, że jest to
    prosty sposób, aby objąć płynność sieci. Dodatkowo, zwolennicy używają tła Parallax jako narzędzie do większego
    zaangażowania użytkowników i zwiększenia satysfakcji, którą strona internetowa zapewnia. Przewijanie Parallax
    pozwala na tworzenie dynamicznych, interaktywnych doświadczeń użytkownika, który ma potencjał, aby poprawić
    atrakcyjność wizualną.</p>

<p>Moduł ten pozwala na dodawanie efektów Parallax na Twojej stronie bez żadnych dodatkowych umiejętności i wysiłku.
    Jest możliwe, aby dodać zarówno obrazy jak i wideo w tle.</p>


