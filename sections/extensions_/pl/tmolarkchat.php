
    <h3><a href="http://special.olark.com/cDqdW ">TM Olark Chat</a></h3>

    <p>Moduł wyświetlający Olark Chat na front-endzie Twojego sklepu.</p>
    <h5>Instalacja/odinstalowanie modułu</h5>

    <p>Ten moduł może być zainstalowany/odinstalowany tak jak każdy inny moduł
        PrestaShop.</p>
    <h5>Ustawienia modułu</h5>

    <p>Po zainstalowaniu, moduł ten może być skonfigurowany na stronie konfiguracyjnej
        modułu.</p>

    <p>Aby skonfigurować Olark Chat, który możesz zobaczyć na live demo szablonu, musisz
        zarejestrować się na <a href="http://www.olark.com/" target="_blank"
                                title="Olark Registration">oficjalnej stronie</a> oraz
        otrzymać ID dla swojej strony. Wprowadź swoje ID w polu "Olark site ID" i ustaw
        moduł "Live mode". Następnie zapisz ustawienia przez kliknięcie przycisku <strong>Zapisz</strong>.
    </p>

