<h4>Smart Blog</h4>

<p>Jest to główny moduł, który zawiera wiele ustawień funkcjonalności bloga. Po jego
    instalacji, zakładka menu Bloga będzie widoczna w menu głównym Twojego panelu
    administratora, gdzie możesz dodawać / usuwać / edytować posty / kategorie lub
    wybierać rodzaj i wymiary obrazów stosowanych w postach (typy i wymiary obrazów
    postów różnią się od ustawień obrazów produktów w Twoim sklepie). Aby dodać
    ustawienia frontend modułu , należy przejść do <strong>Smart Blog
        (Modules->Front Office Features->Smart Blog)</strong> i dodać wszystkie
    parametry.</p>

<h6><strong>Opis parametrów:</strong></h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Meta Title</dt>
            <dd>tytuł bloga, który pojawi się na wszystkich jego stronach.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Keyword</dt>
            <dd>słowa kluczowe, które będą stosowane przez wyszukiwarki do lepszej optymalizacji SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Description</dt>
            <dd>opis bloga, stosowany przez wyszukiwarki do lepszej optymalizacji SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Main Blog Url</dt>
            <dd>URL Twojego Bloga.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Use .html with Friendly Url</dt>
            <dd>włączanie / wyłączanie dodawania .html do URL stron bloga, przy użyciu przyjaznych adresów URL.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Number of posts per page</dt>
            <dd>liczba postów, które pojawiają się na stronie ( kategoria / archiwa strony,itp.).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Auto accepted comment</dt>
            <dd>włączanie / wyłączanie automatycznie akceptowanych komentarzy użytkowników, jeśli są włączone,
                komentarze będą dostępne bez uprawnień administratora.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Enable Captcha</dt>
            <dd>włączanie / wyłączanie Captcha pod formularzem Zostaw Komentarz na stronie produktów.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Enable Comment</dt>
            <dd>włączanie / wyłączanie możliwości pozostawiania komentarzy przez użytkowników.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Author Name</dt>
            <dd>włączanie / wyłączanie wyświetlania postów autora na stronach bloga i postów pojedynczych stron.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Post Viewed</dt>
            <dd>włączanie / wyłączanie możliwości wyświetlania, ile razy użytkownicy oglądali post. Pojawi się ona
                pod
                postem na stronach bloga oraz pojedynczych stron bloga.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Author Name Style</dt>
            <dd>zmiana formatu nazwy autora.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>AVATAR Image:</dt>
            <dd>dodanie standardowego awatara obrazu dla wszystkich użytkowników.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show No Image</dt>
            <dd>włączanie / wyłączanie obrazu. Brak obrazu jest ustawiony, jeśli nie ma obrazu dla postu /
                kategorii,
                itp.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Category</dt>
            <dd>włączanie / wyłączanie kategorii obrazu oraz opisu na stronach kategorii bloga.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Blog Page Column Setting</dt>
            <dd>ustawienie wyświetlania modułów związanych z blogiem. To często wymaga dodatkowych ustawień, dlatego
                zaleca się zainstalowanie dodatkowych modułów w ten sam sposób, w jaki zainstalowane są standardowe
                moduły PrestaShop.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Custom CSS</dt>
            <dd>możesz dodać nadprogramowe style w tym polu. Bądź ostrożny przy użyciu klas, które są już stosowane
                w
                motywie, ponieważ mogą pojawić się konflikty.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Delete Old Thumblr</dt>
            <dd>służy do regeneracji obrazów blogu w przypadku, gdy wymiary obrazów zostały zmienione lub dodano
                nowy
                typ obrazów. Do regeneracji, wybierz <strong>Yes</strong> i kliknij <strong>Regenerate</strong>.
            </dd>
        </dl>
    </li>
</ol>
