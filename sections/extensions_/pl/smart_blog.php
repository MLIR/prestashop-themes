<h4>RSS Smart Blog Feed</h4>

<p>Moduł generowania nowego źródła na Twoim Blogu.</p>

<p><strong>Posiada kilka ustawień w panelu administratora:</strong></p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Update Period</dt>
            <dd>pozwala na wyświetlanie postów z określonego przedziału czasu (godzina / dzień / tydzień / miesiąc /
                rok).
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Update Frequency</dt>
            <dd>pozwala ustawić częstotliwość aktualizacji (1 tydzień, 2 godziny, itp.).</dd>
        </dl>
    </li>
</ol>
<p>Na górze znajduje się ścieżka do pliku, w którym zostaną wygenerowane wiadomości.
    Składa się ona z URL + /modules/smartblogfeed/rss.php. </p>

