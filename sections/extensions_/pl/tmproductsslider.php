<h3>TM Products Slider</h3>

<p>The module shows up store products in the slider.</p>
<h5>Installation and deleting</h5>

<p>The module is installed and deleted like any other Prestashop module.</p>

<p>After installation you can change its settings on the module configuration page.</p>

<figure class="img-polaroid">
    <img src="img/tmproductsslider-1.png" alt="">
</figure>

<p>You can set the order of products in your slider using drag-and-drop method. You can
    also enable/disable product display in the slider.</p>

<p>You can set the width of slider, sliding effect, slider speed, pause, auto scroll,
    pause when hovering over carousel elements, slider control, next/previous buttons,
    play/pause buttons.</p>

<p>After module installation you will see the new tab in your product configuration page
    (Catalog-> Product-> select any product - TM Products Slider).</p>

<figure class="img-polaroid">
    <img src="img/tmproductsslider-2.png" alt="">
</figure>
<p>Here you can set whether the product will show up in the slider or not. After making
    the changes, click Save/Save and Stay button to save them.</p>

<p>The module supports multi-stores. You can change settings for each store, group of
    stores or all stores at a time.</p>
