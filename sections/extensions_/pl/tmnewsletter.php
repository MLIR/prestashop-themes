<h3>TM Newsletter</h3>

<p>Ten moduł wyświetla powiadomienie z propozycją subskrypcji newslettera w wyskakującym
    okienku. Sprawdzenie emaila może być zaznaczone w ustawieniach modułu, skutkujące
    emailem z potwierdzeniem. Możesz również dopasować czas pomiędzy przypomnieniami dla
    zarejestrowanych i niezarejestrowanych użytkowników. Zarejestrowani użytkownicy mogą
    całkowicie zrezygnować z przypomnień.</p>
<h5>Instalowanie/odinstalowanie modułu</h5>

<p>Ten moduł może być zainstalowany/odinstalowany tak jak każdy inny moduł
    PrestaShop.</p>
<h5>Ustawienia modułu</h5>

<p>Po zainstalowaniu modułu możesz go skonfigurować na stronie konfiguracyjnej
    modułu.</p>

<ol class="index-list">
    <li>Live mode - włącz/wyłącz moduł.</li>
    <li>Czy chciałbyś wysłać email weryfikacyjny po subskrypcji? - włącz/wyłącz email
        weryfikacyjny.
    </li>
    <li>Timeout dla gości - ustaw timeout dla wyskakującego okienka z przypomnieniem
        o subskrypcji dla gości.
    </li>
    <li>Timeout dla klientów - ustaw timeout dla wyskakującego okienka z
        przypomnieniem o subskrypcji dla klientów.
    </li>
</ol>
<p class="alert alert-warning">
    Jest koniecznym zainstalowanie i włączenie "Newsletter block", aby moduł działał prawidłowo.
</p>

