<h3>TM Header Account</h3>

<p>Ten moduł wyświetla formularz logowania i niektóre dostępne elementy konta
    użytkownika w nagłówku strony. Pozwala na zalogowanie się na Twoim koncie, zostając
    na aktywnej stronie. Moduł służy jako rozwijany blok.</p>
<h5>Instalacja oraz Usuwanie</h5>

<p>Moduł może być instalowany i usuwany jak każdy inny moduł Prestashop.</p>
<h5>Konfiguracja Modułu</h5>

<p>Moduł nie posiada żadnych dodatkowych ustawień.</p>
