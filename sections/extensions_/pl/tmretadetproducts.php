<h3>TM Related Products</h3>

<p>Ten moduł został stworzony, aby dodawać i wyświetlać powiązane produkty na stronie z
    produktem.</p>
<h5>Instalacja/odinstalowanie modułu</h5>

<p>Ten moduł może być zainstalowany/odinstalowany tak jak każdy inny moduł
    PrestaShop.</p>
<h5>Ustawienia modułu</h5>

<p>Po zainstalowaniu, moduł ten może być skonfigurowany na stronie konfiguracyjnej
    modułu.</p>

<figure class="img-polaroid">
    <img src="img/tmrelatedproducts-1.png" alt="">
</figure>
<ol class="index-list">
    <li>Wyświetl cenę produktu.</li>
    <li>Liczba produktów do wyświetlenia.</li>
</ol>
<p>Możesz dodać lub usunąć powiązane produkty dla głównego produktu w ustawieniach
    <strong>Katalog->Produkty</strong>.</p>
<ol class="index-list">
    <li>Przejdź do <strong>TM Related Products</strong>

        <figure class="img-polaroid">
            <img src="img/tmrelatedproducts-2.png" alt="">
        </figure>
    </li>
    <li>W polu wyszukiwania produktu, zacznij wpisywać nazwę produktu i wybierz go z
        wyświetlonych wariantów;
    </li>
    <li>W celu usunięcia produktu z listy, kliknij na X przed produktem;</li>
    <li>Po skończeniu konfiguracji, kliknij na przycisk <strong>Zapisz i
            Zostań</strong> lub <strong>Zapisz</strong></li>
</ol>
