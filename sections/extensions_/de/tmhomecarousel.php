<h3>TM Homepage Products Carousel</h3>

<p>Das jeweilige Modul fügt das Karussell zu Tabs auf der Hauptseite hinzu.</p>

<h5>Installation und Entfernung des Moduls</h5>

<p>Dieses Modul wird wie ein anderes PrestaShop Modul installiert und entfernt.</p>

<h5>Moduleinstellungen</h5>

<p>Nachdem Sie dieses Modul installiert haben, könenn Sie es auf der Konfigurationsseite des Moduls einstellen.</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Use carousel (Karussell verwenden)</dt>
            <dd> das Karussell aktivieren/deaktivieren</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Number of elements to display (Die Anzahl von angezeigten Elementen)</dt>
            <dd> die Anzahl von angezeigten Elementen</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Items scroll (Die Anzahl von Bildlaufelementen)</dt>
            <dd> die Anzahl von Bildlaufelementen in einem Schritt des Karussells</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Width (Breite von Elementen)</dt>
            <dd> die Breite des Elements in der Liste im Karussell</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Margin (Abstand zwischen den Elementen)</dt>
            <dd> Abstand des Elementes in der Liste im Karussell</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel speed (Geschwindigkeit des Karussells)</dt>
            <dd> die Übergangsgeschwindigkei von Elementen</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pause (Pause)</dt>
            <dd> die Pause zwischen den Übergängen (wenn "Auto scroll" erlaubt wurde)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto scroll (Automatischer Bildlauf)</dt>
            <dd> automatischer Bildlauf des Karussells</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Random (zufällige Reihenfolge)</dt>
            <dd> das Karussell wird mit dem zufälligen Element begonnen</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel loop (Karussell Loop)</dt>
            <dd> das Karussell kehrt zum Anfang, wenn es keine Elemente in der Liste gibtа</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hide control on end (Steuerelemente ausblenden, wenn sie nicht nötig sind)</dt>
            <dd> Steuerelemente ausblenden (Vor/Zurück)</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pager (Paginierung)</dt>
            <dd> die Steuerung mit Hilfe von der Paginierung anzeigen</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Control (Steuerelemente)</dt>
            <dd> Buttons (Vor/Zurück) anzeigen</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto control (Steuerelemente)</dt>
            <dd> Buttons (Wiedergabe/Pause) anzeigen</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto hover (Pause, sobald der Cursor darüber ist)</dt>
            <dd> die Pause im Karussell, sobald der Cursor über einem von den Elementen ist</dd>
        </dl>
    </li>
</ul>

