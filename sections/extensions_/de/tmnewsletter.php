<h3>TM Newsletter</h3>

<p>Das jeweilige Modul zeigt die Meldung über die Anmeldung zu einem Newsletter von
    einem Online-Shop als DropDownFenster. In diesem Modul kann man die Überprüfung von
    einer eingegebenen E-Mail-Adresse mit Hilfe von einer Bestätigungsmail aktivieren.
    Es ist auch möglich das Zeitintervall zwischen Erinnerungen für registrierte und
    nicht registrierte Benutzer einzustellen. Die registrierten Benutzer können auf
    Erinnerugen für immer verzichten.</p>
<h5>Installieren und Entfernen des Moduls</h5>

<p>Dieses Modul wird ein anderes PrestaShop Modul und installiert und entfernt.</p>
<h5>Moduleinstellungen</h5>

<p>Nach der Installation kann dieses Modul auf der Modulkonfigurationsseite eingestellt
    werden.</p>

<figure class="img-polaroid">
    <img src="img/tmnewsletter-1.png" alt="">
</figure>
<ol class="index-list">
    <li>Live mode (Live-Modus) - Modul aktivieren/deaktivieren.</li>
    <li>Would you like to send a verification email after subscription? (Eine
        Bestätigungsmail nach der Anmeldung zu einem Newsletter senden?)- die Sendung
        einer Eine Bestätigungsmail aktivieren/deaktivieren.
    </li>
    <li>Timeout for guests (Timeout der “Pop-up” Erinnerung an das Abonnement für
        Gäste). - das Timeout der “Pop-up” Erinnerung an das Abonnement für Gäste
        installieren.
    </li>
    <li>Timeout for customers (Timeout der “Pop-up” Erinnerung an das Abonnement für
        registrierte Benutzer). - das Timeout der “Pop-up” Erinnerung an das Abonnement
        für registrierte Benutzer.
    </li>
</ol>
<p class="alert alert-warning">
    Damit das Modul funktionieren kann, muss das Modul "Newsletter block" installiert und aktiviert werden.
</p>
