<h3>TM Header Account</h3>

<p>Das jeweilige Modul wurde dazu entwickelt, um das Login-Formular und die Liste von
    verfügbaren Elementen des Accounts im Webseitenkopf anzuzeigen. Dieses Modul
    ermöglicht auch dem Benutzer sich einzuloggen, ohne die Seite zu aktualisieren.
    Dieses Modul wurde als DropDownBlock entwickelt.</p>
<h5>Installation und Entfernung des Moduls</h5>

<p>Dieses Modul wird wie ein anderes PrestaShop Modul installiert und entfernt.</p>
<h5>Moduleinstellungen</h5>

<p>Dieses Modul hat keine zusätzlichen Einstellungen</p>
