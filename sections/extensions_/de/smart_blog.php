    <li>
        <dl class="inline-term">
            <dt>Show Post Viewed (Angeschaute Beiträge zeigen)</dt>
            <dd>aktiviert/deaktiviert die Darstellung von Beiträgen, die Benutzer schon gesehen haben. Dies befindet
