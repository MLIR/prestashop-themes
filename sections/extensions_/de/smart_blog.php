        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Description (Meta-Beschreibung)</dt>
            <dd>Blogbeschreibung, die von Suchmaschinen für bessere SEO-Optimierung benutzt werden</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Main Blog Url (Haupt-URL des Blogs)</dt>
            <dd>URL Ihres Blogs.</dd>
