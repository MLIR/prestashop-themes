<h3>Smart Blog</h3>

<p><strong>Smart blog -</strong> ist ein Modulsatz, der ermöglicht, den Blog im
    Online-Shop zu erstellen. Er besteht aus den folgenden Modulen:</p>
<ul class="marked-list">
    <li>Smart Blog</li>

    <li>RSS Feed Smart Blog</li>

    <li>Archiv Smart Blog </li>

    <li>Kategorien Smart Blog </li>

    <li>Neues auf der Hauptseite</li>

    <li>Neueste Kommentare Smart

    <li>Modul für
        soziale Netzwerke Smart Blog </li>

    <li>Beliebte Beiträge Smart Blog</li>

    <li>Neueste Beiträge Smart Blog</li>

    <li>Ähnliche Beiträge Smart
        Blog</li>

    <li>Suche Smart
        Blog</li>

    <li>Tags Smart
        Blog</li>
</ul>
<p>Er enthält auch eigene Blöcke, in denen Module des Blogs
    auf der Webseite dargestellt werden. Dazu können sie in Standardblöcken (nur
    Spalten) platziert werden.
</p>

<h4>Smart Blog</h4>

<p>Es ist das Hauptmodul mit verschiedenen Einstellungen für den Blog. Nachdem Sie
    es installiert haben, wird im Control Panel im Hauptmenü ein neuer Tab Blog
    dargestellt, wo Sie Beiträge/Kategorien hinzufügen/entfernen/bearbeiten oder
    Arten und Bildauflösung, die in den Beiträgen verwendet werden, (Arten und
    Bildauflösung von Beiträgen unterscheiden sich von Einstellugen für Bilder im
    Online-Shop) wählen können. Um die Einstellungen zum Moduldarstellung auf der
    Webseite hinzuzufügen, gehen Sie in <strong>Smart Blog (Modules->Front Office
        Features->Smart Blog) ((Module->Funktionen front-office->Smart
        Blog))</strong> und fügen Sie alle Optionen hinzu.</p>

