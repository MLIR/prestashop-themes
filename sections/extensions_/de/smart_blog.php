    <li>
        <dl class="inline-term">
            <dt>Show Author Name Style (Format für den Namen des Autors)</dt>
            <dd>ändert das Format für den Namen des Autors.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>AVATAR Image: (Foto:)</dt>
            <dd>fügt das Standardfoto für alle Benutzer hinzu.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show No Image (Bild "Kein Foto" zeigen)</dt>
            <dd>aktiviert/deaktiviert das Standardfoto. Dies wird für diejenigen Beiträge/Kategorien usw. benutzt,
                die
                kein Bild haben.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Category (Kategorien zeigen)</dt>
            <dd>aktiviert/deaktiviert die Darstellung von Bildern und Kategoriebeschreibungen auf den
                Kategorieseiten
                des Blogs.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Blog Page Column Setting (Spaltenformat für Blogseiten)</dt>
            <dd>Einstellung, die verbundene mit dem Blog Module darstellt. Sie enthält oft zusätzliche
                Einstellungen,
