    <li>
        <dl class="inline-term">
            <dt>Number of posts per page (Anzahl von Beiträgen pro Seite)</dt>
            <dd>Anzahl von Beiträgen, die auf der Seite dargestellt werden (Kategorieseiten/Archivs usw.).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Auto accepted comment (Automatische Hinzufügung von Kommentaren)</dt>
            <dd>aktiviert/deaktiviert automatische Hinzufügung von Kommentaren. Wenn Sie diesen Parameter
