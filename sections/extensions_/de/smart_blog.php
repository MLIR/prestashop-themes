<h4>RSS Feed Smart Blog</h4>

<p>Das Modul erzeugt einen RSS-Feed für den Blog.</p>

<p><strong>Es enthält einige Einstellungen im Control Panel:</strong></p>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Update Period (Aktualisierungsintervall)</dt>
            <dd>ermöglicht die Beiträge für eine bestimmte Zeitdauer (Stunde/Tag/Woche/Monat/Jahr) darzustellen.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Update Frequency (Aktualisierungshäufigkeit)</dt>
            <dd>ermöglicht die Aktualisierungshäufigkeit (1 Woche, 2 Stunden, usw.) festzulegen.</dd>
        </dl>
    </li>
</ol>
<p>Oben befindet sich der Pfad zur Datei, wo die Nachrichten erzeugt werden. Sie
    besteht aus URL der Webseite + /modules/smartblogfeed/rss.php.</p>

