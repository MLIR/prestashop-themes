    </li>
    <li>
        <dl class="inline-term">
            <dt>Enable Comment (Kommentare aktivieren)</dt>
            <dd>aktiviert/deaktiviert Kundenkommentare.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Author Name (Namen des Autors zeigen)</dt>
