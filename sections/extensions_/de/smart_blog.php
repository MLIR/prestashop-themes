    </li>
    <li>
        <dl class="inline-term">
            <dt>Use .html with Friendly Url (.html zum freundlichen Link hinzufügen)</dt>
            <dd>aktiviert/deaktiviert die Hinzufügung .html zum freundlichen Link.</dd>
        </dl>
