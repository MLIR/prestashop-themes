<h3>TM Related Products</h3>

<p>Given module was designed in order to add and display the related products on the
    product page.</p>

<h5>Module install/uninstall</h5>

<p>This module can be installed/uninstalled as any other PrestaShop module.</p>

<h5>Module settings</h5>

<p>After module has been installed you can configure it on the module configuration
    page.</p>

<ol class="index-list">
    <li>Display product price.</li>
    <li>Amount of products to display.</li>
</ol>

<figure class="img-polaroid">
    <img src="img/tmrelatedproducts-1.png" alt=""/>
</figure>

<p>You can add or remove related products for the main product in the product settings
    <strong>Catalog->Products</strong>.</p>

<ol class="index-list">
    <li>Navigate to <strong>TM Related Products</strong>
    </li>
    <li>In the product search field start input the title of the product, and choose
        it from the offered variants;
    </li>
    <li>In order to remove the product from the list click the X before the
        product;
    </li>
    <li>Once all the configuration have been made click <strong>Save and
            Stay</strong> or <strong>Save</strong> button
    </li>
</ol>

