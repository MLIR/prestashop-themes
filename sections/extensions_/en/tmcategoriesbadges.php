<h3>TM Categories Badges</h3>

<p>This module allows to add badges to the sidebar categories of the default Prestashop
    Categories module ("Blockcategories"). The badges can be added to the category of
    any level and in any language. The text field of the label is mandatory and should
    be filled in all languages. If this field is left blank in any language, then it
    will automatically take the text of the default language.</p>

<h5>Installation and Deleting</h5>

<p>The module is installed and deleted like any other Prestashop module.</p>

<h5>Module Configuration</h5>

<p>After installation you can change its settings on the module configuration page.</p>

<p>You can add badges to any category after selecting it from the drop down list.
    Attention! You can add only one badge for each category. Then, you can add text that
    will show up in the badge and select the style from the drop-down list (the styles
    may differ in different templates).</p>

<p>You can see the list of existing badges below. Here you can edit or delete badges
    with the help of corresponding buttons.</p>

<p>The module is multilanguage and supports the multi-stores. You can add badges for
    group of stores or all stores at once. They will show up in the list as separate
    elements.</p>

