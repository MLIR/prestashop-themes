<h3>Contenido TM HTML</h3>

<p>Este m&oacute;dulo permite visualizar cualquier contenido (im&aacute;genes, HTML, enlaces, etc.) en las siguientes
    posiciones: displayNav, home, top, topColumn, izquierda y derecha (displayLeftColumn/displayRightColumn), pie de p&aacute;gina.
    Se utiliza para ampliar las oportunidades de dise&ntilde;o y a&ntilde;adir bloques anuncio/informaci&oacute;n a la
    tienda. Es un m&oacute;dulo multilenguaje y tambi&eacute;n funciona con multiples tiendas.</p>

<h5>Para a&ntilde;adir el contenido, debe seguir los siguientes pasos:</h5>

<h6>Vaya a
    <strong>Modules->Front Office Features->TM HTML Content</strong> (si no est&aacute; instalado, inst&aacute;lelo).
</h6>


<ol class="index-list">
    <li>Haga clic en el bot&oacute;n <strong>A&ntilde;adir nuevo</strong>.</li>

    <li>La lista de idiomas disponibles. Puede ver los elementos disponibles para todos los idiomas al cambiar las pesta&ntilde;as
        de idioma. </li>

    <li>La lista de elementos a&ntilde;adidos para la posici&oacute;n e idiomas espec&iacute;fico. </li>
</ol>
<figure class="img-polaroid">
    <img src="img/htmlcontent.png" alt="">
</figure>

<h6>A&ntilde;adir el elemento. Haga clic en el bot&oacute;n <strong>A&ntilde;adir
        elemento</strong> y complete los ajustes necesarios (ninguno de los campos es obligatorio).</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Idioma</dt>
            <dd>seleccionar el idioma deseado en la lista desplegable.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>T&iacute;tulo</dt>
            <dd>t&iacute;tulo del elemento.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Usar t&iacute;tulo delante</dt>
            <dd>visualizar el t&iacute;tulo del elemento en el frontend (el t&iacute;tulo se mostrar&aacute; delante del
                elemento).
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Ancla</dt>
            <dd>posici&oacute;n en la que se a&ntilde;ade el elemento.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Imagen</dt>
            <dd>cargar la imagen desde su ordenador.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Ancho de imagen</dt>
            <dd>ancho de la imagen que se muestra en el frontend.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Alto de imagen</dt>
            <dd>alto de la imagen que se muestra en el frontend.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>URL</dt>
            <dd>a&ntilde;adir enlace (el bloque entero ser&aacute; envuelto en el enlace).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Destino en blanco</dt>
            <dd>abrir el enlace en una nueva pesta&ntilde;a.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>HTML</dt>
