<h3>TM Conta no Cabeçalho (Header Account)</h3>

<p>Esse módulo mostra o formulário de login e alguns elementos da conta de usuário no
    cabeçalho do site. Permite entrar em sua conta dentro da página atual. Esse módulo
    serve como um bloco drop-down.</p>
<h5>Instalação e Remoção</h5>

<p>O módulo pode ser instalado e apagado como qualquer outro módulo do PrestaShop</p>
<h5>Configuração do módulo</h5>

<p>O módulo não tem outras configurações.</p>
