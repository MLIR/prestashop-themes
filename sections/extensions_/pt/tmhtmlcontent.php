<h3>TM HTML Content</h3>

<p>Esse módulo permite incluir qualquer conteúdo (imagem, HTML, links, etc.) nas seguintes posições: displayNav, home,
    top, topColumn, left e right (displayLeftColumn/displayRightColumn), footer. É usado para expandir as oportunidades
    de design e incluir anúncios ou blocos de informação para sua loja. É um módulo com suporte para vários idiomas que
    pode ser incluído em um site com diversas lojas. </p>

<h5>Para incluir o conteúdo, você deve seguir os passos abaixo:</h5>

<h6>Vá para <strong>Módulos (Modules)->Fachada(Front Office Features)->TM HTML
        Content</strong> (se não estiver instalado, faça-o).</h6>


<ol class="index-list">
    <li>Clique em <strong>Adicionar novo (Add new)</strong>.</li>

    <li>A lista de idiomas disponíveis. Você pode verificar os elementos disponíveis para todos os idiomas mudando as
        abas de idioma. </li>

    <li>A lista de elementos adicionados para uma posição e idioma específicos. </li>
</ol>
<figure class="img-polaroid">
    <img src="img/htmlcontent.png" alt="">
</figure>

<h6>Adicionar elemento. Clique em
    <strong>Adicionar item(Add item)</strong> e preencha as configurações necessários (nenhum dos campos precisam ser
    preenchidos).
</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Language</dt>
            <dd>escolha o idioma necessário da lista.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Title</dt>
            <dd>título do elemento.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use title in front</dt>
            <dd>mostrar o título do elemento no site (será incluído antes do elemento).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hook</dt>
            <dd>posição onde o elemento será adicionado.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image</dt>
            <dd>envio de uma imagem do seu computador.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image width</dt>
            <dd>largura da imagem.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image height</dt>
            <dd>altura da imagem.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>URL</dt>
            <dd>adicione um link (o bloco inteiro será envolvido por este link).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Target blank</dt>
            <dd>abrir link numa aba nova.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>HTML</dt>
            <dd>Código HTML (exceto o link se você tiver preenchido o campo de URL acima) ou texto.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Save</dt>
            <dd>clique para salvar o elemento.</dd>
        </dl>
    </li>
</ol>
