    <li>
        <dl class="inline-term">
            <dt>Enable Comment</dt>
            <dd>ativa/desativa comentários de usuários.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Author Name</dt>
            <dd>ativa/desativa mostrar o autor dos posts em páginas do blog e páginas avulsas.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Post Viewed</dt>
            <dd>ativa/desativa mostrador de quanos usuários visualizaram o conteúdo. Esse número aparece nas páginas
