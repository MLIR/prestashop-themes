            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Delete Old Thumblr</dt>
            <dd>é usado para regerar imagens do blog se você alterar as dimensões delas em opções anteriores ou se
                um
                formato novo de imagem for adicionado. Para regerar, escolha <strong>Sim (Yes)</strong> e clique em
                <strong>Regerar (Regenerate)</strong>.
            </dd>
        </dl>
    </li>
</ol>
