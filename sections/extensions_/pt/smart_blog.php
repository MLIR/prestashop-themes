</p>

<h4>Smart Blog</h4>

<p>Esse é o módulo principal que contém muitas configurações para a funcionalidade
    do blog. Depois de instalado, a aba de menu do Blog vai aparecer no menu
    principal de administração, onde você pode incluir/remover/alterar os
    posts/categorias ou selecionar o tipo de imagens e seus tamanhos usados nos
    posts (tipos e dimensões das imagens de post são diferentes das imagens de
    produto de sua loja). Para adicionar configurações de aparência ao módulo,
    navegue até <strong>Smart Blog (Modules->Front Office Features->Smart
        Blog)</strong> e adicione todos os parâmetros.</p>

<h6><strong>Descrição dos parâmetros:</strong></h6>

<ol class="index-list">
    <li>
