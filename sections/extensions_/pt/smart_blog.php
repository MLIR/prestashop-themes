<h4>RSS Smart Blog Feed</h4>

<p>O módulo que gera feeds de notícias do seu blog.</p>

<p><strong>Tem diversas configurações no painel do admin:</strong></p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Update Period</dt>
            <dd>permite mostrar posts de períodos específicos (hora/dia/semana/mês/ano).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Update Frequency</dt>
            <dd>permite escolher a frequência de atualização do Feed (1 semana, 2 horas, etc.).</dd>
        </dl>
    </li>
</ol>
<p>No topo tem um caminho de arquivo onde esses arquivos de notícias serão gerados.
    Ele consiste de endereço do site + /modules/smartblogfeed/rss.php.</p>

