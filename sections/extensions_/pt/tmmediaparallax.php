<h5>
    Adicionando o Módulo Media parallax
</h5>
<ol class="index-list">
    <li>É preciso adicionar o id ou a classe no conteúdo de sua página HTML. Você também pode usar uma tag única, esse
        elemento não deve ser repetido. </li>

    <li>Crie a imagem do tamanho necessário(de preferência criar uma imagem maior do que o bloco onde vai entrar o
        efeito). No caso de você quiser adicionar o vídeo, você vai preparar 3 formatos: .mp4,.webm e .ogv, e também
        adicionar a imagem de amostra do mesmo tamanho (.png, .jpg e .gif) </li>

    <li>
        <p>Clique em
            <strong>Adicionar contêiner (Add container)</strong>
            e depois em
            <strong>Adicionar novo bloco (Add new block)</strong>
            .
        </p>

        <figure class="img-polaroid">
            <img src="img/paralax-1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Na tela que apareceu, inclua todas as configurações necessárias.</p>


