<h3>Media Parallax Module</h3>

<h4>O que é o efeito parallax?</h4>

<p>O Parallax é uma técnica de rolagem especial em gráficos de computador, onde as imagens de fundo se movem mais
    devagar do que as imagens na frente, criando a ilusão de profunidade em um video game 2D e adicionando imersão. Web
    designers começaram a incorporar o parallax em 2011, usando HTML5 e CSS3. Sites com parallax estão se tornando uma
    estratégia popular, já que é uma forma simples de abraçar a fluidez da web. Além disso, usa-se o fundo em parallax
    como uma ferramenta para engajar usuários e aumentar a experiência do usuário no site. Rolagem em parallax permite a
    criação de experiências de usuários dinâmicas e interativas com o potencial de um apelo visual.</p>

<p>Esse módulo adiciona um efeito de parallax sem exigir habilidades ou esforços seus. É possível adicionar tanto
    imagens quanto vídeos.</p>


