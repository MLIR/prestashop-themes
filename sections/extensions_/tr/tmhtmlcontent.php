<h6>Unsur eklemek ve silmek</h6>

<p>Unsur eklediğinizde, hemen ilgili listede yerini alır.</p>


<ol class="index-list">
    <li>Unsurların eklendiği lisan.</li>

    <li>Unsurların eklendiği pozisyon.</li>

    <li>Açılır listedeki düzenleme düğmesi.</li>

    <li>Silme düğmesi.</li>
</ol>

<figure class="img-polaroid">
    <img src="img/htmlcontent-2.png" alt="">
</figure>
