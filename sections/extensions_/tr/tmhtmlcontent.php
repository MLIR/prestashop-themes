<h3>TM HTML Content</h3>

<p>Bu modül, displayNav, home, top, topColumn, left and right (displayLeftColumn/displayRightColumn), footer gibi
    alanlarda görsel, HTML, bağlantı vs gibi içerikler göstermenizi sağlar. Standart tasarımı geliştirmek ve mağazanıza
    reklam/bilgi blokları eklemeniz için kullanılır. Birden fazla lisan destekleyen bir modüldür.</p>

<h5>İçerik eklemek için aşağıdaki adımları takip edebilirsiniz:</h5>

<h6>Öncelikle
    <strong>Modules->Front Office Features->TM HTML Content</strong> alanına girin. (eğer kurulu değilse kurun).
</h6>


<ol class="index-list">
    <li>Ardından <strong>Add new</strong> (Yeni ekle) ye tıklayın.</li>

    <li>Lisan sekmelerinden kullanılabilir olan tüm lisanlar listenecektir.</li>

    <li>Belirtilen pozisyon ve lisan için eklenen unsurlar listelenecektir.</li>
</ol>
<figure class="img-polaroid">
    <img src="img/htmlcontent.png" alt="">
</figure>
<h6>Unsur eklemek için
    <strong>Add item</strong> e tıklayın ve gerekli alanları doldurun (alanları doldurmanız mecburi değildir).
</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Language</dt>
            <dd>açılır listeden lisan seçin.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Title</dt>
            <dd>unsur başlığı.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Use title in front</dt>
            <dd>unsur başlığını ön tarafta göster.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hook</dt>
            <dd>unsurun ekleneceği pozisyon.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image</dt>
            <dd>bilgisayarınızdan görsel yükleyin.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image width</dt>
            <dd>görsel genişliğini belirtin.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Image height</dt>
            <dd>görsel uzunluğunu belirtin.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>URL</dt>
            <dd>bağlantı adresini ekleyin (tüm blok bu adrese bağlantılanacaktır).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Target blank</dt>
            <dd>bağlantıyı yeni sekmede aç.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>HTML</dt>
            <dd>HTML kodu (URL alanına bağlantı adresini yazdıysanız buraya yazmayın) veya yazı.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Save</dt>
            <dd>değişiklikleri kaydedin.</dd>
        </dl>
    </li>
</ol>
<p class="alert alert-info">Aynı pozisyona eklenmiş olan bir dilin tüm unsurları arka ve ön tarafta listelenecektir.</p>
