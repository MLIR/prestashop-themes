<h3>TM Homepage Products Carousel</h3>

<p>Questo modulo permette di aggiungere un carosello alle schede della pagina principale.</p>

<h5>Installazione e disinstallazione</h5>

<p>Il modulo va installato ed eliminato come tutti gli altri moduli PrestaShop.</p>

<h5>Configurazione Modulo</h5>

<p>Una volta installato il modulo, è possibile configurarne le impostazioni sulla pagina di configurazione.</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Use carousel</dt>
            <dd>per abilitare/disabilitare il carosello
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Number of elements to display</dt>
            <dd>numero di elementi da visualizzare
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Items scroll</dt>
            <dd>il numero di elementi da scorrere
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Width</dt>
            <dd>ampiezza di un elemento della lista carosello
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Item Margin</dt>
            <dd>margine tra gli elementi della lista carosello
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel speed</dt>
            <dd>durata della transizione tra gli elementi
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pause</dt>
            <dd>la quantità di tempo tra una transizione automatica e l'altra (se l'opzione "Auto scroll" è abilitata)
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto scroll</dt>
            <dd>abilita la funzione autoscorrimento del carosello
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Random</dt>
            <dd>avvia il carosello a partire da un elemento a caso
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Carousel loop</dt>
            <dd>per far tornare il carosello all'inizio una volta scorsi tutti gli elementi</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Hide control on end</dt>
            <dd>per nascondere i pulsanti di navigazione (next/previous) sull'ultima slide</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pager</dt>
            <dd>mostra impaginazione
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Control</dt>
            <dd>mostra pulsanti next/previous
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto control</dt>
            <dd>mostra pulsanti play/pause
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Auto hover</dt>
            <dd>per mettere il carosello in pausa quando il mouse viene puntato su un elemento del carosello</dd>
        </dl>
    </li>
</ul>

