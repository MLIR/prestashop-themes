<h4>RSS Smart Blog Feed</h4>

<p>Il modulo per generare il news feed per il tuo Blog.</p>

<p><strong>Il modulo ha diverse impostazioni nel pannello di
        amministrazione:</strong></p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Update Period</dt>
            <dd>permette di visualizzare post di periodi specifici (ora/giorno/settimana/mese/anno).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Update Frequency</dt>
            <dd>permette di impostare la frequenza degli aggiornamenti (1 settimana, 2 ore, ecc.).</dd>
        </dl>
    </li>
</ol>
<p>In alto c'è un percorso file in cui viene generato il feed. È composto di
    indirizzo sito web + /modules/smartblogfeed/rss.php.</p>

