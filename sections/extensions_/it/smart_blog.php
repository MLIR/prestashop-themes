<h4>Smart Blog</h4>

<p>È il modulo principale, contenente diverse impostazioni sulle funzionalità del
    blog. Dopo l'installazione la scheda menù del Blog apparirà nel menù principale
    del tuo pannello di amministrazione, dove potrai aggiungere/rimuovere i post o
    categorie, o selezionare il tipo di immagine e le dimensioni da usare nel post
    (tipi e dimensioni delle immagini del post sono diverse dalle impostazioni delle
    immagini per lo store). Per aggiungere le impostazioni frontend del modulo, vai
    su <strong>Smart Blog (Modules->Front Office Features->Smart Blog)</strong> e
    aggiungi tutti i parametri.</p>

<h6><strong>Descrizione dei parametri:</strong></h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Meta Title</dt>
            <dd>il titolo del tuo blog, che apparirà su tutte le pagine del blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Keyword</dt>
            <dd>parole chiave che saranno usate dai motori di ricerca per l'ottimizzazione del SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Description</dt>
            <dd>descrizione del tuo blog utilizzato dai motori di ricerca per l'ottimizzazione del SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Main Blog Url</dt>
            <dd>URL del tuo Blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Use .html with Friendly Url</dt>
            <dd>abilitare/disabilitare l'aggiunta di .html alle URL delle pagine del blog, usando le "Friendly Url".
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Number of posts per page</dt>
            <dd>numero di post da mostrare su una pagina (categoria/pagina archivi, ecc.).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Auto accepted comment</dt>
            <dd>abilitare/disabilitare commenti degli utenti accettati automaticamente. Se abilitato, i commenti
                saranno disponibili senza moderazione dell'amministratore.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Enable Captcha</dt>
            <dd>abilitare/disabilitare il Captcha nel modulo per lasciare commenti su pagine dei prodotti.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Enable Comment</dt>
            <dd>abilitare/disabilitare lasciare commenti da parte degli utenti.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Author Name</dt>
            <dd>abilitare/disabilitare mostrare l'autore del post sulle pagine del blog</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Post Viewed</dt>
            <dd>abilitare/disabilitare il numero di visualizzazione del post. Apparirà sotto il post sulle pagine
                del
                blog.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Author Name Style</dt>
            <dd>cambiare il formato del nome dell'autore.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>AVATAR Image:</dt>
            <dd>aggiungere l'immagine avatar predefinita per tutti gli utenti.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show No Image</dt>
            <dd>abilitare/disabilitare l'immagine. Si imposta No Image se non ci sono immagini per la
                categoria/post,
                etc.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Category</dt>
            <dd>abilitare/disabilitare immagine e descrizione della categoria sulla pagina delle categorie del blog.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Blog Page Column Setting</dt>
            <dd>impostare la visualizzazione dei moduli relativi al Blog. Questo richiede spesso impostazioni
                aggiuntive, per cui si consiglia di installare i moduli aggiuntivi nella stessa maniera in cui si
                intallano i moduli PrestaShop standard.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Custom CSS</dt>
            <dd>in questo campo si possono aggiungere stili aggiuntivi. Fai attenzione a utilizzare le classe già
                usate
                nel tema, altrimenti potrebbero verificarsi dei conflitti.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Delete Old Thumblr</dt>
            <dd>si usa per rigenerar le immagini blog nel caso in cui le dimensioni delle immagini venissero
                modificate
                o se si aggiungono nuovi tipi di immagine. Per rigenerare, seleziona <strong>Yes</strong> e clicca
                <strong>Regenerate</strong>.
            </dd>
        </dl>
    </li>
</ol>
<p class="alert alert-danger"><strong>SOLO ADESSO</strong> si possono installare altri moduli relativi al blog. La
    procedura di rimozione è l'opposto: <strong>PRIMA
        DEVI ELIMINARE TUTTI I MODULI RELATIVI</strong> e solo dopo elimina il modulo Smart Blog. </p>

