<h3>TM Related Products</h3>

<p>Questo modulo è stato realizzato per aggiungere e visualizzare prodotti simili in una
    pagina prodotto.</p>
<h5>Installazione/disinstallazione del modulo</h5>

<p>Questo modulo va installato o disinstallato come tutti gli altri moduli
    PrestaShop.</p>
<h5>Impostazioni modulo</h5>

<p>Dopo che il modulo è stato installato, sarà possibile configurarlo nella apposita
    pagina di configurazione del modulo.</p>

<ol class="index-list">
    <li>Mostra prezzo prodotto.</li>
    <li>Quantità di prodotti da visualizzare.</li>
</ol>
<figure class="img-polaroid">
    <img src="img/tmrelatedproducts-1.png" alt="">
</figure>
<p>Per aggiungere o rimuovere i prodotti simili al prodotto principale, vai sulle
    impostazioni prodotto in <strong>Catalog->Products</strong>.</p>
<ol class="index-list">
    <li>Vai su <strong>TM Related Products</strong>
    </li>
    <li>Nel campo di ricerca prodotti, inserisci il titolo del prodotto e scegli tra
        le variabili possibili;
    </li>
    <li>Per rimuovere il prodotto dalla lista, clicca la X prima del prodotto;</li>
    <li>Una volta fatte tutte le configurazioni, clicca sul pulsante <strong>Save and
            Stay</strong> o sul pulsante <strong>Save</strong></li>
</ol>
