
    <h3>TM Header Account</h3>

    <p>Questo modulo permette di inserire il modulo per il login e alcuni elementi
        disponibili dell'account utente sull'intestazione del sito. Permette di accedere al
        proprio account restando sulla pagina attiva. Il modulo opera come blocco a
        discesa.</p>
    <h5>Installazione e disintallazione</h5>

    <p>Il modulo va installato ed eliminato come tutti gli altri moduli PrestaShop.</p>
    <h5>Configurazione Modulo</h5>

    <p>Il modulo non ha impostazioni aggiuntive.</p>
