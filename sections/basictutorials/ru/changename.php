<h3>Как изменить название магазина</h3>

<p>Чтобы изменить название магазина, откройте панель управления PrestaShop и перейдите в
    <strong>Preferences > Store Contacts (Настройки > Контактные данные
        магазина)</strong>, раздел <strong>Contact details (Контактные данные)</strong>.
</p>

<p>В поле <strong>Shop name (Название магазина)</strong> введите название магазина и
    нажмите кнопку <strong>Save (Сохранить)</strong> для сохранения изменений.</p>

