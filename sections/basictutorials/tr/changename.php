<h3>Mağaza ismini değiştirmek</h3>

<p>Mağaza ismini değiştirmek için PrestaShop yönetici panelinden <strong>Preferences >
        Store Contacts</strong> (Ayarlar > Mağaza Yetkilileri) kısmına girin ve oradan
    <strong>Contact details</strong> (Yetkili detayları) na tıklayın.</p>

<p><strong>Mağaza ismi</strong> alanına istediğiniz ismi yazın ve
    <strong>Kaydet</strong> e tıklayın.</p>

