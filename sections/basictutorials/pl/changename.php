<h3>Jak zmienić nazwę sklepu</h3>

<p>Aby zmienić tytuł sklepu, otwórz panel administratora PrestaShop i przejdź do
    rozdziału <strong>Preferences > Store Contacts</strong>, the <strong>Contact
        details</strong>.</p>

<p>W polu <strong>Shop name</strong>, wpisz Twoją nazwę sklepu i kliknij przycisk
    <strong>Save</strong>, aby zachować zmiany.</p>

