<h3>How to change store name</h3>

<p>In order to change your store title, open the PrestaShop admin panel and go to
    <strong>Preferences > Store Contacts</strong>, the <strong>Contact details</strong>
    section.</p>

<p>In the <strong>Shop name</strong> field, type in your store name and click the
    <strong>Save</strong> button to save your changes.</p>

