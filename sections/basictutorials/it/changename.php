<h3>Come cambiare il nome del negozio</h3>

<p>Per cambiare il titolo del tuo negozio apri il pannello di amministrazione di
    PrestaShop e vai su <strong>Preferences > Store Contacts</strong>, nella sezione
    <strong>Contact details</strong>.</p>

<p>Nel campo <strong>Shop name</strong> digita il nome del tuo negozio e clicca su
    <strong>Save</strong> per salvare le tue modifiche.</p>

