<h3>Como trocar o nome da loja</h3>

<p>Para trocar o título da sua loja, abra o painel e vá para <strong>Preferências
        (Preferences) > Contatos da Loja (Store Contacts)</strong>, na seção <strong>Detalhes
        de contato (Contact details)</strong>.</p>

<p>No campo <strong>Nome da loja (Shop name)</strong>, digite o nome de sua loja e
    clique em <strong>Salvar (Save)</strong> para alterá-lo.</p>

