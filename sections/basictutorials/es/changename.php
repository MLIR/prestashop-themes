<h3>C&oacute;mo cambiar el nombre de la tienda</h3>

<p>Para cambiar el t&iacute;tulo de su tienda, abra el panel de administraci&oacute;n de
    PrestaShop y vaya a <strong>Preferencias > Contactos de la tienda</strong>, y a la
    secci&oacute;n <strong>Datos de contacto</strong> .</p>

<p>En el ampo <strong>Nombre de la tienda</strong>, escriba su nombre de la tienda y
    haga clic en el bot&oacute;n <strong>Guardar</strong> para guardar los cambios.</p>

