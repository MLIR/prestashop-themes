<h3>Wie ändert man den Shopnamen</h3>

<p>Um den Shopnamen zu ändern, öffnen Sie das Control Panel PrestaShop und gehen Sie in
    <strong>Preferences > Store Contacts (Einstellungen > Kontaktdaten des
        Online-Shops)</strong>, Bereich <strong>Contact details (Kontaktdaten)</strong>.
</p>

<p>Im Feld <strong>Shop name (Shopname)</strong> geben Sie den Namen für den Online-Shop
    ein und drücken Sie den Button <strong>Save (Speichern)</strong> um die Änderungen
    zu speichern.</p>

