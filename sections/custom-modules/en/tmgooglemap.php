<h3>TM Google Map</h3>
<p>This module allows to add Google map with your stores' locations and their short description on your front page. The map will show the addresses of all stores
    related to your online shop. You can edit your stores' details in Preferences -> Store Contacts tab.</p>
<h5>Installation and Deleting</h5>
<p>The module is installed and deleted like any other Prestashop module.</p>
<h5>Module configuration</h5>
<p>After installation you can change its settings on the module configuration page.</p>
<p>You can configure the style and elements of your map.</p>
<ul class="index-list">
    <li>Select the map style in the first field (all styles are taken from <a
        target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>). You can also add
    the style from this website. You'll find out how to add them at the end of this
    instruction.</li>
    <li>Select the view type in the second field (satellite view or street view).</li>
    <li>You can also select the zoom ratio for your map from 1 to 17.</li>
    <li>Zoom option while scrolling on the map area.</li>
    <li>You can disable the map control and ability to view the streets.</li>
    <li>The module supports multi-stores. You can change the map style for each
    store.</li>
</ul>
