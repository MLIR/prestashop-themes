<h5>Module Settings</h5>
<ul class="index-list">
    <li>Pages - pages, where the module is supposed to work.</li>
    <li>Auto load - if this option is enabled, products will automatically load on scroll. Otherwise, if the option is disabled, you'll see the 'Show more' button and the products will load, when you press this button.</li>
    <li>Offset - this option is displayed when 'Auto load' is enabled. It allows to determine the offset before the products loading area (or after it - if the value is negative).</li>
    <li>Pagination - this setting is displayed when 'Auto load' is disabled. It allows to add pagination to the products page.</li>
    <li>Display 'Show all' button - this option is available, only if 'Auto load' is disabled. After clicking this button all products show up.</li>
    <li>Preloader - in this field you can choose a preloader image that will be displayed while products are loading.</li>
</ul>