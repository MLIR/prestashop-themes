<h5>Adding Media parallax module</h5>
<ul class="index-list">
    <li>
        <p>It is required to add the id or class in the content of your HTML page. You can also use a unique tag, this
            element should not be repeated.</p>
    </li>

    <li>
        <p>Create the image of the required size(it is desirable to create the image of larger size than the block where
            the effect will be added). In case you want to add the video, you will have to prepare it in 3 formats:
            .mp4, .webm and .ogv, and also to add the preview image of the same size(.png, .jpg and .gif)</p>
    </li>
    <li>
        <p>Click <strong>'Add container'</strong> button in the <strong>'Add new block'</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/parallax-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>In the appeared screen add all the required settings.</p>
        <h6>
            <strong>Settings:</strong>
        </h6>
        <ul class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Selector</dt>
                    <dd>element where the effect will be added (id or unique tag).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Parallax type</dt>
                    <dd>type of effect that will be used (image or video).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Media files</dt>
                    <dd>upload all the required files.</dd>
                </dl>
            </li>
            <li>
                <p>Click <strong>'Save'</strong> button.</p>
            </li>
        </ul>
        <figure class="img-polaroid">
            <img src="img/parallax-2.png" alt=""/>
        </figure>
    </li>
</ul>
