<h3>Media Parallax Module</h3>
<h4>What is parallax effect?</h4>
<p>Parallax scrolling is a special scrolling technique in computer graphics, wherein background images move by the
    camera slower than foreground images, creating an illusion of depth in a 2D video game and adding to the immersion.
    Web designers began incorporating parallax scrolling in 2011, using HTML5 and CSS3. Websites with parallax
    backgrounds are becoming an increasingly popular strategy, as advocates argue it is a simple way to embrace the
    fluidity of the Web. Additionally, proponents use parallax backgrounds as a tool to better engage users and improve
    the overall experience that a website provides. Parallax scrolling allows for the creation of dynamic, interactive
    user experience that has the potential to improve visual appeal.</p>
<p>This module allows to add parallax effects to your website without any additional skills and effort. It is possible
    to add both images and background video.</p>
