<h3>TM Homepage Products Carousel</h3>
<p>The module adds a carousel to the main page tabs.</p>
<h5>Installation and Deleting</h5>
<p>The module can be installed and deleted like any other PrestaShop module.</p>
<h5>Module configuration</h5>
<p>The installed module settings can be configured on the module configuration page.</p>
