<h3>TM Header Account</h3>
<p>This module displays the login form and some available user account elements in the
    site header. It allows to log into your account staying on the active page. The
    module serves as a drop-down block.</p>
<h5>Installation and Deleting</h5>
<p>The module can be installed and deleted like any other PrestaShop module.</p>
<h5>Module configuration</h5>
<p>The module does not have any additional settings.</p>
