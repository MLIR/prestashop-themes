<h3>TM Category Products 0.1.3</h3>
<p>This module displays category products in displayHome and displayHomeTabContent hooks. The module allows displaying
    single products, products from a category or all products. Moreover, the module comes with a carousel that works,
    when the module occupies the displayHome position.</p>
<h4>Module Installation</h4>
<p>To install the module navigate to Admin panel -> Modules and Services -> Modules and Services and find 'TM Category
    Products' module in Front Office Features, then click the 'Install' button (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-1.png" alt=""/>
</figure>
<h4>Module Configuration</h4>
<ul class="index-list">
    <li>
        <p>When the module has been successfully installed, the button 'Configure' (1) will appear.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>On the main module configuration page there are lists of tabs and blocks (1) (where you can see the created
            items) and two buttons - 'Settings' (2) and 'Add Item' (3).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Click the 'Settings' button and choose module's options according to your needs.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><p><strong>Visible items</strong> - the number of visible items.</p></li>
            <li><p><strong>Items scroll</strong> - the number of items changed with one carousel scroll.</p></li>
            <li><p><strong>Slide Width</strong> - the carousel list item width.</p></li>
            <li><p><strong>Slide Margin</strong> - the carousel list item margin.</p></li>
            <li><p><strong>Auto scroll</strong> - auto scroll of carousel items.</p></li>
            <li><p><strong>Carousel speed</strong> - speed of changing items.</p></li>
            <li><p><strong>Pause</strong> - pause between scrolls (if "Auto scroll" is enabled).</p></li>
            <li><p><strong>Random</strong> - the carousel starts with a random item.</p></li>
            <li><p><strong>Carousel loop</strong> - the loop: when all the carousel items have been displayed, carousel
                    starts from the beginning.</p></li>
            <li><p><strong>Hide control at the end</strong> - hide control buttons (Next/Prev).</p></li>
            <li><p><strong>Pager</strong> - display pagination.</p></li>
            <li><p><strong>Control</strong> - display controls (Next/Prev).</p></li>
            <li><p><strong>Auto control</strong> - display controls (Play/Pause).</p></li>
            <li><p><strong>Auto hover</strong> - carousel pauses, if the user hovers a list item.</p></li>
        </ul>
    </li>
</ul>
<h4>Adding new tab/block</h4>
<p>Tab is a block that displays products in displayHomeTabContent position.</p>
<p>This block allows selecting products in displayHome position.</p>
<ul>
    <li>
        <p>To add new tab or block click the 'Add Item' button.</p>
        <p>You'll see the screen where you can choose the following options:</p>
        <ul class="marked-list">
            <li><p><strong>Select category</strong> - choosing a category, products of which will
                    be displayed.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-5.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Number of products to display</strong> - number of items to be displayed.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-6.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Mode</strong> - tab/block mode.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-7.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Status</strong> - allows enabling/disabling the tab or block.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-8.png" alt=""/>
                </figure>
            </li>
        </ul>
        <p>After choosing the parameters click 'Save' button. The tab/block will be saved and will appear in the list of
            tabs/blocks.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-9.png" alt=""/>
        </figure>
    </li>
</ul>
