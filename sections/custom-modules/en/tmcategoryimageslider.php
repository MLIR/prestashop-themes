<h3>TM Category Image Slider</h3>
<p>Module allows creating multilingual slides for any category of the store. Slider will be displayed on the category
    pages.</p>
<h4>Installation and Removal</h4>
<p>The installation procedure is standard, module can be installed and deleted as a regular PrestaShop module.</p>
<h4>Module settings</h4>
<p>After the module installation, you can set or change the needed options on the module configuration page.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-1.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Mode</strong> - type of animation (fade, vertical, horizontal).</li>
    <li><strong>Speed</strong> - animation speed.</li>
    <li><strong>Width</strong> - slides width (for the correct view, it is recommended using the actual image width).
    </li>
    <li><strong>Height</strong> - slides height (for the correct view, it is recommended using the actual image height).
    </li>
    <li><strong>Auto</strong> - autoplay when page is opened.</li>
    <li><strong>Pause</strong> - time before transition to the next slide.</li>
    <li><strong>Auto Hover</strong> - pause on hover.</li>
    <li><strong>Controls</strong> - "Next"/"Previous" buttons.</li>
    <li><strong>Next Text</strong> - "Next" button text.</li>
    <li><strong>Prev Text</strong> -"Previous" button text.</li>
    <li><strong>Auto Controls</strong> - "Play"/"Pause" buttons.</li>
    <li><strong>Pager</strong> - pagination.</li>
</ul>
<h5>Adding/removing/editing slides</h5>
<p>After the module installation, you will see the new field 'Slides' in category settings.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-2.png" alt=""/>
</figure>
<p>To add a slide, click on the 'Add new slide' button. Fill in the needed fields in the form.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-3.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Select a file</strong> - select your file. You can upload different images for each language. In case
        there is no image selected for the specific language, images set for default language will load.
    </li>
    <li><strong>Enter Slide title</strong> - type in the slide title.</li>
    <li><strong>Enter Slide Url</strong> - provide the slide URL.</li>
    <li><strong>Enter Slide caption</strong> - type in the slide caption.</li>
    <li><strong>Open in new window</strong> - open in new window.</li>
    <li><strong>Status</strong> - enable/disable slide.</li>
</ul>
<p>When the slide is saved, it will appear in a category slides list.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-4.png" alt=""/>
</figure>
<p>In order to make changes to slide, click the <strong>'Edit'</strong> button.</p>
<p>In order to delete the slide, click on <strong>'Delete'</strong> below the <strong>'Edit'</strong> button.</p>
<div class="alert alert-info">
    <span>Slides can only be added to saved categories.</span>
</div>