<h2 class="item6">Politique de Cookies</h2>

<p>Les cookies sont de petits fichiers texte que les sites mettent dans les ordinateurs des visiteurs. Ils sont
    généralement utilisés pour identifier un utilisateur et lui proposer une meilleure expérience. Les cookies sont un
    élément crucial pour plusieurs fonctions essentielles des sites web.</p>

<p>Par exemple, ce sont généralement les cookies qui permettent aux boutiques en ligne de se souvenir des articles que
    vous avez dans votre panier d'achat. Ils permettent aussi de vous garder connecté à un site, ou de fournir des
    statistiques et des informations riches aux propriétaires de sites web.</p>

<p>Si vous possédez un site web, il utilise à coup sûr des cookies. Leur fonction la plus répandue est le suivi du
    nombre et du comportement des visiteurs à l'aide d'outils comme Google Analytics. Ils peuvent aussi servir à
    afficher des publicités pertinentes aux visiteurs ou - si vous vendez en ligne - à renforcer des secteurs clés de
    votre système d'achat en ligne.</p>

<p>26 Mai 2012 - L'ICO a apporté un changement à la loi, le "consentement implicite" est maintenant autorisé tant qu'on
    indique clairement où se trouve sa politique de vie privée/cookies.</p>

