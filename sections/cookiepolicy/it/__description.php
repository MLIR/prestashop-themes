<h2 class="item6">Informativa sui Cookie</h2>

<p>I cookie sono dei piccoli file di testo che un sito web crea sul computer di un visitatore. Vengono utilizzati
    tipicamente per identificare quello specifico visitatore allo scopo di fornirgli un'esperienza di navigazione
    migliore. I Cookie sono un elemento importantissimo per il corretto funzionamento di tante funzioni dei siti web che
    tutti ormai diamo per scontate.</p>

<p>Ad esempio, sono solitamente proprio i cookie che permettono ai negozi online di ricordare quali articoli hai messo
    nel tuo carrello. Sono usati anche per tenere attivo il tuo login sul sito, o per dare ai gestori del sito
    informazioni statistiche importanti.</p>

<p>Se gestisci un sito web, esso quasi sicuramente usa i cookie. La loro funzione più probabile è quella di monitorare
    il numero dei visitatori e i loro comportamenti, servendosi di strumenti come Google Analytics. Possono essere usati
    anche per mostrare agli utenti delle pubblicità il più possibile rilevanti, oppure - per chi vende prodotti - per
    far funzionare alcuni tra gli elementi più importanti del sistema di compravendita del tuo negozio online.</p>

<p>26 maggio 2012 - La ICO ha pubblicato una modifica all'applicazione della regolamentazione: "il consenso implicito" è
    ora consentito purché sia ben chiaro dove si trovano le tue informative su cookie e privacy.</p>

