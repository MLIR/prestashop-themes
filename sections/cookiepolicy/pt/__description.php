<h2 class="item6">Cookies Policy</h2>

<h5>Sua configuração é muito simples.</h5>

<ol class="index-list">
    <li>Encontre-o na pasta manual_install dentro do pacote do tema (se você realizou a Instalação manual com dados de
        amostra, então o módulo será adicionado automaticamente). </li>

    <li>
        <p>Encontre-o na lista de módulos no painel administrativo (módulo Cookieslaw em módulos da "Fachada (Front
            Office Features)") e clique em
            <strong>instalar</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/cookies.png" alt="">
        </figure>
    </li>
    <li>
        <p>Após a instalação (ignore esse passo se você foi direcionado para a página de configuração) clique em
            <strong>Configurar (Configure)</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/cookies_conf.png" alt="">
        </figure>
    </li>
    <li>
        <p>Adicione as configurações necessárias e clique em <strong>Salvar (Save)</strong>. </p>

        <figure class="img-polaroid">
            <img src="img/cookies_set.png" alt="">
        </figure>
    </li>
</ol>
<h5>Configurações:</h5>
<ul class="marked-list">
    <li>Display at the top of the page(by default). Se marcar esta opção, o módulo será mostrado no topo da
        página. </li>

    <li>Cookies policy link, link para Política de Cookies.</li>

    <li>Link para a página onde o usuário pode ser enviado quando não aceitar a Política de Cookies. </li>
</ul>

