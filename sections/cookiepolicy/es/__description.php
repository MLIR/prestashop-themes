<h2 class="item6">Pol&iacute;tica de cookies</h2>

<p>Las cookies son peque&ntilde;os archivos de texto que los sitios web colocan en los ordenadores de los visitantes.
    Por lo general son utilizados para identificar a un visitante concreto y proporcionarle una mejor experiencia. Las
    cookies son una parte fundamental de muchas funciones del sitio web.</p>

<p>Por ejemplo, las cookies permiten a las tiendas en l&iacute;nea recordar los elementos que tiene en su carrito de la
    compra. Tambi&eacute;n se utilizan para mantenerle conectado a un sitio web, o para mostrar estad&iacute;sticas de
    uso e informaci&oacute;n valiososa para los propietarios/administradores de los sitios web.</p>

<p>Si accede un sitio web, con total seguridad est&aacute; utilizando cookies. Su funci&oacute;n m&aacute;s probable es
    controlar el n&uacute;mero de visitantes y sus comportamientos mediante herramientas como Google Analytics. Tambi&eacute;n
    se pueden utilizar para mostrar anuncios personalizados a los visitantes, o - si tiene una tienda en l&iacute;nea -
    para mejorar las piezas clave de su sistema de compras en l&iacute;nea.</p>

<p>26 de mayo 2012 - El ICO lanz&oacute; un cambio en la solicitud, el "consentimiento impl&iacute;cito" no est&aacute;
    permitido, debe dejar claro donde se encuentra su pol&iacute;tica de privacidad/cookie.</p>

