<h2 class="item6">Çerez Politikası</h2>

<p>Çerezler, websiteler tarafından ziyaretçilerin bilgisayarına yerleştirilen küçük dosyalardır. Ziyaretçiyi tanımlamak
    ve onlara daha kolay bir kullanım sağlamak için hazırlanmışlardır. Çerezler, sitelerin özümsenmiş olan önemli bir
    parçasıdır.</p>

<p>Örneğin, sanal mağaza sitelerinde sepetinize eklediğiniz ürünlerin kaydı çerezler tarafından tutulur. Ayrıca siteye
    kayıtlı üye olarak giriş yapabilmenizi de sağlarlar. Aynı zamanda site sahiplerine kullanıcı istatistiklerini ve
    gerekli bilgiler de sağlarlar.</p>

<p>Bir websiteniz varsa, mutlaka çerez kullanıyorsunuzdur. Ziyaretçi sayısı ve Google Analytics gibi araçlarla
    ziyaretçilerin davranışları hakkında bilgiler, çerezler aracılığıyla kaydedilir. Ayrıca ziyaretçilere reklam
    gösterilmesini de sağlarlar. Online mağazanız varsa, sisteminizin sorunsuz olarak çalışması ve verimli olması
    çerezleri sizin için vazgeçilmez kılmaktadır.</p>

<p>26 Mayıs 2012 - ICO, Çerez politikasında bir değişiklik yaptı. Buna göre gizlilik/çerez politikanızın tam olarak
    nerede yer aldığını göstermeniz gerekmekte.</p>

