<h2 class="item6">Polityka Cookies </h2>

<p>Pliki cookies to małe pliki tekstowe, jakie strony internetowe umieszczają na komputerach odwiedzających. Są one
    zazwyczaj używane do identyfikacji poszczególnych gości i zapewniają im lepsze doświadczenie. Cookies przedstawiają
    istotną część wielu funkcji witryny, które przyjmujemy za rzecz oczywistą. </p>

<p>Na przykład, są to zazwyczaj pliki cookies, które umożliwiają sklepom internetowym zapamiętanie, jakie przedmioty
    masz w koszyku. Są one również wykorzystywane do utrzymania użytkownika zalogowanego na stronie internetowej lub
    dostarczenia cennych statystyk użytkowania oraz informacji dla właścicieli stron internetowych.</p>

<p>Jeśli prowadzisz stronę internetową, to prawie na pewno używa ona cookies. Ich najczęstszą funkcją jest monitorowanie
    liczby turystów i zachowań za pomocą narzędzi Google Analytics. Mogą być również używane do wyświetlania reklam dla
    użytkowników. Cookies również wspomagają kluczowe części Twojego systemu handlu on-line.</p>

<p>26 maja 2012 - ICO opublikowała zmiany w polityce Cookies. Począwszy od tego momentu, "domniemana zgoda" jest
    dozwolona pod warunkiem, że jasno określisz Twoją prywatność / Politykę Cookies.</p>

