<h3>How to change info about your store</h3>

<p>Open the PrestaShop admin panel, go to <strong>Shop Parameters > Contact</strong> and open the tab <strong>Stores</strong>.</p>
<p>First you'll see the list of stores in <strong>Stores</strong> section. Here you can enable/disable, add/delete or edit each store.</p>

<figure class="img-polaroid">
    <img src="img/change-store-info-1.png" alt=""/>
</figure>

<h4>How to change store name</h4>
<p>In <strong>Contact Details</strong> block you can change the main info about your store - its name (1) and other contact details. Don't forget to press the 'Save' button (2) after editing.</p>

<figure class="img-polaroid">
    <img src="img/change-store-info-2.png" alt=""/>
</figure>

<p>To edit the info on one particular store, choose the store you want to edit in <strong>Store Contacts</strong> and click 'Edit' (1).</p>

<figure class="img-polaroid">
    <img src="img/change-store-info-3.png" alt=""/>
</figure>

<p>Here you can edit all info concerning your store contacts (such as store's name, address, status, picture, working schedule etc.)<br>
After it's done, click the 'Save' button (1).</p>

