<h3>How to change info about your store</h3>

<p>Open the PrestaShop admin panel and go to <strong>Preferences > Store Contacts</strong>.</p>
<p>First you'll see the list of stores in <strong>Store Contacts</strong> section. Here you can enable/disable, add/delete or edit each store.</p>

<figure class="img-polaroid">
    <img src="img/10-shop_info-1.png" alt=""/>
</figure>

<h4>Store mapping</h4>
<p>Below the list of stores there's a <strong>Parameters</strong> section where you can edit the main settings concerning the mapping of your store. Change them according to your needs and press 'Save' button (1).</p>

<figure class="img-polaroid">
    <img src="img/10-shop_info-2.png" alt=""/>
</figure>

<h4>How to change store name</h4>
<p>In <strong>Contact Details</strong> block you can change the main info about your store - its name (1) and other contact details. Don't forget to press the 'Save' button (2) after editing.</p>

<figure class="img-polaroid">
    <img src="img/10-shop_info-3.png" alt=""/>
</figure>

<p>To edit the info on one particular store, choose the store you want to edit in <strong>Store Contacts</strong> and click 'Edit' (1).</p>

<figure class="img-polaroid">
    <img src="img/10-shop_info-4.png" alt=""/>
</figure>

<p>Here you can edit all info concerning your store contacts (such as store's name, address, status, picture, working schedule etc.)<br>
After it's done, click the 'Save' button (1).</p>

