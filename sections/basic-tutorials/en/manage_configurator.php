<h3>How to manage the Theme Configurator module</h3>

<p>This tutorial is going to show you how to manage the Theme Configurator module on your Prestashop 1.6 template.</p>

<p>The module handles a lot of things on your theme. Mainly, it enables you to switch some features on and off (social sharing buttons, top banner, payment logos, etc.), and perhaps more importantly, it lets you easily add/change images on your home-page.</p>
<ul class="index-list">
	<li>
		<span>Log into your Prestashop admin panel and go to <strong>Modules and services</strong> (1) -> <strong>Modules and services</strong> (2). </span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-1.png" alt=""/>
		</figure>
	</li>

	<li>
		<span>Then find 'Theme Configurator' module in <strong>Modules list</strong> (1). It will show up in the right column. If it is not installed yet, click its 'Install' button. <br>
		Once the module is installed, the 'Install' button will be changed to the 'Configure' button (2) which you need to click to configure the settings.</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-2.png" alt=""/>
		</figure>
	</li>

	<li>
		<span>On the settings configuration page, you can see different blocks which you can switch on and off by clicking 'Yes' and 'No' buttons (1). Click 'Configure' (2) to configure the settings of a particular block:</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-3.png" alt=""/>
		</figure>

		<ul class="marked-list">
			<li>
				<span><strong>Display links to your store's social accounts</strong>: enter web addresses for the icons you want to display in this block and click 'Save' (1) when you are done.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-4.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Display your contact information</strong>: enter your company name, address, phone number and email in the corresponding fields and click 'Save' (1).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-5.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Display social sharing buttons on the product's page</strong>: displays Facebook, Twitter, Google, Pinterest buttons on your product pages.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-6.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Display the Facebook block on the home page</strong>: displays 'Follow us on Facebook' block.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-7.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Display the custom CMS information block</strong>: allows you to add one and more info blocks for each language of your site. On the configuration page, click '+' at the top right (1) to add a new custom CMS block. Enter your text in the text field and click 'Save'. Or click 'Edit' (2) to change any block.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-8.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Display quick view window on homepage and category pages</strong></span>
			</li>
			<li>
				<span><strong>Display categories as a list of products instead of the default grid-based display</strong></span>
			</li>
			<li>
				<span><strong>Display top banner</strong>: Click the 'Configure' button. In 'Settings' you can choose another banner (1), change its link and description. After editing click 'Save' button (2).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-9.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Display logos of available payment methods</strong>: allows you to upload your image and add a title/link for the payment logos on the product page. Click 'Add file' (1) to upload your own file for the payment logo or insert a link with the image to 'Image link' field (2). Then click 'Save' button (3).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-10.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Display Live Configurator</strong>: only you can see it on the Front Page of your site when enabled.</span>
			</li>
			<li>
				<span><strong>Display subcategories</strong></span>
			</li>
			<li>
				<span>Scroll down to <strong>change/remove/add html content items</strong>. The default items that initially come with this module can be removed only when the Theme Configurator module is disabled or you can just unpublish them by unchecking 'Active' on the item configuration page.  Enter the item title,  hook it to one of the positions suggested (home, top, left, right, footer), click 'Browse' in order to upload an image, manually enter the image width/height values in pixels, specify  the URL this item will be pointed to and enter your content in the 'HTML' filed. Then click 'Save'. </span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-11.png" alt=""/>
				</figure>
				<span>If you are running a multilingual site, click the corresponding language tab and repeat the same adjustments for it.  Click the 'Add Item' button to add a new item.</span>
			</li>
		</ul>

	</li>

	<li>
		<span>Open the front end of your site to see the changes.</span>
	</li>

</ul>