<h3>How to edit slider</h3>

<p>First find the <strong>Image slider for your homepage</strong> module in <strong>Modules list</strong> of <strong>Modules and services</strong> menu tab of your PrestaShop store (1) (it is located
    in the <strong>Front Office Features</strong> modules section). Then press the 'Configure' button (2) to edit the module.
</p>

<figure class="img-polaroid">
    <img src="img/12-slider-1.png" alt=""/>
</figure>

<p>On the screen that appears next you'll be able to set the main properties of your slider like maximum width of images, speed of changing slides, the delay between two slides and autoplay option. <br>
After you've changed it, press the 'Save' button (1).</p>

<figure class="img-polaroid">
    <img src="img/12-slider-2.png" alt=""/>
</figure>

<p>To add a new slide find the '+' button in the top right corner of Slides list section (1) and click it. <br>
If you want to edit the existing slide, click the 'Edit' button (2).</p>

<figure class="img-polaroid">
    <img src="img/12-slider-3.png" alt=""/>
</figure>

<p>Further process is quite simple. In 'Slide Information' section fill all the required fields and 'Save' (1) it.</p>

