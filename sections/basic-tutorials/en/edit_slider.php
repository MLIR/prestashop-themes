<h3>How to edit slider</h3>

<p>First navigate to Modules -> Modules and Services in the admin panel of your PrestaShop store (1) and open the tab 'Installed modules'. Then find the <strong>Image slider</strong> module and press the 'Configure' button (2) to edit the module.
</p>

<figure class="img-polaroid">
    <img src="img/image-slider-1.png" alt=""/>
</figure>

<p>On the screen that appears next you'll be able to set the main properties of your slider like speed of changing slides, the pause on hover and loop option.<br>
After you've changed it, press the 'Save' button (1).</p>

<figure class="img-polaroid">
    <img src="img/image-slider-2.png" alt=""/>
</figure>

<p>To add a new slide find the '+' button in the top right corner of Slides list section (1) and click it. <br>
If you want to edit the existing slide, click the 'Edit' button (2).</p>

<figure class="img-polaroid">
    <img src="img/image-slider-3.png" alt=""/>
</figure>

<p>Further process is quite simple. In 'Slide Information' section fill all the required fields and 'Save' (1) it.</p>

